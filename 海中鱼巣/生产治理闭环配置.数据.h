// 文件规则：生产治理闭环配置只保存固定值式请求材料；不得保存权威句柄、仓库或生命周期旧版本。
#pragma once

#include "生产运行期配置.数据.h"

#include <array>
#include <cstddef>
#include <cstdint>

namespace 海中鱼巣 {

inline constexpr std::uint32_t 生产治理闭环配置当前版本 = 1;
inline constexpr std::uint64_t 生产治理闭环稳定键前缀 = 0x4859'0003'0000'0000ULL;

struct 生产治理闭环配置 {
    std::uint32_t 配置版本 = 生产治理闭环配置当前版本;
    系统角色稳定键用途 目标根角色 = 系统角色稳定键用途::安全根需求;
    std::uint64_t 任务幂等主键 = 生产治理闭环稳定键前缀 + 1;
    std::uint64_t 任务虚拟存在幂等主键 = 生产治理闭环稳定键前缀 + 2;
    std::uint64_t 任务初始状态幂等主键 = 生产治理闭环稳定键前缀 + 3;
    std::uint64_t 已承接幂等材料编号 = 生产治理闭环稳定键前缀 + 4;
    std::uint64_t 筹办中幂等材料编号 = 生产治理闭环稳定键前缀 + 5;
    std::uint64_t 方法幂等主键 = 生产治理闭环稳定键前缀 + 6;
    std::uint64_t 方法虚拟存在幂等主键 = 生产治理闭环稳定键前缀 + 7;
    std::uint64_t 方法角色状态幂等主键 = 生产治理闭环稳定键前缀 + 8;
    std::uint64_t 方法结果幂等主键 = 生产治理闭环稳定键前缀 + 9;
    std::uint64_t 方法结果角色状态幂等主键 = 生产治理闭环稳定键前缀 + 10;
    std::uint64_t 方法动作角色状态幂等主键 = 生产治理闭环稳定键前缀 + 12;
    std::uint64_t 方法动作入口状态幂等主键 = 生产治理闭环稳定键前缀 + 13;
    std::uint64_t 选择记录幂等主键 = 生产治理闭环稳定键前缀 + 14;
    std::uint64_t 选择幂等材料编号 = 生产治理闭环稳定键前缀 + 15;
    std::uint64_t 执行请求编号 = 生产治理闭环稳定键前缀 + 16;
    std::uint64_t 执行请求幂等键 = 生产治理闭环稳定键前缀 + 17;
    std::uint64_t 排队生命周期幂等主键 = 生产治理闭环稳定键前缀 + 18;
    std::uint64_t 执行生命周期幂等主键 = 生产治理闭环稳定键前缀 + 19;
    std::uint64_t 待重筹办幂等主键 = 生产治理闭环稳定键前缀 + 20;
    std::uint64_t 实际状态幂等主键 = 生产治理闭环稳定键前缀 + 21;
    std::uint64_t 动态幂等主键 = 生产治理闭环稳定键前缀 + 22;
    std::uint64_t 回执编号 = 生产治理闭环稳定键前缀 + 23;
    std::uint64_t 回执幂等键 = 生产治理闭环稳定键前缀 + 24;
    std::uint32_t 排序规则版本 = 1;
    std::uint32_t 召回请求规则版本 = 1;
    std::uint64_t 方法规格来源版本 = 1;
    std::int64_t 方法动作入口状态值 = 1;
    std::uint64_t 任务序号 = 1;
    std::uint64_t 批次编号 = 1100;
    std::uint64_t 任务创建时间戳 = 10'000;
    std::uint64_t 已承接时间戳 = 10'010;
    std::uint64_t 筹办中时间戳 = 10'020;
    std::uint64_t 截止时间戳 = 10'200;
    std::uint64_t 排队时间戳 = 10'030;
    std::uint64_t 执行时间戳 = 10'040;
    std::uint64_t 待重筹办时间戳 = 10'100;
    std::uint64_t 实际状态时间戳 = 10'050;
    std::uint64_t 回执时间戳 = 10'060;
    std::uint64_t 生产动作键 = 生产运行期稳定动作键;

    std::array<std::uint64_t, 23> 读取稳定键组() const noexcept {
        return {
            任务幂等主键, 任务虚拟存在幂等主键, 任务初始状态幂等主键,
            已承接幂等材料编号, 筹办中幂等材料编号,
            方法幂等主键, 方法虚拟存在幂等主键, 方法角色状态幂等主键,
            方法结果幂等主键, 方法结果角色状态幂等主键,
            方法动作角色状态幂等主键, 方法动作入口状态幂等主键,
            选择记录幂等主键,
            选择幂等材料编号, 执行请求编号, 执行请求幂等键,
            排队生命周期幂等主键, 执行生命周期幂等主键,
            待重筹办幂等主键, 实际状态幂等主键, 动态幂等主键,
            回执编号, 回执幂等键};
    }

    bool 有效() const noexcept {
        if (配置版本 != 生产治理闭环配置当前版本
            || 目标根角色 != 系统角色稳定键用途::安全根需求
            || 排序规则版本 == 0 || 召回请求规则版本 == 0
            || 方法规格来源版本 == 0
            || 方法动作入口状态值 < 1 || 方法动作入口状态值 > 6
            || 任务序号 == 0 || 批次编号 != 1100
            || 生产动作键 != 生产运行期稳定动作键
            || 任务创建时间戳 == 0 || 任务创建时间戳 > 已承接时间戳
            || 已承接时间戳 > 筹办中时间戳
            || 筹办中时间戳 > 排队时间戳
            || 排队时间戳 > 执行时间戳
            || 执行时间戳 > 实际状态时间戳
            || 实际状态时间戳 > 回执时间戳
            || 截止时间戳 < 任务创建时间戳
            || 待重筹办时间戳 < 排队时间戳) {
            return false;
        }
        const auto 键组 = 读取稳定键组();
        for (std::size_t 左 = 0; 左 < 键组.size(); ++左) {
            if (键组[左] == 0 || 键组[左] == 生产运行期稳定动作键) return false;
            for (std::size_t 右 = 左 + 1; 右 < 键组.size(); ++右) {
                if (键组[左] == 键组[右]) return false;
            }
        }
        const auto 运行期 = 读取生产运行期固定配置();
        for (const auto 键 : 键组) {
            for (const auto 系统键 : 运行期.系统角色.读取稳定键组()) {
                if (键 == 系统键) return false;
            }
            for (const auto 活动键 : 运行期.概念活动.读取稳定键组()) {
                if (键 == 活动键) return false;
            }
        }
        return true;
    }
};

inline 生产治理闭环配置 读取生产治理闭环固定配置() noexcept {
    return {};
}

}
