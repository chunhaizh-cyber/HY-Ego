enum class 关系状态变更状态 : std::uint32_t {
    未定义 = 0,
    已变更 = 1,
    已在目标状态 = 2,
    无效句柄 = 3,
    状态转换不允许 = 4,
    版本已耗尽 = 5,
    内部不一致 = 6
};

struct 关系状态变更材料 {
    关系状态变更状态 状态 = 关系状态变更状态::未定义;
    关系句柄 原关系;
    关系句柄 当前关系;
    关系类型 类型 = 关系类型::普通父子;
    节点句柄 源节点;
    节点句柄 目标节点;
    记录状态 原状态 = 记录状态::无效;
    记录状态 当前状态 = 记录状态::无效;

    bool 完整() const {
        if ((状态 != 关系状态变更状态::已变更
                && 状态 != 关系状态变更状态::已在目标状态)
            || !句柄有效(原关系)
            || !句柄有效(当前关系)
            || !句柄有效(源节点)
            || !句柄有效(目标节点)
            || 原关系.仓库编号 != 当前关系.仓库编号
            || 原关系.关系编号 != 当前关系.关系编号
            || 当前状态 != 记录状态::已失效) {
            return false;
        }
        if (状态 == 关系状态变更状态::已变更) {
            return 原状态 == 记录状态::有效
                && 原关系.版本号 != std::numeric_limits<std::uint32_t>::max()
                && 当前关系.版本号 == 原关系.版本号 + 1;
        }
        return (原状态 == 记录状态::有效
                && 原关系.版本号 != std::numeric_limits<std::uint32_t>::max()
                && 当前关系.版本号 == 原关系.版本号 + 1)
            || (原状态 == 记录状态::已失效
                && 当前关系 == 原关系);
    }
};

enum class 节点挂载结果状态 : std::uint32_t {
    已创建 = 1,
    已重挂 = 2,
    已在目标父 = 3,
    无效端点 = 4,
    拒绝自身 = 5,
    拒绝成环 = 6,
    内部不一致 = 7
};

struct 节点挂载结果 {
    节点挂载结果状态 状态 = 节点挂载结果状态::无效端点;
    节点句柄 节点;
    std::optional<节点句柄> 旧父节点;
    节点句柄 新父节点;
    std::optional<关系句柄> 父关系;

    bool 关系已确定() const {
        if (!句柄有效(节点) || !句柄有效(新父节点) || 节点 == 新父节点 || !父关系.has_value()) {
            return false;
        }
        if (状态 == 节点挂载结果状态::已创建) {
            return !旧父节点.has_value();
        }
        if (状态 == 节点挂载结果状态::已重挂) {
            return 旧父节点.has_value() && !(旧父节点.value() == 新父节点);
        }
        return 状态 == 节点挂载结果状态::已在目标父
            && 旧父节点 == std::optional<节点句柄>{新父节点};
    }
};

struct 结构化节点挂载结果 {
    结构写入结果 操作;
    节点挂载结果 挂载;
    std::optional<已发布关系变更能力> 能力;

    bool 成功() const noexcept {
        return 操作.成功() && 挂载.关系已确定();
    }
};

struct 节点关系失效准备包 {
    节点句柄 目标;
    std::vector<关系记录> 关系组;
    std::uint64_t 写集身份 = 0;

    bool 完整() const {
        if (!句柄有效(目标) || 写集身份 == 0) return false;
        for (std::size_t 索引 = 0; 索引 < 关系组.size(); ++索引) {
            const auto& 关系 = 关系组[索引];
            if (关系.状态 != 记录状态::有效
                || (关系.源节点 != 目标 && 关系.目标节点 != 目标)) return false;
            for (std::size_t 已有 = 0; 已有 < 索引; ++已有) {
                if (关系组[已有].关系编号 == 关系.关系编号) return false;
            }
        }
        return true;
    }
};

