// 文件规则：事务状态是结构写入边界状态，不代表需求、任务或方法结果。
#include "写入事务.h"

namespace 海中鱼巣 {

void 写入事务::开始() {
    if (状态_ == 事务状态::未开始 || 状态_ == 事务状态::已放弃) {
        状态_ = 事务状态::已开始;
    }
}

bool 写入事务::提交() {
    if (状态_ != 事务状态::已开始) {
        return false;
    }
    状态_ = 事务状态::已提交;
    return true;
}

void 写入事务::放弃() {
    if (状态_ == 事务状态::已开始) {
        状态_ = 事务状态::已放弃;
    }
}

事务状态 写入事务::当前状态() const {
    return 状态_;
}

}
