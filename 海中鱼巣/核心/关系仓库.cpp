namespace {

bool 关系类型已定义(关系类型 类型) {
    return 类型 >= 关系类型::普通父子 && 类型 <= 关系类型::概念定义材料;
}

}

关系仓库::关系仓库(const 节点仓库& 节点, std::uint64_t 仓库编号)
    : 节点_(节点), 仓库编号_(仓库编号 == 0 ? 1 : 仓库编号) {
}

关系句柄 关系仓库::创建关系(关系类型 类型, 节点句柄 源节点, 节点句柄 目标节点, std::int64_t 顺序号) {
    const bool 关系类型有效 = 关系类型已定义(类型);
    if (!关系类型有效) {
        海中鱼巣容错检查(关系类型有效, L"创建关系收到未定义关系类型。");
        return {};
    }
    const bool 关系端点句柄有效 = 句柄有效(源节点) && 句柄有效(目标节点);
    if (!关系端点句柄有效) {
        海中鱼巣容错检查(关系端点句柄有效, L"创建关系收到无效源节点或目标节点。");
        return {};
    }
    const bool 关系端点节点有效 = 节点_.节点是否有效(源节点) && 节点_.节点是否有效(目标节点);
    if (!关系端点节点有效) {
        海中鱼巣容错检查(关系端点节点有效, L"创建关系收到不存在或已删除的源节点或目标节点。");
        return {};
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    for (const auto& 条目 : 关系表_) {
        const 关系记录& 现有 = 条目.second;
        if (现有.状态 != 记录状态::有效 || 现有.类型 != 类型) {
            continue;
        }
        if (现有.源节点 == 源节点 && 现有.目标节点 == 目标节点) {
            return {};
        }
        if (类型 == 关系类型::普通父子 && 现有.目标节点 == 目标节点) {
            return {};
        }
    }
    const std::uint64_t 编号 = 下个关系编号_++;
    关系记录 记录;
    记录.关系编号 = 编号;
    记录.类型 = 类型;
    记录.源节点 = 源节点;
    记录.目标节点 = 目标节点;
    记录.顺序号 = 顺序号;
    记录.版本号 = 1;
    记录.状态 = 记录状态::有效;
    关系表_.emplace(编号, 记录);
    return 关系句柄{仓库编号_, 编号, 记录.版本号};
}

std::optional<关系记录> 关系仓库::读取关系(关系句柄 关系) const {
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()) {
        return std::nullopt;
    }
    const 关系记录& 记录 = 位置->second;
    if (关系.仓库编号 != 仓库编号_ || 关系.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return std::nullopt;
    }
    if (!节点_.节点是否有效(记录.源节点) || !节点_.节点是否有效(记录.目标节点)) {
        return std::nullopt;
    }
    return 记录;
}

bool 关系仓库::删除关系(关系句柄 关系) {
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()) {
        return false;
    }
    关系记录& 记录 = 位置->second;
    if (关系.仓库编号 != 仓库编号_ || 关系.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return false;
    }
    记录.状态 = 记录状态::已删除;
    ++记录.版本号;
    return true;
}

bool 关系仓库::重挂关系(关系句柄 关系, 节点句柄 新源节点, 节点句柄 新目标节点) {
    const bool 新端点句柄有效 = 句柄有效(新源节点) && 句柄有效(新目标节点);
    if (!新端点句柄有效) {
        海中鱼巣容错检查(新端点句柄有效, L"重挂关系收到无效新源节点或新目标节点。");
        return false;
    }
    const bool 新端点节点有效 = 节点_.节点是否有效(新源节点) && 节点_.节点是否有效(新目标节点);
    if (!新端点节点有效) {
        海中鱼巣容错检查(新端点节点有效, L"重挂关系收到不存在或已删除的新源节点或新目标节点。");
        return false;
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()) {
        return false;
    }
    关系记录& 记录 = 位置->second;
    if (关系.仓库编号 != 仓库编号_ || 关系.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return false;
    }
    记录.源节点 = 新源节点;
    记录.目标节点 = 新目标节点;
    ++记录.版本号;
    return true;
}

bool 关系仓库::重挂节点(节点句柄 节点, 节点句柄 新父节点) {
    const bool 重挂节点输入有效 = 节点_.节点是否有效(节点) && 节点_.节点是否有效(新父节点);
    if (!重挂节点输入有效) {
        海中鱼巣容错检查(重挂节点输入有效, L"重挂节点收到不存在或已删除的节点。");
        return false;
    }
    const bool 新父不是自身 = !(节点 == 新父节点);
    if (!新父不是自身) {
        海中鱼巣容错检查(新父不是自身, L"重挂节点不能把节点移动到自身下面。");
        return false;
    }
    普通父关系复核材料 写前父关系;
    父链复核结果 写前父链结果 = 父链复核结果::未包含目标;
    std::vector<节点句柄> 写前父链节点组;
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        写前父关系 = 复核普通父关系_已加锁(节点);
        写前父链结果 = 复核父链_已加锁(新父节点, 节点, &写前父链节点组);
    }
    const bool 写前父关系端点有效 = !写前父关系.父节点.has_value()
        || 节点_.节点是否有效(写前父关系.父节点.value());
    const bool 写前父链端点有效 = std::all_of(
        写前父链节点组.begin(),
        写前父链节点组.end(),
        [&](const 节点句柄& 父链节点) { return 节点_.节点是否有效(父链节点); });
    if (写前父关系.结构不一致
        || 写前父链结果 == 父链复核结果::结构不一致
        || !写前父关系端点有效
        || !写前父链端点有效) {
        (void)追根因检查(false, L"重挂节点写前发现普通父关系或父链结构不一致。");
        return false;
    }
    bool 已重挂 = false;
    bool 结构不一致 = false;
    {
        std::unique_lock<std::shared_mutex> 锁(仓库锁_);
        const auto 当前父关系 = 复核普通父关系_已加锁(节点);
        const auto 父链结果 = 复核父链_已加锁(新父节点, 节点);
        if (当前父关系.结构不一致 || 父链结果 == 父链复核结果::结构不一致) {
            结构不一致 = true;
        } else if (父链结果 != 父链复核结果::包含目标
            && 当前父关系.关系编号.has_value()
            && 当前父关系.父节点.has_value()
            && !(当前父关系.父节点.value() == 新父节点)) {
            auto 位置 = 关系表_.find(当前父关系.关系编号.value());
            if (位置 == 关系表_.end()
                || 位置->second.版本号 == std::numeric_limits<std::uint32_t>::max()) {
                结构不一致 = true;
            } else {
                关系记录& 记录 = 位置->second;
                记录.源节点 = 新父节点;
                ++记录.版本号;
                已重挂 = 记录.状态 == 记录状态::有效
                    && 记录.类型 == 关系类型::普通父子
                    && 记录.源节点 == 新父节点
                    && 记录.目标节点 == 节点;
                结构不一致 = !已重挂;
            }
        }
    }
    if (结构不一致) {
        (void)追根因检查(false, L"重挂节点锁内复核或原地写入出现结构不一致。");
    }
    return 已重挂;
}

节点挂载结果 关系仓库::挂载或重挂节点(节点句柄 节点, 节点句柄 新父节点) {
    if (!节点_.节点是否有效(节点) || !节点_.节点是否有效(新父节点)) {
        return {节点挂载结果状态::无效端点, 节点, std::nullopt, 新父节点, std::nullopt};
    }
    if (节点 == 新父节点) {
        return {节点挂载结果状态::拒绝自身, 节点, std::nullopt, 新父节点, std::nullopt};
    }

    普通父关系复核材料 写前父关系;
    父链复核结果 写前父链结果 = 父链复核结果::未包含目标;
    std::vector<节点句柄> 写前父链节点组;
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        写前父关系 = 复核普通父关系_已加锁(节点);
        写前父链结果 = 复核父链_已加锁(新父节点, 节点, &写前父链节点组);
    }
    const bool 写前父关系端点有效 = !写前父关系.父节点.has_value()
        || 节点_.节点是否有效(写前父关系.父节点.value());
    const bool 写前父链端点有效 = std::all_of(
        写前父链节点组.begin(),
        写前父链节点组.end(),
        [&](const 节点句柄& 父链节点) { return 节点_.节点是否有效(父链节点); });
    if (写前父关系.结构不一致
        || 写前父链结果 == 父链复核结果::结构不一致
        || !写前父关系端点有效
        || !写前父链端点有效) {
        (void)追根因检查(false, L"普通父子挂载写前发现多父、悬空端点或既有环。");
        return {
            节点挂载结果状态::内部不一致,
            节点,
            写前父关系.父节点,
            新父节点,
            std::nullopt};
    }

    节点挂载结果 结果{
        节点挂载结果状态::内部不一致,
        节点,
        std::nullopt,
        新父节点,
        std::nullopt};
    {
        std::unique_lock<std::shared_mutex> 锁(仓库锁_);
        const auto 当前父关系 = 复核普通父关系_已加锁(节点);
        const auto 父链结果 = 复核父链_已加锁(新父节点, 节点);
        结果.旧父节点 = 当前父关系.父节点;

        if (当前父关系.结构不一致 || 父链结果 == 父链复核结果::结构不一致) {
            结果.状态 = 节点挂载结果状态::内部不一致;
        } else if (当前父关系.父节点 == std::optional<节点句柄>{新父节点}) {
            if (父链结果 == 父链复核结果::包含目标 || !当前父关系.关系编号.has_value()) {
                结果.状态 = 节点挂载结果状态::内部不一致;
            } else {
                const auto 位置 = 关系表_.find(当前父关系.关系编号.value());
                if (位置 == 关系表_.end()) {
                    结果.状态 = 节点挂载结果状态::内部不一致;
                } else {
                    结果.状态 = 节点挂载结果状态::已在目标父;
                    结果.父关系 = 关系句柄{仓库编号_, 位置->second.关系编号, 位置->second.版本号};
                }
            }
        } else if (父链结果 == 父链复核结果::包含目标) {
            结果.状态 = 节点挂载结果状态::拒绝成环;
        } else if (!当前父关系.关系编号.has_value()) {
            if (下个关系编号_ == 0 || 下个关系编号_ == std::numeric_limits<std::uint64_t>::max()) {
                结果.状态 = 节点挂载结果状态::内部不一致;
            } else {
                const std::uint64_t 编号 = 下个关系编号_++;
                关系记录 记录;
                记录.关系编号 = 编号;
                记录.类型 = 关系类型::普通父子;
                记录.源节点 = 新父节点;
                记录.目标节点 = 节点;
                记录.版本号 = 1;
                记录.状态 = 记录状态::有效;
                const auto 插入结果 = 关系表_.emplace(编号, 记录);
                if (!插入结果.second
                    || 插入结果.first->second.源节点 != 新父节点
                    || 插入结果.first->second.目标节点 != 节点) {
                    结果.状态 = 节点挂载结果状态::内部不一致;
                } else {
                    结果.状态 = 节点挂载结果状态::已创建;
                    结果.父关系 = 关系句柄{仓库编号_, 编号, 1};
                }
            }
        } else {
            const auto 位置 = 关系表_.find(当前父关系.关系编号.value());
            if (位置 == 关系表_.end()
                || 位置->second.版本号 == std::numeric_limits<std::uint32_t>::max()) {
                结果.状态 = 节点挂载结果状态::内部不一致;
            } else {
                关系记录& 记录 = 位置->second;
                记录.源节点 = 新父节点;
                ++记录.版本号;
                if (记录.状态 != 记录状态::有效
                    || 记录.类型 != 关系类型::普通父子
                    || 记录.源节点 != 新父节点
                    || 记录.目标节点 != 节点) {
                    结果.状态 = 节点挂载结果状态::内部不一致;
                } else {
                    结果.状态 = 节点挂载结果状态::已重挂;
                    结果.父关系 = 关系句柄{仓库编号_, 记录.关系编号, 记录.版本号};
                }
            }
        }
    }
    if (结果.状态 == 节点挂载结果状态::内部不一致) {
        (void)追根因检查(false, L"普通父子挂载或重挂的锁内结构不一致。");
    }
    return 结果;
}

bool 关系仓库::节点在父链中(节点句柄 起点, 节点句柄 目标) const {
    if (!节点_.节点是否有效(起点) || !节点_.节点是否有效(目标)) {
        return false;
    }
    父链复核结果 结果 = 父链复核结果::未包含目标;
    std::vector<节点句柄> 父链节点组;
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        结果 = 复核父链_已加锁(起点, 目标, &父链节点组);
    }
    const bool 父链端点有效 = std::all_of(
        父链节点组.begin(),
        父链节点组.end(),
        [&](const 节点句柄& 父链节点) { return 节点_.节点是否有效(父链节点); });
    if (结果 == 父链复核结果::结构不一致 || !父链端点有效) {
        (void)追根因检查(false, L"读取普通父链时发现多父、悬空端点或既有环。");
        return false;
    }
    return 结果 == 父链复核结果::包含目标;
}

关系仓库::普通父关系复核材料 关系仓库::复核普通父关系_已加锁(节点句柄 子节点) const {
    普通父关系复核材料 结果;
    for (const auto& 条目 : 关系表_) {
        const 关系记录& 记录 = 条目.second;
        if (记录.状态 != 记录状态::有效
            || 记录.类型 != 关系类型::普通父子
            || !(记录.目标节点 == 子节点)) {
            continue;
        }
        if (结果.关系编号.has_value()) {
            结果.结构不一致 = true;
            continue;
        }
        结果.关系编号 = 记录.关系编号;
        结果.父节点 = 记录.源节点;
    }
    return 结果;
}

关系仓库::父链复核结果 关系仓库::复核父链_已加锁(
    节点句柄 起点,
    节点句柄 目标,
    std::vector<节点句柄>* 父链节点组) const {
    std::vector<节点句柄> 已经过节点{起点};
    节点句柄 当前节点 = 起点;
    const std::uint64_t 最大检查数量 = static_cast<std::uint64_t>(关系表_.size()) + 1;
    for (std::uint64_t 已检查数量 = 0; 已检查数量 < 最大检查数量; ++已检查数量) {
        const auto 当前父关系 = 复核普通父关系_已加锁(当前节点);
        if (当前父关系.结构不一致) {
            return 父链复核结果::结构不一致;
        }
        if (!当前父关系.父节点.has_value()) {
            return 父链复核结果::未包含目标;
        }
        const 节点句柄 当前父节点 = 当前父关系.父节点.value();
        if (父链节点组 != nullptr) {
            父链节点组->push_back(当前父节点);
        }
        if (当前父节点 == 目标) {
            return 父链复核结果::包含目标;
        }
        if (std::find(已经过节点.begin(), 已经过节点.end(), 当前父节点) != 已经过节点.end()) {
            return 父链复核结果::结构不一致;
        }
        已经过节点.push_back(当前父节点);
        当前节点 = 当前父节点;
    }
    return 父链复核结果::结构不一致;
}

std::vector<节点句柄> 关系仓库::获取子节点(节点句柄 父节点) const {
    std::vector<节点句柄> 子节点候选;
    if (!节点_.节点是否有效(父节点)) {
        return 子节点候选;
    }
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        for (const auto& 条目 : 关系表_) {
            const 关系记录& 记录 = 条目.second;
            if (记录.状态 == 记录状态::有效
                && 记录.类型 == 关系类型::普通父子
                && 记录.源节点 == 父节点) {
                子节点候选.push_back(记录.目标节点);
            }
        }
    }
    std::vector<节点句柄> 子节点;
    子节点.reserve(子节点候选.size());
    for (const auto& 候选 : 子节点候选) {
        if (节点_.节点是否有效(候选)) {
            子节点.push_back(候选);
        }
    }
    return 子节点;
}

std::optional<节点句柄> 关系仓库::获取父节点(节点句柄 子节点) const {
    if (!节点_.节点是否有效(子节点)) {
        return std::nullopt;
    }
    普通父关系复核材料 结果;
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        结果 = 复核普通父关系_已加锁(子节点);
    }
    if (结果.结构不一致) {
        (void)追根因检查(false, L"读取普通父节点时发现多父或悬空端点。");
        return std::nullopt;
    }
    if (结果.父节点.has_value() && !节点_.节点是否有效(结果.父节点.value())) {
        (void)追根因检查(false, L"读取普通父节点时发现悬空父节点。");
        return std::nullopt;
    }
    return 结果.父节点;
}

