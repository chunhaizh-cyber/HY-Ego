std::optional<节点关系失效准备包> 关系仓库::准备节点关系失效包(
    节点句柄 节点,
    const std::vector<关系记录>& 精确当前关系,
    const 结构事务令牌& 令牌) const {
    if (!验证独占令牌(事务接线_, 令牌)
        || !节点_.节点是否有效(节点, 令牌)
        || 令牌.许可序号 == 0) return std::nullopt;
    std::vector<关系记录> 当前关系;
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        当前关系.reserve(精确当前关系.size());
        for (const auto& 条目 : 关系表_) {
            const auto& 记录 = 条目.second;
            if (记录.状态 == 记录状态::有效
                && (记录.源节点 == 节点 || 记录.目标节点 == 节点)) {
                当前关系.push_back(记录);
            }
        }
    }
    const auto 按编号 = [](const 关系记录& 左, const 关系记录& 右) {
        return 左.关系编号 < 右.关系编号;
    };
    std::sort(当前关系.begin(), 当前关系.end(), 按编号);
    auto 预期关系 = 精确当前关系;
    std::sort(预期关系.begin(), 预期关系.end(), 按编号);
    if (当前关系.size() != 预期关系.size()) return std::nullopt;
    for (std::size_t 索引 = 0; 索引 < 当前关系.size(); ++索引) {
        const auto& 当前 = 当前关系[索引];
        const auto& 预期 = 预期关系[索引];
        if (当前.关系编号 != 预期.关系编号
            || 当前.版本号 != 预期.版本号
            || 当前.类型 != 预期.类型
            || 当前.源节点 != 预期.源节点
            || 当前.目标节点 != 预期.目标节点
            || 当前.状态 != 预期.状态) return std::nullopt;
    }
    节点关系失效准备包 包{节点, std::move(当前关系), 令牌.许可序号};
    return 包.完整() ? std::optional<节点关系失效准备包>{std::move(包)} : std::nullopt;
}

void 关系仓库::提交节点关系失效包(
    const 节点关系失效准备包& 包,
    const 结构事务令牌& 令牌,
    const 概念安全删除提交会话& 会话) {
    if (!验证独占令牌(事务接线_, 令牌)
        || !包.完整()
        || !会话.有效()
        || 会话.读取运行期状态() != 事务接线_.运行期状态
        || 会话.读取目标() != 包.目标
        || 会话.读取写集身份() != 包.写集身份
        || 包.写集身份 != 令牌.许可序号
        || 会话.读取阶段() != 概念安全删除提交阶段::关系) {
        throw std::logic_error("概念安全删除关系提交能力不匹配");
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    for (const auto& 预期 : 包.关系组) {
        const auto 位置 = 关系表_.find(预期.关系编号);
        if (位置 == 关系表_.end()
            || 位置->second.版本号 != 预期.版本号
            || 位置->second.状态 != 记录状态::有效
            || 位置->second.类型 != 预期.类型
            || 位置->second.源节点 != 预期.源节点
            || 位置->second.目标节点 != 预期.目标节点
            || 位置->second.版本号 == std::numeric_limits<std::uint32_t>::max()) {
            throw std::logic_error("概念安全删除关系提交前结构漂移");
        }
    }
    for (const auto& 预期 : 包.关系组) {
        auto& 记录 = 关系表_.find(预期.关系编号)->second;
        记录.状态 = 记录状态::已失效;
        ++记录.版本号;
    }
}
