仓库权威导出结果<关系仓库权威材料> 关系仓库::导出权威状态(
    const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌)) {
        return {仓库权威导出状态::入口拒绝, {}};
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    if (仓库编号_ == 0 || 下个关系编号_ == 0) {
        return {仓库权威导出状态::内部不一致, {}};
    }
    关系仓库权威材料 材料{仓库编号_, 下个关系编号_, {}};
    try {
        材料.记录组.reserve(关系表_.size());
        for (const auto& [编号, 记录] : 关系表_) {
            if (编号 == 0 || 记录.关系编号 != 编号 || 编号 >= 下个关系编号_
                || !关系类型已定义(记录.类型) || !句柄有效(记录.源节点)
                || !句柄有效(记录.目标节点) || 记录.版本号 == 0
                || !仓库记录状态可导出(记录.状态)) {
                return {仓库权威导出状态::内部不一致, {}};
            }
            材料.记录组.push_back({记录.关系编号, 记录.类型, 记录.源节点,
                记录.目标节点, 记录.顺序号, 记录.版本号, 记录.状态});
        }
    } catch (...) {
        return {仓库权威导出状态::资源失败, {}};
    }
    return {仓库权威导出状态::已形成, std::move(材料)};
}

