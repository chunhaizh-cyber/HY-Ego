结构写入结果 关系仓库::严格删除关系(
    关系句柄 关系,
    const 结构事务令牌& 令牌) {
    结构写入结果 结果{结构写入状态::入口拒绝, 关系.关系编号, 关系.版本号, 0};
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (!句柄有效(关系) || 关系.仓库编号 != 仓库编号_) {
        return 结果;
    }

    关系记录 快照;
    {
        std::shared_lock<std::shared_mutex> 锁(仓库锁_);
        const auto 位置 = 关系表_.find(关系.关系编号);
        if (位置 == 关系表_.end()) return 结果;
        快照 = 位置->second;
    }
    结果.当前版本 = 快照.版本号;
    if (关系.版本号 != 快照.版本号) {
        const bool 是已提交删除 = 快照.状态 == 记录状态::已删除
            && 关系.版本号 != std::numeric_limits<std::uint32_t>::max()
            && 关系.版本号 + 1 == 快照.版本号;
        结果.状态 = 是已提交删除 ? 结构写入状态::幂等读回 : 结构写入状态::版本漂移;
        return 结果;
    }
    if (快照.状态 == 记录状态::已删除) {
        结果.状态 = 结构写入状态::幂等读回;
        return 结果;
    }
    if (快照.状态 != 记录状态::有效) return 结果;
    if (!节点_.节点是否有效(快照.源节点, 令牌)
        || !节点_.节点是否有效(快照.目标节点, 令牌)
        || 快照.版本号 == std::numeric_limits<std::uint32_t>::max()) {
        结果.状态 = 结构写入状态::内部不一致;
        return 结果;
    }

    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()
        || 位置->second.版本号 != 快照.版本号
        || 位置->second.状态 != 快照.状态
        || 位置->second.类型 != 快照.类型
        || 位置->second.源节点 != 快照.源节点
        || 位置->second.目标节点 != 快照.目标节点) {
        结果.状态 = 结构写入状态::内部不一致;
        return 结果;
    }
    位置->second.状态 = 记录状态::已删除;
    ++位置->second.版本号;
    结果.状态 = 结构写入状态::已提交;
    结果.当前版本 = 位置->second.版本号;
    return 结果;
}
