// 文件规则：关系仓库只维护结构关系，不通过日志或显示文本表达机器事实。
#include "关系仓库.h"
#include "容错检查.h"

namespace 海中鱼巣 {

关系仓库::关系仓库(const 节点仓库& 节点, std::uint64_t 仓库编号)
    : 节点_(节点), 仓库编号_(仓库编号 == 0 ? 1 : 仓库编号) {
}

关系句柄 关系仓库::创建关系(关系类型 类型, 节点句柄 源节点, 节点句柄 目标节点, std::int64_t 顺序号) {
    const bool 关系端点句柄有效 = 句柄有效(源节点) && 句柄有效(目标节点);
    if (!关系端点句柄有效) {
        海中鱼巣容错检查(关系端点句柄有效, L"创建关系收到无效源节点或目标节点。");
        return {};
    }
    const bool 关系端点节点有效 = 节点_.节点是否有效(源节点) && 节点_.节点是否有效(目标节点);
    if (!关系端点节点有效) {
        海中鱼巣容错检查(关系端点节点有效, L"创建关系收到不存在或已删除的源节点或目标节点。");
        return {};
    }
    const std::uint64_t 编号 = 下个关系编号_++;
    关系记录 记录;
    记录.关系编号 = 编号;
    记录.类型 = 类型;
    记录.源节点 = 源节点;
    记录.目标节点 = 目标节点;
    记录.顺序号 = 顺序号;
    记录.版本号 = 1;
    记录.状态 = 记录状态::有效;
    关系表_.emplace(编号, 记录);
    return 关系句柄{仓库编号_, 编号, 记录.版本号};
}

std::optional<关系记录> 关系仓库::读取关系(关系句柄 关系) const {
    const auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()) {
        return std::nullopt;
    }
    const 关系记录& 记录 = 位置->second;
    if (关系.仓库编号 != 仓库编号_ || 关系.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return std::nullopt;
    }
    return 记录;
}

bool 关系仓库::删除关系(关系句柄 关系) {
    auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()) {
        return false;
    }
    关系记录& 记录 = 位置->second;
    if (关系.仓库编号 != 仓库编号_ || 关系.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return false;
    }
    记录.状态 = 记录状态::已删除;
    ++记录.版本号;
    return true;
}

bool 关系仓库::重挂关系(关系句柄 关系, 节点句柄 新源节点, 节点句柄 新目标节点) {
    const bool 新端点句柄有效 = 句柄有效(新源节点) && 句柄有效(新目标节点);
    if (!新端点句柄有效) {
        海中鱼巣容错检查(新端点句柄有效, L"重挂关系收到无效新源节点或新目标节点。");
        return false;
    }
    const bool 新端点节点有效 = 节点_.节点是否有效(新源节点) && 节点_.节点是否有效(新目标节点);
    if (!新端点节点有效) {
        海中鱼巣容错检查(新端点节点有效, L"重挂关系收到不存在或已删除的新源节点或新目标节点。");
        return false;
    }
    auto 位置 = 关系表_.find(关系.关系编号);
    if (位置 == 关系表_.end()) {
        return false;
    }
    关系记录& 记录 = 位置->second;
    if (关系.仓库编号 != 仓库编号_ || 关系.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return false;
    }
    记录.源节点 = 新源节点;
    记录.目标节点 = 新目标节点;
    ++记录.版本号;
    return true;
}

bool 关系仓库::重挂节点(节点句柄 节点, 节点句柄 新父节点) {
    const bool 重挂节点输入有效 = 节点_.节点是否有效(节点) && 节点_.节点是否有效(新父节点);
    if (!重挂节点输入有效) {
        海中鱼巣容错检查(重挂节点输入有效, L"重挂节点收到不存在或已删除的节点。");
        return false;
    }
    for (auto& 条目 : 关系表_) {
        关系记录& 记录 = 条目.second;
        if (记录.状态 == 记录状态::有效 && 记录.类型 == 关系类型::普通父子 && 记录.目标节点 == 节点) {
            记录.源节点 = 新父节点;
            ++记录.版本号;
            return true;
        }
    }
    return false;
}

std::vector<节点句柄> 关系仓库::获取子节点(节点句柄 父节点) const {
    std::vector<节点句柄> 子节点;
    for (const auto& 条目 : 关系表_) {
        const 关系记录& 记录 = 条目.second;
        if (记录.状态 == 记录状态::有效 && 记录.类型 == 关系类型::普通父子 && 记录.源节点 == 父节点) {
            子节点.push_back(记录.目标节点);
        }
    }
    return 子节点;
}

std::optional<节点句柄> 关系仓库::获取父节点(节点句柄 子节点) const {
    for (const auto& 条目 : 关系表_) {
        const 关系记录& 记录 = 条目.second;
        if (记录.状态 == 记录状态::有效 && 记录.类型 == 关系类型::普通父子 && 记录.目标节点 == 子节点) {
            return 记录.源节点;
        }
    }
    return std::nullopt;
}

std::optional<节点句柄> 关系仓库::获取目标节点(节点句柄 源节点, 关系类型 类型) const {
    if (!节点_.节点是否有效(源节点)) {
        return std::nullopt;
    }
    for (const auto& 条目 : 关系表_) {
        const 关系记录& 记录 = 条目.second;
        if (记录.状态 == 记录状态::有效 && 记录.类型 == 类型 && 记录.源节点 == 源节点) {
            return 记录.目标节点;
        }
    }
    return std::nullopt;
}

}
