节点仓库::节点仓库(const 主信息仓库& 主信息, std::uint64_t 仓库编号, 结构事务接线 接线)
    : 主信息_(主信息), 仓库编号_(仓库编号 == 0 ? 1 : 仓库编号), 事务接线_(std::move(接线)) {
    if (!事务接线_.接线形态有效() || !主信息_.事务接线_.接线形态有效()) {
        throw std::invalid_argument("节点仓库只接受全空未接域或完整结构事务接线");
    }
    if (!接线一致(事务接线_, 主信息_.事务接线_)) {
        throw std::invalid_argument("节点仓库与主信息仓库必须同时未接域或使用同一结构事务域");
    }
}

节点句柄 节点仓库::创建节点(节点类型 类型, 主信息句柄 主信息) {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() ? 创建节点(类型, 主信息, 许可.读取令牌()) : 节点句柄{};
    }
    if (!主信息_.主信息是否有效(主信息) || !节点类型已定义(类型)) {
        return {};
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const std::uint64_t 编号 = 下个节点编号_++;
    节点记录 记录;
    记录.节点编号 = 编号;
    记录.类型 = 类型;
    记录.主信息 = 主信息;
    记录.版本号 = 1;
    记录.状态 = 记录状态::有效;
    记录.创建序号 = 下个创建序号_++;
    节点表_.emplace(编号, 记录);
    return 节点句柄{仓库编号_, 编号, 记录.版本号};
}

节点句柄 节点仓库::创建节点(节点类型 类型, 主信息句柄 主信息, const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌) || !主信息_.主信息是否有效(主信息, 令牌) || !节点类型已定义(类型)) return {};
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const std::uint64_t 编号 = 下个节点编号_++;
    节点记录 记录{编号, 类型, 主信息, 1, 记录状态::有效, 下个创建序号_++};
    节点表_.emplace(编号, 记录);
    return {仓库编号_, 编号, 记录.版本号};
}

带值结构写入结果<节点未发布候选> 节点仓库::结构化创建节点未发布候选(
    节点类型 类型,
    主信息句柄 主信息,
    const 结构事务令牌& 令牌) {
    带值结构写入结果<节点未发布候选> 结果;
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.操作.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (!句柄有效(主信息)
        || !节点类型已定义(类型)
        || !主信息_.主信息是否有效(主信息, 令牌)) {
        结果.操作 = {结构写入状态::入口拒绝, 主信息.主信息编号, 主信息.版本号, 0};
        return 结果;
    }
    auto 候选 = 创建节点未发布候选(类型, 主信息, 令牌);
    if (!候选.has_value()) {
        结果.操作 = {结构写入状态::内部不一致, 主信息.主信息编号, 主信息.版本号, 主信息.版本号};
        return 结果;
    }
    const auto 节点 = 候选->读取节点();
    结果.操作 = {结构写入状态::已提交, 节点.节点编号, 节点.版本号, 节点.版本号};
    结果.值.emplace(std::move(*候选));
    return 结果;
}

std::optional<节点未发布候选> 节点仓库::创建节点未发布候选(
    节点类型 类型,
    主信息句柄 主信息,
    const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)
        || !主信息_.主信息是否有效(主信息, 令牌)
        || !节点类型已定义(类型)) {
        return std::nullopt;
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const std::uint64_t 编号 = 下个节点编号_++;
    节点记录 记录{编号, 类型, 主信息, 1, 记录状态::有效, 下个创建序号_++};
    节点表_.emplace(编号, 记录);
    return 节点未发布候选{this, 令牌, {仓库编号_, 编号, 记录.版本号}};
}

核心未发布候选操作状态 节点仓库::确认未发布候选(
    节点未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)
        || 候选.仓库_ != this
        || !令牌一致(候选.令牌_, 令牌)
        || 候选.阶段_ != 节点未发布候选::阶段::未发布) {
        return 核心未发布候选操作状态::已拒绝;
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(候选.节点_.节点编号);
    if (位置 == 节点表_.end()
        || 候选.节点_.仓库编号 != 仓库编号_
        || 候选.节点_.版本号 != 位置->second.版本号
        || 位置->second.状态 != 记录状态::有效) {
        return 核心未发布候选操作状态::内部不一致;
    }
    候选.阶段_ = 节点未发布候选::阶段::已确认;
    return 核心未发布候选操作状态::已完成;
}

结构写入结果 节点仓库::结构化确认未发布候选(
    节点未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    const auto 节点 = 候选.读取节点();
    结构写入结果 结果{结构写入状态::入口拒绝, 节点.节点编号, 节点.版本号, 节点.版本号};
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (候选.仓库_ != this || !令牌一致(候选.令牌_, 令牌)) {
        return 结果;
    }
    if (候选.阶段_ == 节点未发布候选::阶段::已确认) {
        结果.状态 = 结构写入状态::候选已确认;
        return 结果;
    }
    if (候选.阶段_ == 节点未发布候选::阶段::已撤销) {
        结果.状态 = 结构写入状态::候选已撤销;
        结果.当前版本 = 0;
        return 结果;
    }
    const auto 状态 = 确认未发布候选(候选, 令牌);
    结果.状态 = 状态 == 核心未发布候选操作状态::已完成
        ? 结构写入状态::已提交
        : 状态 == 核心未发布候选操作状态::内部不一致
            ? 结构写入状态::内部不一致
            : 结构写入状态::入口拒绝;
    return 结果;
}

核心未发布候选操作状态 节点仓库::撤销未发布候选(
    节点未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)
        || 候选.仓库_ != this
        || !令牌一致(候选.令牌_, 令牌)) {
        return 核心未发布候选操作状态::已拒绝;
    }
    if (候选.阶段_ == 节点未发布候选::阶段::已撤销) {
        return 核心未发布候选操作状态::幂等已撤销;
    }
    if (候选.阶段_ != 节点未发布候选::阶段::未发布
        && 候选.阶段_ != 节点未发布候选::阶段::已确认) {
        return 核心未发布候选操作状态::已拒绝;
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(候选.节点_.节点编号);
    if (位置 == 节点表_.end()
        || 候选.节点_.仓库编号 != 仓库编号_
        || 候选.节点_.版本号 != 位置->second.版本号
        || 位置->second.状态 != 记录状态::有效) {
        return 核心未发布候选操作状态::内部不一致;
    }
    节点表_.erase(位置);
    候选.阶段_ = 节点未发布候选::阶段::已撤销;
    return 核心未发布候选操作状态::已完成;
}

结构写入结果 节点仓库::结构化撤销未发布候选(
    节点未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    const auto 节点 = 候选.读取节点();
    结构写入结果 结果{结构写入状态::入口拒绝, 节点.节点编号, 节点.版本号, 节点.版本号};
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (候选.仓库_ != this || !令牌一致(候选.令牌_, 令牌)) {
        return 结果;
    }
    if (候选.阶段_ == 节点未发布候选::阶段::已撤销) {
        结果.状态 = 结构写入状态::候选已撤销;
        结果.当前版本 = 0;
        return 结果;
    }
    const auto 状态 = 撤销未发布候选(候选, 令牌);
    结果.状态 = 状态 == 核心未发布候选操作状态::已完成
        ? 结构写入状态::已提交
        : 状态 == 核心未发布候选操作状态::幂等已撤销
            ? 结构写入状态::候选已撤销
            : 状态 == 核心未发布候选操作状态::内部不一致
                ? 结构写入状态::内部不一致
                : 结构写入状态::入口拒绝;
    if (结果.成功()) 结果.当前版本 = 0;
    return 结果;
}

std::optional<节点记录> 节点仓库::读取节点(节点句柄 节点) const {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() ? 读取节点(节点, 许可.读取令牌()) : std::nullopt;
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end()) {
        return std::nullopt;
    }
    const 节点记录& 记录 = 位置->second;
    if (节点.仓库编号 != 仓库编号_ || 节点.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return std::nullopt;
    }
    return 记录;
}

std::optional<节点记录> 节点仓库::读取节点(节点句柄 节点, const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌)) return std::nullopt;
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end() || 节点.仓库编号 != 仓库编号_ || 节点.版本号 != 位置->second.版本号 || 位置->second.状态 != 记录状态::有效) return std::nullopt;
    return 位置->second;
}

std::optional<节点记录> 节点仓库::读取节点审计(节点句柄 节点) const {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() ? 读取节点审计(节点, 许可.读取令牌()) : std::nullopt;
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end() || 节点.仓库编号 != 仓库编号_) return std::nullopt;
    const auto& 记录 = 位置->second;
    const bool 版本匹配 = 节点.版本号 == 记录.版本号
        || (记录.状态 == 记录状态::已删除
            && 节点.版本号 != std::numeric_limits<std::uint32_t>::max()
            && 节点.版本号 + 1 == 记录.版本号);
    return 版本匹配 ? std::optional<节点记录>{记录} : std::nullopt;
}

std::optional<节点记录> 节点仓库::读取节点审计(
    节点句柄 节点,
    const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌)) return std::nullopt;
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end() || 节点.仓库编号 != 仓库编号_) return std::nullopt;
    const auto& 记录 = 位置->second;
    const bool 版本匹配 = 节点.版本号 == 记录.版本号
        || (记录.状态 == 记录状态::已删除
            && 节点.版本号 != std::numeric_limits<std::uint32_t>::max()
            && 节点.版本号 + 1 == 记录.版本号);
    return 版本匹配 ? std::optional<节点记录>{记录} : std::nullopt;
}

std::optional<节点删除准备包> 节点仓库::准备节点删除包(
    节点句柄 节点,
    const 结构事务令牌& 令牌) const {
    if (!验证独占令牌(事务接线_, 令牌)) return std::nullopt;
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end()
        || 节点.仓库编号 != 仓库编号_
        || 节点.版本号 != 位置->second.版本号
        || 位置->second.状态 != 记录状态::有效
        || 令牌.许可序号 == 0) return std::nullopt;
    return 节点删除准备包{节点, 位置->second.主信息, 位置->second.版本号, 令牌.许可序号};
}

void 节点仓库::提交节点删除包(
    const 节点删除准备包& 包,
    const 结构事务令牌& 令牌,
    const 概念安全删除提交会话& 会话) {
    if (!验证独占令牌(事务接线_, 令牌)
        || !包.完整()
        || !会话.有效()
        || 会话.读取运行期状态() != 事务接线_.运行期状态
        || 会话.读取目标() != 包.目标
        || 会话.读取写集身份() != 包.写集身份
        || 包.写集身份 != 令牌.许可序号
        || 会话.读取阶段() != 概念安全删除提交阶段::节点) {
        throw std::logic_error("概念安全删除节点提交能力不匹配");
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(包.目标.节点编号);
    if (位置 == 节点表_.end()
        || 包.目标.仓库编号 != 仓库编号_
        || 位置->second.版本号 != 包.预期版本
        || 位置->second.主信息 != 包.主信息
        || 位置->second.状态 != 记录状态::有效
        || 位置->second.版本号 == std::numeric_limits<std::uint32_t>::max()) {
        throw std::logic_error("概念安全删除节点提交前结构漂移");
    }
    位置->second.状态 = 记录状态::已删除;
    ++位置->second.版本号;
}

bool 节点仓库::删除节点(节点句柄 节点) {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得独占许可(事务接线_.运行期状态);
        return 许可.有效() && 删除节点(节点, 许可.读取令牌());
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end()) {
        return false;
    }
    节点记录& 记录 = 位置->second;
    if (节点.仓库编号 != 仓库编号_ || 节点.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return false;
    }
    记录.状态 = 记录状态::已删除;
    ++记录.版本号;
    return true;
}

bool 节点仓库::删除节点(节点句柄 节点, const 结构事务令牌& 令牌) {
    if (!验证独占令牌(事务接线_, 令牌)) return false;
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end()) return false;
    节点记录& 记录 = 位置->second;
    if (节点.仓库编号 != 仓库编号_ || 节点.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) return false;
    记录.状态 = 记录状态::已删除;
    ++记录.版本号;
    return true;
}

节点记录组读取结果 节点仓库::读取指定类型有效节点记录组(
    节点类型 类型,
    const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌) || !节点类型已定义(类型)) {
        return {节点记录组读取状态::入口拒绝, {}};
    }
#ifdef HY_EGO_ENABLE_STRUCTURE_COMMIT_FAULT_SELF_TEST
    if (下一次节点记录组资源失败_.exchange(false)) {
        return {节点记录组读取状态::资源失败, {}};
    }
#endif
    节点记录组读取结果 输出{节点记录组读取状态::已形成, {}};
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    try {
        输出.记录组.reserve(节点表_.size());
        for (const auto& [节点编号, 记录] : 节点表_) {
            if (记录.状态 != 记录状态::有效) continue;
            if (节点编号 == 0 || 记录.节点编号 != 节点编号
                || !节点类型已定义(记录.类型) || !句柄有效(记录.主信息)
                || 记录.版本号 == 0 || 记录.创建序号 == 0) {
                return {节点记录组读取状态::内部不一致, {}};
            }
            if (记录.类型 != 类型) continue;
            输出.记录组.push_back(记录);
        }
        std::sort(输出.记录组.begin(), 输出.记录组.end(), [](const 节点记录& 左, const 节点记录& 右) {
            return 左.节点编号 < 右.节点编号;
        });
    } catch (...) {
        return {节点记录组读取状态::资源失败, {}};
    }
    return 输出;
}

#ifdef HY_EGO_ENABLE_STRUCTURE_COMMIT_FAULT_SELF_TEST
void 节点仓库::自检注入下一次节点记录组资源失败() {
    下一次节点记录组资源失败_.store(true);
}
#endif

结构写入结果 节点仓库::严格删除节点(
    节点句柄 节点,
    const 结构事务令牌& 令牌) {
    结构写入结果 结果{结构写入状态::入口拒绝, 节点.节点编号, 节点.版本号, 0};
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (!句柄有效(节点) || 节点.仓库编号 != 仓库编号_) {
        return 结果;
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 节点表_.find(节点.节点编号);
    if (位置 == 节点表_.end()) return 结果;
    auto& 记录 = 位置->second;
    结果.当前版本 = 记录.版本号;
    if (节点.版本号 != 记录.版本号) {
        const bool 是已提交删除 = 记录.状态 == 记录状态::已删除
            && 节点.版本号 != std::numeric_limits<std::uint32_t>::max()
            && 节点.版本号 + 1 == 记录.版本号;
        结果.状态 = 是已提交删除 ? 结构写入状态::幂等读回 : 结构写入状态::版本漂移;
        return 结果;
    }
    if (记录.状态 == 记录状态::已删除) {
        结果.状态 = 结构写入状态::幂等读回;
        return 结果;
    }
    if (记录.状态 != 记录状态::有效) return 结果;
    if (记录.版本号 == std::numeric_limits<std::uint32_t>::max()) {
        结果.状态 = 结构写入状态::内部不一致;
        return 结果;
    }
    记录.状态 = 记录状态::已删除;
    ++记录.版本号;
    结果.状态 = 结构写入状态::已提交;
    结果.当前版本 = 记录.版本号;
    return 结果;
}

bool 节点仓库::节点是否有效(节点句柄 节点) const {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() && 节点是否有效(节点, 许可.读取令牌());
    }
    return 读取节点(节点).has_value();
}

bool 节点仓库::节点是否有效(节点句柄 节点, const 结构事务令牌& 令牌) const {
    return 读取节点(节点, 令牌).has_value();
}

std::uint64_t 节点仓库::有效节点数量() const {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() ? 有效节点数量(许可.读取令牌()) : 0;
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    std::uint64_t 数量 = 0;
    for (const auto& 条目 : 节点表_) {
        if (条目.second.状态 == 记录状态::有效) {
            ++数量;
        }
    }
    return 数量;
}

std::uint64_t 节点仓库::有效节点数量(const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌)) return 0;
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    std::uint64_t 数量 = 0;
    for (const auto& 条目 : 节点表_) if (条目.second.状态 == 记录状态::有效) ++数量;
    return 数量;
}

std::uint64_t 节点仓库::仓库编号() const {
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    return 仓库编号_;
}

仓库权威导出结果<节点仓库权威材料> 节点仓库::导出权威状态(
    const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌)) {
        return {仓库权威导出状态::入口拒绝, {}};
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    if (仓库编号_ == 0 || 下个节点编号_ == 0 || 下个创建序号_ == 0) {
        return {仓库权威导出状态::内部不一致, {}};
    }
    节点仓库权威材料 材料{仓库编号_, 下个节点编号_, 下个创建序号_, {}};
    try {
        材料.记录组.reserve(节点表_.size());
        for (const auto& [编号, 记录] : 节点表_) {
            if (编号 == 0 || 记录.节点编号 != 编号 || 编号 >= 下个节点编号_
                || !节点类型已定义(记录.类型) || !句柄有效(记录.主信息)
                || 记录.版本号 == 0 || !仓库记录状态可导出(记录.状态)
                || 记录.创建序号 == 0 || 记录.创建序号 >= 下个创建序号_) {
                return {仓库权威导出状态::内部不一致, {}};
            }
            材料.记录组.push_back({记录.节点编号, 记录.类型, 记录.主信息,
                记录.版本号, 记录.状态, 记录.创建序号});
        }
    } catch (...) {
        return {仓库权威导出状态::资源失败, {}};
    }
    return {仓库权威导出状态::已形成, std::move(材料)};
}

