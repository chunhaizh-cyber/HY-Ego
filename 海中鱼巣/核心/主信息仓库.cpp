主信息仓库::主信息仓库(std::uint64_t 仓库编号, 结构事务接线 接线)
    : 仓库编号_(仓库编号 == 0 ? 1 : 仓库编号), 事务接线_(std::move(接线)) {
}

主信息句柄 主信息仓库::创建主信息() {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() ? 创建主信息(许可.读取令牌()) : 主信息句柄{};
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const std::uint64_t 编号 = 下个主信息编号_++;
    主信息记录 记录;
    记录.主信息编号 = 编号;
    记录.版本号 = 1;
    记录.状态 = 记录状态::有效;
    主信息表_.emplace(编号, 记录);
    return 主信息句柄{仓库编号_, 编号, 记录.版本号};
}

主信息句柄 主信息仓库::创建主信息(const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)) return {};
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const std::uint64_t 编号 = 下个主信息编号_++;
    主信息记录 记录{编号, 1, 记录状态::有效};
    主信息表_.emplace(编号, 记录);
    return {仓库编号_, 编号, 记录.版本号};
}

带值结构写入结果<主信息未发布候选> 主信息仓库::结构化创建主信息未发布候选(
    const 结构事务令牌& 令牌) {
    带值结构写入结果<主信息未发布候选> 结果;
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.操作.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    auto 候选 = 创建主信息未发布候选(令牌);
    if (!候选.has_value()) {
        结果.操作.状态 = 结构写入状态::内部不一致;
        return 结果;
    }
    const auto 主信息 = 候选->读取主信息();
    结果.操作 = {结构写入状态::已提交, 主信息.主信息编号, 主信息.版本号, 主信息.版本号};
    结果.值.emplace(std::move(*候选));
    return 结果;
}

std::optional<主信息未发布候选> 主信息仓库::创建主信息未发布候选(
    const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)) return std::nullopt;
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const std::uint64_t 编号 = 下个主信息编号_++;
    主信息记录 记录{编号, 1, 记录状态::有效};
    主信息表_.emplace(编号, 记录);
    return 主信息未发布候选{this, 令牌, {仓库编号_, 编号, 记录.版本号}};
}

核心未发布候选操作状态 主信息仓库::确认未发布候选(
    主信息未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)
        || 候选.仓库_ != this
        || !令牌一致(候选.令牌_, 令牌)
        || 候选.阶段_ != 主信息未发布候选::阶段::未发布) {
        return 核心未发布候选操作状态::已拒绝;
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 主信息表_.find(候选.主信息_.主信息编号);
    if (位置 == 主信息表_.end()
        || 候选.主信息_.仓库编号 != 仓库编号_
        || 候选.主信息_.版本号 != 位置->second.版本号
        || 位置->second.状态 != 记录状态::有效) {
        return 核心未发布候选操作状态::内部不一致;
    }
    候选.阶段_ = 主信息未发布候选::阶段::已确认;
    return 核心未发布候选操作状态::已完成;
}

结构写入结果 主信息仓库::结构化确认未发布候选(
    主信息未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    const auto 主信息 = 候选.读取主信息();
    结构写入结果 结果{结构写入状态::入口拒绝, 主信息.主信息编号, 主信息.版本号, 主信息.版本号};
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (候选.仓库_ != this || !令牌一致(候选.令牌_, 令牌)) {
        return 结果;
    }
    if (候选.阶段_ == 主信息未发布候选::阶段::已确认) {
        结果.状态 = 结构写入状态::候选已确认;
        return 结果;
    }
    if (候选.阶段_ == 主信息未发布候选::阶段::已撤销) {
        结果.状态 = 结构写入状态::候选已撤销;
        结果.当前版本 = 0;
        return 结果;
    }
    const auto 状态 = 确认未发布候选(候选, 令牌);
    结果.状态 = 状态 == 核心未发布候选操作状态::已完成
        ? 结构写入状态::已提交
        : 状态 == 核心未发布候选操作状态::内部不一致
            ? 结构写入状态::内部不一致
            : 结构写入状态::入口拒绝;
    return 结果;
}

核心未发布候选操作状态 主信息仓库::撤销未发布候选(
    主信息未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    if (!验证共享令牌(事务接线_, 令牌)
        || 候选.仓库_ != this
        || !令牌一致(候选.令牌_, 令牌)) {
        return 核心未发布候选操作状态::已拒绝;
    }
    if (候选.阶段_ == 主信息未发布候选::阶段::已撤销) {
        return 核心未发布候选操作状态::幂等已撤销;
    }
    if (候选.阶段_ != 主信息未发布候选::阶段::未发布
        && 候选.阶段_ != 主信息未发布候选::阶段::已确认) {
        return 核心未发布候选操作状态::已拒绝;
    }
    std::unique_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 主信息表_.find(候选.主信息_.主信息编号);
    if (位置 == 主信息表_.end()
        || 候选.主信息_.仓库编号 != 仓库编号_
        || 候选.主信息_.版本号 != 位置->second.版本号
        || 位置->second.状态 != 记录状态::有效) {
        return 核心未发布候选操作状态::内部不一致;
    }
    主信息表_.erase(位置);
    候选.阶段_ = 主信息未发布候选::阶段::已撤销;
    return 核心未发布候选操作状态::已完成;
}

结构写入结果 主信息仓库::结构化撤销未发布候选(
    主信息未发布候选& 候选,
    const 结构事务令牌& 令牌) {
    const auto 主信息 = 候选.读取主信息();
    结构写入结果 结果{结构写入状态::入口拒绝, 主信息.主信息编号, 主信息.版本号, 主信息.版本号};
    if (!验证独占令牌(事务接线_, 令牌)) {
        结果.状态 = 结构写入状态::许可拒绝;
        return 结果;
    }
    if (候选.仓库_ != this || !令牌一致(候选.令牌_, 令牌)) {
        return 结果;
    }
    if (候选.阶段_ == 主信息未发布候选::阶段::已撤销) {
        结果.状态 = 结构写入状态::候选已撤销;
        结果.当前版本 = 0;
        return 结果;
    }
    const auto 状态 = 撤销未发布候选(候选, 令牌);
    结果.状态 = 状态 == 核心未发布候选操作状态::已完成
        ? 结构写入状态::已提交
        : 状态 == 核心未发布候选操作状态::幂等已撤销
            ? 结构写入状态::候选已撤销
            : 状态 == 核心未发布候选操作状态::内部不一致
                ? 结构写入状态::内部不一致
                : 结构写入状态::入口拒绝;
    if (结果.成功()) 结果.当前版本 = 0;
    return 结果;
}

std::optional<主信息记录> 主信息仓库::读取主信息(主信息句柄 主信息) const {
    if (事务接线_.已接域()) { auto 许可=事务接线_.取得共享许可(事务接线_.运行期状态); return 许可.有效()?读取主信息(主信息,许可.读取令牌()):std::nullopt; }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 主信息表_.find(主信息.主信息编号);
    if (位置 == 主信息表_.end()) {
        return std::nullopt;
    }
    const 主信息记录& 记录 = 位置->second;
    if (主信息.仓库编号 != 仓库编号_ || 主信息.版本号 != 记录.版本号 || 记录.状态 != 记录状态::有效) {
        return std::nullopt;
    }
    return 记录;
}

std::optional<主信息记录> 主信息仓库::读取主信息(主信息句柄 主信息, const 结构事务令牌& 令牌) const {
    if (!验证共享令牌(事务接线_, 令牌)) return std::nullopt;
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置=主信息表_.find(主信息.主信息编号);
    if(位置==主信息表_.end()||主信息.仓库编号!=仓库编号_||主信息.版本号!=位置->second.版本号||位置->second.状态!=记录状态::有效)return std::nullopt;
    return 位置->second;
}

std::optional<主信息记录> 主信息仓库::读取主信息审计(主信息句柄 主信息) const {
    if (事务接线_.已接域()) {
        auto 许可 = 事务接线_.取得共享许可(事务接线_.运行期状态);
        return 许可.有效() ? 读取主信息审计(主信息, 许可.读取令牌()) : std::nullopt;
    }
    std::shared_lock<std::shared_mutex> 锁(仓库锁_);
    const auto 位置 = 主信息表_.find(主信息.主信息编号);
    if (位置 == 主信息表_.end() || 主信息.仓库编号 != 仓库编号_) return std::nullopt;
    const auto& 记录 = 位置->second;
    const bool 版本匹配 = 主信息.版本号 == 记录.版本号
        || (记录.状态 == 记录状态::已删除
            && 主信息.版本号 != std::numeric_limits<std::uint32_t>::max()
            && 主信息.版本号 + 1 == 记录.版本号);
    return 版本匹配 ? std::optional<主信息记录>{记录} : std::nullopt;
}

