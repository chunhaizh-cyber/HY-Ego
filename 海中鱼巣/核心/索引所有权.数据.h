// 文件规则：本文件只定义索引所有者、命名域、探测与键保留稳定值合同，不执行仓库写入。
#pragma once

#include "句柄.h"

#include <cstdint>

namespace 海中鱼巣 {

enum class 索引所有者 : std::uint16_t {
    未知兼容 = 0,
    存在场景 = 1,
    状态动态 = 2,
    特征体系 = 3,
    语素基础 = 4,
    轻量因果 = 5,
    需求任务方法 = 6,
    用途观察 = 7
};

enum class 索引键保留策略 : std::uint8_t {
    未定义 = 0,
    兼容释放 = 1,
    删除后永久保留 = 2
};

struct 索引所有者声明 {
    索引所有者 所有者 = 索引所有者::未知兼容;
    std::uint32_t 命名域 = 0;
    std::uint32_t 所有者格式版本 = 0;
    std::uint32_t 探测规则版本 = 0;
    std::uint32_t 最大探测次数 = 0;
    std::uint32_t 规范身份字节数 = 0;
    索引键保留策略 键保留策略 = 索引键保留策略::未定义;

    bool 显式完整() const noexcept {
        return 所有者 != 索引所有者::未知兼容 && 命名域 != 0
            && 所有者格式版本 != 0 && 探测规则版本 != 0 && 最大探测次数 != 0
            && 键保留策略 == 索引键保留策略::删除后永久保留;
    }

    bool 未知兼容完整() const noexcept {
        return 所有者 == 索引所有者::未知兼容 && 命名域 == 0
            && 所有者格式版本 == 1 && 探测规则版本 == 1 && 最大探测次数 == 1
            && 规范身份字节数 == 0 && 键保留策略 == 索引键保留策略::兼容释放;
    }
};

inline bool operator==(const 索引所有者声明& 左, const 索引所有者声明& 右) noexcept {
    return 左.所有者 == 右.所有者 && 左.命名域 == 右.命名域
        && 左.所有者格式版本 == 右.所有者格式版本
        && 左.探测规则版本 == 右.探测规则版本
        && 左.最大探测次数 == 右.最大探测次数
        && 左.规范身份字节数 == 右.规范身份字节数
        && 左.键保留策略 == 右.键保留策略;
}

inline bool operator!=(const 索引所有者声明& 左, const 索引所有者声明& 右) noexcept {
    return !(左 == 右);
}

inline constexpr 索引所有者声明 形成未知兼容索引所有者声明() noexcept {
    return {索引所有者::未知兼容, 0, 1, 1, 1, 0, 索引键保留策略::兼容释放};
}

inline constexpr 索引所有者声明 形成索引所有者声明(索引所有者 所有者) noexcept {
    switch (所有者) {
    case 索引所有者::存在场景:
        return {所有者, 0x4859'0001U, 1, 1, 1, 0, 索引键保留策略::删除后永久保留};
    case 索引所有者::状态动态:
        return {所有者, 0x4859'0002U, 1, 1, 1, 0, 索引键保留策略::删除后永久保留};
    case 索引所有者::特征体系:
        return {所有者, 0x4859'0003U, 1, 1, 1, 0, 索引键保留策略::删除后永久保留};
    case 索引所有者::语素基础:
        return {所有者, 0x4859'0004U, 1, 1, 1, 0, 索引键保留策略::删除后永久保留};
    case 索引所有者::轻量因果:
        return {所有者, 0x4859'0005U, 1, 1, 1, 0, 索引键保留策略::删除后永久保留};
    case 索引所有者::需求任务方法:
        return {所有者, 0x4859'0006U, 1, 1, 1, 0, 索引键保留策略::删除后永久保留};
    case 索引所有者::用途观察:
        return {所有者, 0x4F31'5332U, 1, 1, 1024, 36, 索引键保留策略::删除后永久保留};
    default:
        return 形成未知兼容索引所有者声明();
    }
}

inline bool 索引所有者声明符合规范(const 索引所有者声明& 声明) noexcept {
    return 声明.显式完整() && 声明 == 形成索引所有者声明(声明.所有者);
}

struct 索引绑定请求 {
    std::uint64_t 最终物理键 = 0;
    节点句柄 目标;
    索引所有者声明 所有者声明;
    std::uint32_t 探测序号 = 0;

    bool 显式完整() const noexcept {
        return 最终物理键 != 0 && 句柄有效(目标) && 索引所有者声明符合规范(所有者声明)
            && 探测序号 < 所有者声明.最大探测次数;
    }

    bool 兼容完整() const noexcept {
        return 最终物理键 != 0 && 句柄有效(目标)
            && (索引所有者声明符合规范(所有者声明) || 所有者声明.未知兼容完整())
            && 探测序号 < 所有者声明.最大探测次数;
    }
};

inline bool operator==(const 索引绑定请求& 左, const 索引绑定请求& 右) noexcept {
    return 左.最终物理键 == 右.最终物理键 && 左.目标 == 右.目标
        && 左.所有者声明 == 右.所有者声明 && 左.探测序号 == 右.探测序号;
}

inline 索引绑定请求 形成索引绑定请求(
    std::uint64_t 最终物理键,
    节点句柄 目标,
    索引所有者 所有者,
    std::uint32_t 探测序号 = 0) noexcept {
    return {最终物理键, 目标, 形成索引所有者声明(所有者), 探测序号};
}

inline 索引绑定请求 形成未知兼容索引绑定请求(
    std::uint64_t 最终物理键,
    节点句柄 目标) noexcept {
    return {最终物理键, 目标, 形成未知兼容索引所有者声明(), 0};
}

}
