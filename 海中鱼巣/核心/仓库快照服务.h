// 文件规则：仓库快照服务当前只承载恢复拒绝矩阵和隔离候选材料；不得写运行期仓库或执行恢复替换。
#pragma once

#include <cstdint>

namespace 海中鱼巣 {

enum class 快照恢复状态 : std::uint32_t {
    已拒绝 = 0,
    隔离候选可读 = 1
};

enum class 快照恢复拒绝原因 : std::uint32_t {
    无 = 0,
    未确认恢复专项 = 1,
    格式版本不匹配 = 2,
    句柄材料无效 = 3,
    关系材料无效 = 4,
    索引材料无效 = 5,
    隔离读回失败 = 6
};

struct 快照恢复请求 {
    bool 已确认恢复专项 = false;
    std::uint32_t 格式版本 = 0;
    bool 句柄材料通过 = false;
    bool 关系材料通过 = false;
    bool 索引材料通过 = false;
    bool 隔离读回通过 = false;
};

struct 快照恢复评估 {
    快照恢复状态 状态 = 快照恢复状态::已拒绝;
    快照恢复拒绝原因 拒绝原因 = 快照恢复拒绝原因::未确认恢复专项;
    bool 候选仓库隔离 = true;
    bool 允许替换运行期仓库 = false;
};

class 仓库快照服务 {
public:
    static constexpr std::uint32_t 当前快照格式版本 = 1;

    快照恢复评估 评估恢复请求(const 快照恢复请求& 请求) const {
        if (!请求.已确认恢复专项) {
            return 拒绝(快照恢复拒绝原因::未确认恢复专项);
        }
        if (请求.格式版本 != 当前快照格式版本) {
            return 拒绝(快照恢复拒绝原因::格式版本不匹配);
        }
        if (!请求.句柄材料通过) {
            return 拒绝(快照恢复拒绝原因::句柄材料无效);
        }
        if (!请求.关系材料通过) {
            return 拒绝(快照恢复拒绝原因::关系材料无效);
        }
        if (!请求.索引材料通过) {
            return 拒绝(快照恢复拒绝原因::索引材料无效);
        }
        if (!请求.隔离读回通过) {
            return 拒绝(快照恢复拒绝原因::隔离读回失败);
        }

        return 快照恢复评估{
            快照恢复状态::隔离候选可读,
            快照恢复拒绝原因::无,
            true,
            false
        };
    }

private:
    快照恢复评估 拒绝(快照恢复拒绝原因 原因) const {
        return 快照恢复评估{
            快照恢复状态::已拒绝,
            原因,
            true,
            false
        };
    }
};

}
