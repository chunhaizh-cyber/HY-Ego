// 文件规则：本文件只定义运行期稳定句柄和值类型，不承载业务裁决；长期身份必须使用编号和版本号。
#pragma once

#include <cstdint>

namespace 海中鱼巣 {

enum class 记录状态 : std::uint32_t {
    无效 = 0,
    有效 = 1,
    已删除 = 2,
    已失效 = 3
};

enum class 节点类型 : std::uint32_t {
    未分类 = 0,
    语素 = 1,
    基础信息 = 2,
    存在 = 3,
    场景 = 4,
    特征 = 5,
    特征值 = 6,
    需求 = 7,
    任务 = 8,
    方法 = 9,
    动态 = 10,
    因果引用 = 11,
    状态 = 12,
    二次特征 = 13,
    任务方法选择记录 = 14,
    用途观察记录 = 15
};

enum class 关系类型 : std::uint32_t {
    普通父子 = 0,
    归属 = 1,
    引用 = 2,
    索引 = 3,
    模板 = 4,
    运行期临时 = 5,
    因果来源 = 6,
    语素对应信息 = 7,
    语素概念追溯 = 8,
    实例支持概念 = 9,
    概念上下位 = 10,
    概念定义材料 = 11,
    概念生命周期 = 12,
    需求目标概念 = 13,
    任务生命周期 = 14,
    任务生命周期证据 = 15,
    任务方法选择 = 16,
    方法动作场景 = 17,
    用途观察证据角色 = 18
};

struct 节点句柄 {
    std::uint64_t 仓库编号 = 0;
    std::uint64_t 节点编号 = 0;
    std::uint32_t 版本号 = 0;
};

struct 主信息句柄 {
    std::uint64_t 仓库编号 = 0;
    std::uint64_t 主信息编号 = 0;
    std::uint32_t 版本号 = 0;
};

struct 关系句柄 {
    std::uint64_t 仓库编号 = 0;
    std::uint64_t 关系编号 = 0;
    std::uint32_t 版本号 = 0;
};

inline bool 句柄有效(const 节点句柄& 句柄) {
    return 句柄.仓库编号 != 0 && 句柄.节点编号 != 0 && 句柄.版本号 != 0;
}

inline bool 句柄有效(const 主信息句柄& 句柄) {
    return 句柄.仓库编号 != 0 && 句柄.主信息编号 != 0 && 句柄.版本号 != 0;
}

inline bool 句柄有效(const 关系句柄& 句柄) {
    return 句柄.仓库编号 != 0 && 句柄.关系编号 != 0 && 句柄.版本号 != 0;
}

inline bool operator==(const 节点句柄& 左, const 节点句柄& 右) {
    return 左.仓库编号 == 右.仓库编号 && 左.节点编号 == 右.节点编号 && 左.版本号 == 右.版本号;
}

inline bool operator==(const 主信息句柄& 左, const 主信息句柄& 右) {
    return 左.仓库编号 == 右.仓库编号 && 左.主信息编号 == 右.主信息编号 && 左.版本号 == 右.版本号;
}

inline bool operator==(const 关系句柄& 左, const 关系句柄& 右) {
    return 左.仓库编号 == 右.仓库编号 && 左.关系编号 == 右.关系编号 && 左.版本号 == 右.版本号;
}

}
