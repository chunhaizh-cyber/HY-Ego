inline constexpr std::uint32_t 状态动态类型化记录当前格式版本 = 1;

enum class 状态记录模式 : std::uint32_t {
    未指定 = 0,
    观察 = 1,
    动作结果 = 2,
    治理事实 = 3
};

enum class 动态种类 : std::uint32_t {
    未指定 = 0,
    状态迁移动能 = 1,
    动作致变 = 2,
    观察动态 = 3,
    外部事件动态 = 4,
    聚合动态 = 5
};

inline bool 状态记录模式有效(状态记录模式 模式) noexcept {
    return 模式 == 状态记录模式::观察
        || 模式 == 状态记录模式::动作结果
        || 模式 == 状态记录模式::治理事实;
}

inline bool 动态种类有效(动态种类 种类) noexcept {
    return 种类 == 动态种类::状态迁移动能
        || 种类 == 动态种类::动作致变
        || 种类 == 动态种类::观察动态
        || 种类 == 动态种类::外部事件动态
        || 种类 == 动态种类::聚合动态;
}

inline bool 已发布记录状态有效(记录状态 状态) noexcept {
    return 状态 == 记录状态::有效
        || 状态 == 记录状态::已删除
        || 状态 == 记录状态::已失效;
}

struct 状态域记录 final {
    节点句柄 所属状态;
    std::uint32_t 格式版本 = 状态动态类型化记录当前格式版本;
    std::uint64_t 记录版本 = 0;
    记录状态 状态 = 记录状态::无效;
    std::uint64_t 发生时间 = 0;
    std::optional<std::uint64_t> 接收时间;
    状态记录模式 记录模式 = 状态记录模式::未指定;

    bool 完整() const noexcept {
        return 句柄有效(所属状态)
            && 格式版本 == 状态动态类型化记录当前格式版本
            && 记录版本 != 0 && 已发布记录状态有效(状态)
            && 发生时间 != 0
            && (!接收时间.has_value() || *接收时间 >= 发生时间)
            && 状态记录模式有效(记录模式);
    }

    bool operator==(const 状态域记录&) const = default;
};

struct 动态域记录 final {
    节点句柄 所属动态;
    std::uint32_t 格式版本 = 状态动态类型化记录当前格式版本;
    std::uint64_t 记录版本 = 0;
    记录状态 状态 = 记录状态::无效;
    std::uint64_t 发生时间 = 0;
    动态种类 种类 = 动态种类::未指定;
    std::uint64_t 聚合规则版本 = 0;

    bool 完整() const noexcept {
        if (!句柄有效(所属动态)
            || 格式版本 != 状态动态类型化记录当前格式版本
            || 记录版本 == 0 || !已发布记录状态有效(状态)
            || 发生时间 == 0 || !动态种类有效(种类)) {
            return false;
        }
        return 种类 == 动态种类::聚合动态
            ? 聚合规则版本 != 0 : 聚合规则版本 == 0;
    }

    bool operator==(const 动态域记录&) const = default;
};

