struct 状态动态迁移写入结果 final {
    合同结果状态 状态 = 合同结果状态::入口拒绝;
    合同结果原因 原因 = 合同结果原因::无效身份;
    std::optional<状态动态迁移值式材料> 材料;
    std::uint64_t 状态记录结构版本 = 0;
    std::uint64_t 动态记录结构版本 = 0;

    bool 自洽() const noexcept {
        const bool 可带材料 = 合同状态携带单项材料(状态);
        return 材料.has_value() == 可带材料
            && (可带材料
                ? 状态记录结构版本 != 0 && 动态记录结构版本 != 0
                : 状态记录结构版本 == 0 && 动态记录结构版本 == 0);
    }

    bool 成功() const noexcept {
        return 合同状态携带单项材料(状态) && 材料.has_value()
            && 状态记录结构版本 != 0 && 动态记录结构版本 != 0;
    }
};

