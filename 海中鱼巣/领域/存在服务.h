// 文件规则：存在服务只创建和读取存在节点；文本、日志和外部输入不得直接成为存在事实。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"
#include "../核心/容错检查.h"

namespace 海中鱼巣 {

class 存在服务 {
public:
    存在服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系)
        : 主信息_(主信息), 节点_(节点), 关系_(关系) {
    }

    节点句柄 创建实际存在() {
        const auto 主信息句柄 = 主信息_.创建主信息();
        const auto 存在 = 节点_.创建节点(节点类型::存在, 主信息句柄);
        const auto 记录 = 读取存在(存在);
        if (!记录.has_value() || !主信息_.主信息是否有效(记录->主信息)) {
            return {};
        }
        return 存在;
    }

    节点句柄 创建存在概念根() {
        const auto 主信息句柄 = 主信息_.创建主信息();
        const auto 根节点 = 节点_.创建节点(节点类型::存在, 主信息句柄);
        const auto 记录 = 读取存在(根节点);
        if (!追根因检查(记录.has_value() && 主信息_.主信息是否有效(记录->主信息),
            L"创建存在概念根后读回身份不符合内部预期。")) {
            return {};
        }
        return 根节点;
    }

    节点句柄 创建虚拟存在(节点句柄 来源节点) {
        const auto 来源记录 = 节点_.读取节点(来源节点);
        if (!来源记录.has_value()
            || (来源记录->类型 != 节点类型::需求 && 来源记录->类型 != 节点类型::任务 && 来源记录->类型 != 节点类型::方法)) {
            return {};
        }
        const auto 虚拟存在 = 创建实际存在();
        if (!句柄有效(虚拟存在)) {
            return {};
        }
        const auto 来源关系 = 关系_.创建关系(关系类型::引用, 虚拟存在, 来源节点);
        if (!句柄有效(来源关系)) {
            return {};
        }
        return 虚拟存在;
    }

    std::optional<节点记录> 读取存在(节点句柄 存在节点) const {
        const auto 记录 = 节点_.读取节点(存在节点);
        if (!记录.has_value() || 记录->类型 != 节点类型::存在) {
            return std::nullopt;
        }
        return 记录;
    }

    bool 存在是否有效(节点句柄 存在节点) const {
        return 读取存在(存在节点).has_value();
    }

    std::optional<概念结构所有者清理授权> 形成概念节点清理授权(
        const 概念结构所有者清理请求& 请求,
        const 结构事务令牌& 令牌) const {
        if (!请求.完整() || 请求.所有者 != 概念结构所有者类型::存在服务) return std::nullopt;
        const auto 记录 = 节点_.读取节点(请求.目标, 令牌);
        if (!记录.has_value() || 记录->类型 != 节点类型::存在 || 记录->主信息 != 请求.主信息) return std::nullopt;
        概念结构所有者清理授权 授权{请求.目标, 请求.主信息, 请求.所有者, 令牌.许可序号};
        return 授权.完整() ? std::optional<概念结构所有者清理授权>{授权} : std::nullopt;
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
};

}
