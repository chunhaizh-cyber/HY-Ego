// 文件规则：存在服务只创建和读取存在节点；文本、日志和外部输入不得直接成为存在事实。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"
#include "../核心/容错检查.h"
#include "../核心/结构事务接线.数据.h"

#include <utility>

namespace 海中鱼巣 {

class 存在服务;

enum class 未发布候选操作状态 : std::uint32_t { 已完成 = 1, 幂等已撤销 = 2, 已拒绝 = 3, 内部不一致 = 4 };

class 存在未发布候选 {
public:
    存在未发布候选() = delete;
    存在未发布候选(const 存在未发布候选&) = delete;
    存在未发布候选& operator=(const 存在未发布候选&) = delete;
    存在未发布候选(存在未发布候选&&) noexcept = default;
    存在未发布候选& operator=(存在未发布候选&&) noexcept = default;
    节点句柄 读取存在() const { return 存在_; }
private:
    friend class 存在服务;
    enum class 阶段 : std::uint8_t { 未发布, 已确认, 已撤销 };
    存在未发布候选(const 存在服务* 服务, 结构事务令牌 令牌, 节点句柄 存在,
        主信息句柄 主信息, 关系句柄 来源关系)
        : 服务_(服务), 令牌_(令牌), 存在_(存在), 主信息_(主信息), 来源关系_(来源关系) {}
    const 存在服务* 服务_ = nullptr;
    结构事务令牌 令牌_;
    节点句柄 存在_;
    主信息句柄 主信息_;
    关系句柄 来源关系_;
    阶段 阶段_ = 阶段::未发布;
};

class 存在服务 {
public:
    存在服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系)
        : 主信息_(主信息), 节点_(节点), 关系_(关系) {
    }

    存在服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系, 结构事务接线 接线)
        : 主信息_(主信息), 节点_(节点), 关系_(关系), 接线_(std::move(接线)) {}

    std::optional<存在未发布候选> 创建虚拟存在候选(
        节点句柄 来源节点, const 结构事务令牌& 令牌) {
        if (!验证独占(令牌)) return std::nullopt;
        const auto 来源 = 节点_.读取节点(来源节点, 令牌);
        if (!来源 || (来源->类型 != 节点类型::需求 && 来源->类型 != 节点类型::任务
            && 来源->类型 != 节点类型::方法)) return std::nullopt;
        const auto 主信息 = 主信息_.创建主信息(令牌);
        const auto 存在 = 句柄有效(主信息)
            ? 节点_.创建节点(节点类型::存在, 主信息, 令牌) : 节点句柄{};
        const auto 来源关系 = 句柄有效(存在)
            ? 关系_.创建关系(关系类型::引用, 存在, 来源节点, 0, 令牌) : 关系句柄{};
        if (句柄有效(主信息) && 句柄有效(存在) && 句柄有效(来源关系)
            && 节点_.读取节点(存在, 令牌).has_value()) {
            return 存在未发布候选{this, 令牌, 存在, 主信息, 来源关系};
        }
        if (句柄有效(来源关系)) (void)关系_.删除关系(来源关系, 令牌);
        if (句柄有效(存在)) (void)节点_.删除节点(存在, 令牌);
        if (句柄有效(主信息)) (void)主信息_.删除主信息(主信息, 令牌);
        return std::nullopt;
    }

    未发布候选操作状态 撤销候选(存在未发布候选& 候选, const 结构事务令牌& 令牌) {
        if (候选.服务_ != this || !令牌匹配(候选.令牌_, 令牌) || !验证独占(令牌))
            return 未发布候选操作状态::已拒绝;
        if (候选.阶段_ == 存在未发布候选::阶段::已撤销) return 未发布候选操作状态::幂等已撤销;
        if (候选.阶段_ != 存在未发布候选::阶段::未发布
            || !关系_.读取关系(候选.来源关系_, 令牌).has_value()
            || !节点_.读取节点(候选.存在_, 令牌).has_value()
            || !主信息_.读取主信息(候选.主信息_, 令牌).has_value()) return 未发布候选操作状态::内部不一致;
        const bool 完成 = 关系_.删除关系(候选.来源关系_, 令牌)
            && 节点_.删除节点(候选.存在_, 令牌)
            && 主信息_.删除主信息(候选.主信息_, 令牌);
        if (!完成) return 未发布候选操作状态::内部不一致;
        候选.阶段_ = 存在未发布候选::阶段::已撤销;
        return 未发布候选操作状态::已完成;
    }

    未发布候选操作状态 确认候选(存在未发布候选& 候选, const 结构事务令牌& 令牌) {
        if (候选.服务_ != this || !令牌匹配(候选.令牌_, 令牌) || !验证独占(令牌)
            || 候选.阶段_ != 存在未发布候选::阶段::未发布) return 未发布候选操作状态::已拒绝;
        候选.阶段_ = 存在未发布候选::阶段::已确认;
        return 未发布候选操作状态::已完成;
    }

    节点句柄 创建实际存在() {
        const auto 主信息句柄 = 主信息_.创建主信息();
        const auto 存在 = 节点_.创建节点(节点类型::存在, 主信息句柄);
        const auto 记录 = 读取存在(存在);
        if (!记录.has_value() || !主信息_.主信息是否有效(记录->主信息)) {
            return {};
        }
        return 存在;
    }

    节点句柄 创建存在概念根() {
        const auto 主信息句柄 = 主信息_.创建主信息();
        const auto 根节点 = 节点_.创建节点(节点类型::存在, 主信息句柄);
        const auto 记录 = 读取存在(根节点);
        if (!追根因检查(记录.has_value() && 主信息_.主信息是否有效(记录->主信息),
            L"创建存在概念根后读回身份不符合内部预期。")) {
            return {};
        }
        return 根节点;
    }

    节点句柄 创建虚拟存在(节点句柄 来源节点) {
        const auto 来源记录 = 节点_.读取节点(来源节点);
        if (!来源记录.has_value()
            || (来源记录->类型 != 节点类型::需求 && 来源记录->类型 != 节点类型::任务 && 来源记录->类型 != 节点类型::方法)) {
            return {};
        }
        const auto 虚拟存在 = 创建实际存在();
        if (!句柄有效(虚拟存在)) {
            return {};
        }
        const auto 来源关系 = 关系_.创建关系(关系类型::引用, 虚拟存在, 来源节点);
        if (!句柄有效(来源关系)) {
            return {};
        }
        return 虚拟存在;
    }

    std::optional<节点记录> 读取存在(节点句柄 存在节点) const {
        const auto 记录 = 节点_.读取节点(存在节点);
        if (!记录.has_value() || 记录->类型 != 节点类型::存在) {
            return std::nullopt;
        }
        return 记录;
    }

    bool 存在是否有效(节点句柄 存在节点) const {
        return 读取存在(存在节点).has_value();
    }

    std::optional<概念结构所有者清理授权> 形成概念节点清理授权(
        const 概念结构所有者清理请求& 请求,
        const 结构事务令牌& 令牌) const {
        if (!请求.完整() || 请求.所有者 != 概念结构所有者类型::存在服务) return std::nullopt;
        const auto 记录 = 节点_.读取节点(请求.目标, 令牌);
        if (!记录.has_value() || 记录->类型 != 节点类型::存在 || 记录->主信息 != 请求.主信息) return std::nullopt;
        概念结构所有者清理授权 授权{请求.目标, 请求.主信息, 请求.所有者, 令牌.许可序号};
        return 授权.完整() ? std::optional<概念结构所有者清理授权>{授权} : std::nullopt;
    }

private:
    bool 验证独占(const 结构事务令牌& 令牌) const {
        return 接线_.已接域() && 接线_.验证独占令牌(接线_.运行期状态, 令牌);
    }
    static bool 令牌匹配(const 结构事务令牌& 左, const 结构事务令牌& 右) {
        return 左.域编号 == 右.域编号 && 左.运行期纪元 == 右.运行期纪元
            && 左.许可序号 == 右.许可序号 && 左.类型 == 右.类型;
    }
    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
    结构事务接线 接线_;
};

}
