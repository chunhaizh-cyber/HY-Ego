struct 方法召回请求材料 {
    std::uint32_t 规则版本 = 0;
    节点句柄 任务;
    节点句柄 来源需求;
    std::optional<节点句柄> 父需求;
    节点句柄 需求主体;
    节点句柄 目标宿主;
    节点句柄 运行场景;
    节点句柄 目标特征;
    节点句柄 目标状态;
    std::int64_t 目标状态值材料 = 0;
    方法召回请求状态 状态 = 方法召回请求状态::未定义;
    bool 非权威 = true;

    bool 完整() const {
        return 规则版本 == 方法召回请求规则版本
            && 句柄有效(任务)
            && 句柄有效(来源需求)
            && (!父需求.has_value() || 句柄有效(父需求.value()))
            && 句柄有效(需求主体)
            && 句柄有效(目标宿主)
            && 句柄有效(运行场景)
            && 句柄有效(目标特征)
            && 句柄有效(目标状态)
            && 状态 == 方法召回请求状态::可召回
            && 非权威;
    }
};

inline bool 方法召回请求材料相同(
    const 方法召回请求材料& 左,
    const 方法召回请求材料& 右) {
    return 左.规则版本 == 右.规则版本
        && 左.任务 == 右.任务
        && 左.来源需求 == 右.来源需求
        && 左.父需求 == 右.父需求
        && 左.需求主体 == 右.需求主体
        && 左.目标宿主 == 右.目标宿主
        && 左.运行场景 == 右.运行场景
        && 左.目标特征 == 右.目标特征
        && 左.目标状态 == 右.目标状态
        && 左.目标状态值材料 == 右.目标状态值材料
        && 左.状态 == 右.状态
        && 左.非权威 == 右.非权威;
}

struct 方法召回候选材料 {
    方法召回请求材料 请求;
    方法结果索引项 来源索引项;
    方法结果规格材料 当前结果规格;
    方法召回候选状态 状态 = 方法召回候选状态::未定义;
    bool 非权威 = true;

    bool 完整() const {
        const 方法结果能力键 当前能力键{
            方法召回索引规则版本,
            当前结果规格.目标特征,
            当前结果规格.目标状态值材料};
        return 请求.完整()
            && 来源索引项.完整()
            && 当前结果规格.完整()
            && 来源索引项.方法首 == 当前结果规格.方法首
            && 来源索引项.结果节点 == 当前结果规格.结果节点
            && 来源索引项.结果特征关系 == 当前结果规格.特征关系
            && 来源索引项.结果目标状态关系 == 当前结果规格.目标状态关系
            && 来源索引项.结果规格规则版本 == 当前结果规格.规格规则版本
            && 来源索引项.能力键 == 当前能力键
            && 来源索引项.能力键.目标特征 == 请求.目标特征
            && 来源索引项.能力键.目标状态值材料 == 请求.目标状态值材料
            && 状态 == 方法召回候选状态::当前
            && 非权威;
    }
};

inline bool 方法召回候选稳定小于(
    const 方法召回候选材料& 左,
    const 方法召回候选材料& 右) {
    return 方法结果索引项稳定小于(左.来源索引项, 右.来源索引项);
}

inline bool 方法召回候选身份相同(
    const 方法召回候选材料& 左,
    const 方法召回候选材料& 右) {
    return 方法结果索引项身份相同(左.来源索引项, 右.来源索引项);
}

inline bool 方法召回候选组身份相同(
    const std::vector<方法召回候选材料>& 左,
    const std::vector<方法召回候选材料>& 右) {
    if (左.size() != 右.size()) {
        return false;
    }
    for (std::size_t 索引 = 0; 索引 < 左.size(); ++索引) {
        if (!方法召回候选身份相同(左[索引], 右[索引])) {
            return false;
        }
    }
    return true;
}

struct 方法召回候选组材料 {
    方法召回请求材料 请求;
    std::uint64_t 索引发布版本 = 0;
    方法召回候选状态 状态 = 方法召回候选状态::未定义;
    std::uint64_t 过期项数量 = 0;
    std::uint64_t 矛盾项数量 = 0;
    std::vector<方法召回候选材料> 候选组;
    bool 非权威 = true;

    bool 结构可读() const {
        if (!非权威) {
            return false;
        }
        switch (状态) {
        case 方法召回候选状态::请求拒绝:
            return !请求.完整()
                && 索引发布版本 == 0
                && 过期项数量 == 0
                && 矛盾项数量 == 0
                && 候选组.empty();
        case 方法召回候选状态::索引不可用:
            return 请求.完整()
                && 索引发布版本 == 0
                && 过期项数量 == 0
                && 矛盾项数量 == 0
                && 候选组.empty();
        case 方法召回候选状态::无候选:
            return 请求.完整()
                && 索引发布版本 != 0
                && 过期项数量 == 0
                && 矛盾项数量 == 0
                && 候选组.empty();
        case 方法召回候选状态::当前:
            if (!请求.完整()
                || 索引发布版本 == 0
                || 过期项数量 != 0
                || 矛盾项数量 != 0
                || 候选组.empty()) {
                return false;
            }
            break;
        case 方法召回候选状态::需要重建:
            if (!请求.完整()
                || 索引发布版本 == 0
                || 过期项数量 == 0
                || 矛盾项数量 != 0) {
                return false;
            }
            break;
        case 方法召回候选状态::内部矛盾:
            return 请求.完整()
                && 索引发布版本 != 0
                && 矛盾项数量 != 0
                && 候选组.empty();
        default:
            return false;
        }

        for (std::size_t 索引 = 0; 索引 < 候选组.size(); ++索引) {
            if (!候选组[索引].完整()
                || !方法召回请求材料相同(候选组[索引].请求, 请求)
                || (索引 != 0 && !方法召回候选稳定小于(
                    候选组[索引 - 1], 候选组[索引]))) {
                return false;
            }
        }
        return true;
    }

    bool 可进入条件复判() const {
        return 结构可读()
            && !候选组.empty()
            && (状态 == 方法召回候选状态::当前
                || 状态 == 方法召回候选状态::需要重建);
    }
};

enum class 条件复判结论 : std::uint32_t {
    未定义 = 0,
    全部满足 = 1,
    明确不满足 = 2,
    不可判定 = 3,
    材料过期 = 4,
    内部不一致 = 5
};

enum class 条件复判来源状态 : std::uint32_t {
    无效 = 0,
    当前 = 1,
    已过期 = 2
};

struct 条件项复判材料 {
    方法条件规格材料 条件规格;
    std::optional<节点句柄> 实例特征槽位;
    std::optional<节点句柄> 当前特征值;
    特征值原始类型 当前值原始类型 = 特征值原始类型::未建立;
    std::uint64_t 当前值原始版本 = 0;
    条件复判结论 结论 = 条件复判结论::未定义;
    bool 非权威 = true;

    bool 结构可读() const {
        if (!非权威 || !条件规格.完整()
            || (实例特征槽位.has_value() && !句柄有效(实例特征槽位.value()))
            || (当前特征值.has_value() && !句柄有效(当前特征值.value()))
            || (当前特征值.has_value() && !实例特征槽位.has_value())) {
            return false;
        }
        if (结论 == 条件复判结论::材料过期
            || 结论 == 条件复判结论::内部不一致) {
            return true;
        }
        if (结论 == 条件复判结论::不可判定) {
            return true;
        }
        return (结论 == 条件复判结论::全部满足
                || 结论 == 条件复判结论::明确不满足)
            && 实例特征槽位.has_value()
            && 当前特征值.has_value()
            && 当前值原始类型 == 条件规格.期望值原始类型
            && 当前值原始版本 != 0;
    }
};

inline bool 条件复判节点稳定小于(
    const 节点句柄& 左节点,
    const 节点句柄& 右节点) {
    if (左节点.仓库编号 != 右节点.仓库编号) {
        return 左节点.仓库编号 < 右节点.仓库编号;
    }
    if (左节点.节点编号 != 右节点.节点编号) {
        return 左节点.节点编号 < 右节点.节点编号;
    }
    return 左节点.版本号 < 右节点.版本号;
}

inline bool 条件项复判稳定小于(
    const 条件项复判材料& 左,
    const 条件项复判材料& 右) {
    return 条件复判节点稳定小于(
        左.条件规格.条件节点, 右.条件规格.条件节点);
}

struct 方法条件复判材料 {
    方法召回候选材料 候选;
    std::optional<方法条件集合策略材料> 条件策略;
    std::vector<条件项复判材料> 条件项组;
    std::vector<节点句柄> 不可读条件节点组;
    条件复判结论 结论 = 条件复判结论::未定义;
    bool 非权威 = true;

    bool 结构可读() const {
        if (!非权威) {
            return false;
        }
        if (!候选.完整()) {
            return 结论 == 条件复判结论::不可判定
                && !条件策略.has_value()
                && 条件项组.empty()
                && 不可读条件节点组.empty();
        }
        if (!条件策略.has_value()) {
            return (结论 == 条件复判结论::不可判定
                    || 结论 == 条件复判结论::材料过期
                    || 结论 == 条件复判结论::内部不一致)
                && 条件项组.empty()
                && 不可读条件节点组.empty();
        }
        if (!条件策略->完整()
            || !(条件策略->方法首 == 候选.来源索引项.方法首)) {
            return false;
        }

        for (std::size_t 索引 = 0; 索引 < 条件项组.size(); ++索引) {
            if (!条件项组[索引].结构可读()
                || !(条件项组[索引].条件规格.方法首 == 条件策略->方法首)
                || (索引 != 0 && !条件项复判稳定小于(
                    条件项组[索引 - 1], 条件项组[索引]))) {
                return false;
            }
        }
        for (std::size_t 索引 = 0; 索引 < 不可读条件节点组.size(); ++索引) {
            if (!句柄有效(不可读条件节点组[索引])
                || (索引 != 0 && !条件复判节点稳定小于(
                    不可读条件节点组[索引 - 1], 不可读条件节点组[索引]))) {
                return false;
            }
        }
        std::size_t 条件项索引 = 0;
        std::size_t 不可读索引 = 0;
        while (条件项索引 < 条件项组.size()
            && 不可读索引 < 不可读条件节点组.size()) {
            const auto& 条件节点 = 条件项组[条件项索引].条件规格.条件节点;
            const auto& 不可读节点 = 不可读条件节点组[不可读索引];
            if (条件节点 == 不可读节点) {
                return false;
            }
            if (条件复判节点稳定小于(条件节点, 不可读节点)) {
                ++条件项索引;
            }
            else {
                ++不可读索引;
            }
        }
        if (结论 == 条件复判结论::材料过期
            || 结论 == 条件复判结论::内部不一致) {
            return true;
        }
        if (条件策略->策略 == 方法条件集合策略::显式无条件) {
            return 结论 == 条件复判结论::全部满足
                && 条件项组.empty()
                && 不可读条件节点组.empty();
        }
        if (条件策略->策略 != 方法条件集合策略::全部满足
            || (条件项组.empty() && 不可读条件节点组.empty())) {
            return false;
        }

        bool 存在不满足 = false;
        bool 存在不可判定 = false;
        for (const auto& 条件项 : 条件项组) {
            if (条件项.结论 == 条件复判结论::材料过期
                || 条件项.结论 == 条件复判结论::内部不一致) {
                return false;
            }
            存在不满足 = 存在不满足
                || 条件项.结论 == 条件复判结论::明确不满足;
            存在不可判定 = 存在不可判定
                || 条件项.结论 == 条件复判结论::不可判定;
        }
        if (结论 == 条件复判结论::明确不满足) {
            return 存在不满足;
        }
        if (结论 == 条件复判结论::不可判定) {
            return !存在不满足
                && (存在不可判定 || !不可读条件节点组.empty());
        }
        return 结论 == 条件复判结论::全部满足
            && !存在不满足
            && !存在不可判定
            && 不可读条件节点组.empty()
            && std::all_of(条件项组.begin(), 条件项组.end(), [](const auto& 条件项) {
                return 条件项.结论 == 条件复判结论::全部满足;
            });
    }

    bool 可进入排序() const {
        return 结构可读()
            && 候选.完整()
            && (结论 == 条件复判结论::全部满足
                || 结论 == 条件复判结论::明确不满足
                || 结论 == 条件复判结论::不可判定);
    }
};

