    static bool 节点句柄小于(const 节点句柄& 左, const 节点句柄& 右) {
        if (左.仓库编号 != 右.仓库编号) {
            return 左.仓库编号 < 右.仓库编号;
        }
        if (左.节点编号 != 右.节点编号) {
            return 左.节点编号 < 右.节点编号;
        }
        return 左.版本号 < 右.版本号;
    }

    static std::uint64_t ZigZag64(std::int64_t 值) {
        const auto 原始位 = static_cast<std::uint64_t>(值);
        return (原始位 << 1) ^ (值 < 0 ? std::numeric_limits<std::uint64_t>::max() : 0ULL);
    }

    static void 追加哈希字节(std::uint64_t& 哈希, std::uint8_t 字节) {
        哈希 ^= static_cast<std::uint64_t>(字节);
        哈希 *= FNV1a质数;
    }

    static void 追加小端U32(std::uint64_t& 哈希, std::uint32_t 值) {
        for (std::uint32_t 位移 = 0; 位移 < 32; 位移 += 8) {
            追加哈希字节(哈希, static_cast<std::uint8_t>((值 >> 位移) & 0xFFU));
        }
    }

    static void 追加小端U64(std::uint64_t& 哈希, std::uint64_t 值) {
        for (std::uint32_t 位移 = 0; 位移 < 64; 位移 += 8) {
            追加哈希字节(哈希, static_cast<std::uint8_t>((值 >> 位移) & 0xFFULL));
        }
    }

    static bool 内容签名相同(const 特征值内容签名材料& 左, const 特征值内容签名材料& 右) {
        return 左.特征节点 == 右.特征节点
            && 左.特征值节点 == 右.特征值节点
            && 左.原始类型 == 右.原始类型
            && 左.原始值版本 == 右.原始值版本
            && 左.哈希规则版本 == 右.哈希规则版本
            && 左.内容哈希64 == 右.内容哈希64;
    }

    static 特征值序列化拒绝原因 复核特征值序列化材料字段(const 特征值序列化材料& 材料) {
        if (材料.格式版本 != 特征值序列化格式版本) {
            return 特征值序列化拒绝原因::格式版本不匹配;
        }
        if (材料.节点类型枚举版本 != 特征值节点类型枚举版本) {
            return 特征值序列化拒绝原因::节点类型枚举版本不匹配;
        }
        if (材料.关系类型枚举版本 != 特征值关系类型枚举版本) {
            return 特征值序列化拒绝原因::关系类型枚举版本不匹配;
        }
        if (材料.值类型枚举版本 != 特征值原始类型枚举版本) {
            return 特征值序列化拒绝原因::值类型枚举版本不匹配;
        }
        if (!材料.仓库上下文.完整()
            || 材料.特征节点.仓库编号 != 材料.仓库上下文.节点仓库编号
            || 材料.特征值节点.仓库编号 != 材料.仓库上下文.节点仓库编号) {
            return 特征值序列化拒绝原因::仓库上下文不匹配;
        }
        if (材料.材料序号 == 0) {
            return 特征值序列化拒绝原因::材料序号无效;
        }
        if (!句柄有效(材料.特征节点)
            || !句柄有效(材料.特征值节点)
            || 材料.特征节点 == 材料.特征值节点) {
            return 特征值序列化拒绝原因::句柄材料无效;
        }
        if (材料.原始值版本 == 0
            || 材料.原始值版本 == std::numeric_limits<std::uint64_t>::max()) {
            return 特征值序列化拒绝原因::原始版本无效;
        }
        if (材料.非权威恢复请求 != 特征值非权威恢复请求::未携带) {
            return 特征值序列化拒绝原因::非权威材料请求;
        }

        switch (材料.原始类型) {
        case 特征值原始类型::I64:
            if (材料.元素数量 != 1) {
                return 特征值序列化拒绝原因::元素数量不一致;
            }
            if (!材料.I64值.has_value() || !材料.VecI64值.empty() || !材料.VecU64值.empty()) {
                return 特征值序列化拒绝原因::原始类型冲突;
            }
            break;
        case 特征值原始类型::VecI64:
            if (材料.I64值.has_value() || !材料.VecU64值.empty()) {
                return 特征值序列化拒绝原因::原始类型冲突;
            }
            if (材料.VecI64值.empty()
                || 材料.元素数量 != static_cast<std::uint64_t>(材料.VecI64值.size())) {
                return 特征值序列化拒绝原因::元素数量不一致;
            }
            break;
        case 特征值原始类型::VecU64:
            if (材料.I64值.has_value() || !材料.VecI64值.empty()) {
                return 特征值序列化拒绝原因::原始类型冲突;
            }
            if (材料.VecU64值.empty()
                || 材料.元素数量 != static_cast<std::uint64_t>(材料.VecU64值.size())) {
                return 特征值序列化拒绝原因::元素数量不一致;
            }
            break;
        default:
            return 特征值序列化拒绝原因::原始类型冲突;
        }
        return 特征值序列化拒绝原因::无;
    }

    特征值候选样本读取结果 读取特征值候选样本组(
        节点句柄 特征节点,
        const std::vector<节点句柄>& 样本特征值组,
        std::size_t 最小样本数,
        std::uint64_t 规则版本) const {
        特征值候选样本读取结果 结果;
        if (规则版本 != 特征值候选比较规则版本) {
            结果.状态 = 特征值候选结果状态::规则不支持;
            return 结果;
        }
        if (样本特征值组.size() < 最小样本数) {
            结果.状态 = 特征值候选结果状态::候选不足;
            return 结果;
        }
        if (!节点是特征(特征节点)
            || std::any_of(样本特征值组.begin(), 样本特征值组.end(), [](const auto& 句柄) {
                return !句柄有效(句柄);
            })) {
            结果.状态 = 特征值候选结果状态::不可比;
            return 结果;
        }

        auto 排序句柄组 = 样本特征值组;
        std::sort(排序句柄组.begin(), 排序句柄组.end(), 节点句柄小于);
        if (std::adjacent_find(排序句柄组.begin(), 排序句柄组.end()) != 排序句柄组.end()) {
            结果.状态 = 特征值候选结果状态::候选不足;
            return 结果;
        }

        结果.来源.特征节点 = 特征节点;
        结果.来源.来源特征值组 = 样本特征值组;
        结果.来源.样本计数 = static_cast<std::uint64_t>(样本特征值组.size());
        结果.来源.规则版本 = 规则版本;
        结果.来源.来源版本组.reserve(样本特征值组.size());
        结果.原始材料组.reserve(样本特征值组.size());
        for (const auto& 特征值节点 : 样本特征值组) {
            const auto 原始材料 = 读取特征值原始材料(特征节点, 特征值节点);
            if (!原始材料.has_value()) {
                结果.状态 = 特征值候选结果状态::不可比;
                return 结果;
            }
            if (结果.原始材料组.empty()) {
                结果.来源.原始类型 = 原始材料->原始类型;
            } else if (结果.来源.原始类型 != 原始材料->原始类型) {
                结果.状态 = 特征值候选结果状态::不可比;
                return 结果;
            }
            结果.来源.来源版本组.push_back(原始材料->原始值版本);
            结果.原始材料组.push_back(原始材料.value());
        }
        if (!追根因检查(结果.来源.完整(), L"特征值候选来源材料读取后不完整。")) {
            结果.状态 = 特征值候选结果状态::不可比;
            return 结果;
        }
        结果.状态 = 复核特征值候选来源状态(结果.来源);
        return 结果;
    }

    bool 节点类型匹配(节点句柄 节点句柄值, 节点类型 类型) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        return 记录.has_value() && 记录->类型 == 类型;
    }

    bool 节点类型是允许特征宿主(节点句柄 节点句柄值) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        if (!记录.has_value()) {
            return false;
        }
        return 记录->类型 == 节点类型::存在
            || 记录->类型 == 节点类型::场景
            || 记录->类型 == 节点类型::需求
            || 记录->类型 == 节点类型::任务
            || 记录->类型 == 节点类型::方法;
    }

    std::optional<节点句柄> 读取槽位唯一特征定义(节点句柄 槽位节点) const {
        if (关系_ == nullptr || !节点是特征(槽位节点)) {
            return std::nullopt;
        }
        std::optional<节点句柄> 结果;
        const auto 候选组 = 关系_->获取目标节点组(槽位节点, 关系类型::模板);
        for (const auto& 候选 : 候选组) {
            if (!节点是特征(候选)) {
                continue;
            }
            if (结果.has_value()) {
                return std::nullopt;
            }
            结果 = 候选;
        }
        return 结果;
    }

    实例槽位当前值材料 读取实例槽位当前值材料(节点句柄 槽位节点) const {
        实例槽位当前值材料 结果;
        if (关系_ == nullptr || !节点是实例特征槽位(槽位节点)) {
            return 结果;
        }
        std::vector<节点句柄> 当前值组;
        const auto 候选组 = 关系_->获取目标节点组(槽位节点, 关系类型::归属);
        for (const auto& 候选 : 候选组) {
            if (!节点类型匹配(候选, 节点类型::特征值)) {
                return 结果;
            }
            当前值组.push_back(候选);
        }
        std::sort(当前值组.begin(), 当前值组.end(), 节点句柄小于);
        当前值组.erase(std::unique(当前值组.begin(), 当前值组.end()), 当前值组.end());
        if (当前值组.empty()) {
            结果.状态 = 实例槽位当前值状态::无当前值;
            return 结果;
        }
        if (当前值组.size() == 1) {
            结果.状态 = 实例槽位当前值状态::唯一当前值;
            结果.当前特征值 = 当前值组.front();
            return 结果;
        }
        (void)追根因检查(false, L"实例特征槽位存在多个归属特征值，不能选择第一个。" );
        结果.状态 = 实例槽位当前值状态::多值歧义;
        return 结果;
    }

    bool 节点是实例特征槽位(节点句柄 槽位节点) const {
        if (关系_ == nullptr || !节点是特征(槽位节点) || !槽位绑定特征类型(槽位节点)) {
            return false;
        }
        const auto 宿主候选组 = 关系_->获取来源节点组(槽位节点, 关系类型::归属);
        return std::any_of(宿主候选组.begin(), 宿主候选组.end(), [this](const auto& 宿主候选) {
            return 节点类型是允许特征宿主(宿主候选);
        });
    }

    bool 槽位绑定特征类型(节点句柄 槽位节点) const {
        if (关系_ == nullptr || !节点是特征(槽位节点)) {
            return false;
        }
        const auto 类型候选组 = 关系_->获取目标节点组(槽位节点, 关系类型::模板);
        for (const auto& 类型候选 : 类型候选组) {
            if (节点是特征(类型候选)) {
                return true;
            }
        }
        return false;
    }

