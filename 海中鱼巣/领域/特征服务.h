// 文件规则：特征服务只建立特征结构入口；具体值必须交给特征值服务承载。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"

namespace 海中鱼巣 {

class 特征服务 {
public:
    特征服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 创建特征类型() {
        return 节点_.创建节点(节点类型::特征, 主信息_.创建主信息());
    }

    节点句柄 创建二次特征() {
        return 节点_.创建节点(节点类型::二次特征, 主信息_.创建主信息());
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
