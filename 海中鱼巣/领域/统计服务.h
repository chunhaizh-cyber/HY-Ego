    struct 概念用途缓存键哈希 {
        std::size_t operator()(const 概念用途缓存键& 键) const noexcept {
            std::size_t 结果 = 0;
            const auto 合并 = [&结果](std::uint64_t 值) {
                const auto 当前哈希 = std::hash<std::uint64_t>{}(值);
                结果 ^= 当前哈希 + static_cast<std::size_t>(0x9E37'79B9U)
                    + (结果 << 6) + (结果 >> 2);
            };
            合并(static_cast<std::uint32_t>(键.命名空间.类型));
            合并(static_cast<std::uint32_t>(键.命名空间.来源));
            合并(键.命名空间.来源版本);
            合并(键.命名空间.规则版本);
            合并(static_cast<std::uint32_t>(键.命名空间.值类型));
            合并(键.命名空间.可持久化 ? 1 : 0);
            合并(static_cast<std::uint32_t>(键.命名空间.失效条件));
            合并(键.概念.仓库编号);
            合并(键.概念.节点编号);
            合并(键.概念.版本号);
            合并(static_cast<std::uint32_t>(键.用途));
            return 结果;
        }
    };

