enum class 缓存类型 : std::uint32_t {
    未设置 = 0,
    结构统计 = 1,
    特征值内容哈希 = 2,
    特征值命中观察 = 3,
    需求结算摘要 = 4,
    需求观察统计 = 5,
    存在观测缓存 = 6,
    二次特征签名观察 = 7,
    概念用途观察 = 8
};

enum class 来源结构类型 : std::uint32_t {
    未设置 = 0,
    结构仓库集合 = 1,
    节点仓库 = 2,
    关系仓库 = 3,
    索引仓库 = 4,
    特征值 = 5,
    需求 = 6,
    存在 = 7,
    二次特征 = 8,
    概念 = 9
};

