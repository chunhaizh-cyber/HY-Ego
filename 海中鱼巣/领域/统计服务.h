// 文件规则：统计服务只维护非权威运行期快照；不得写业务事实或修复权威结构。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/关系仓库.h"
#include "../核心/索引仓库.h"

#include <cstdint>
#include <mutex>
#include <optional>

namespace 海中鱼巣 {

enum class 缓存类型 : std::uint32_t {
    未设置 = 0,
    结构统计 = 1,
    特征值内容哈希 = 2,
    特征值命中观察 = 3,
    需求结算摘要 = 4,
    需求观察统计 = 5,
    存在观测缓存 = 6,
    二次特征签名观察 = 7
};

enum class 来源结构类型 : std::uint32_t {
    未设置 = 0,
    结构仓库集合 = 1,
    节点仓库 = 2,
    关系仓库 = 3,
    索引仓库 = 4,
    特征值 = 5,
    需求 = 6,
    存在 = 7,
    二次特征 = 8
};

enum class 缓存值类型 : std::uint32_t {
    未设置 = 0,
    结构数量 = 1,
    哈希值 = 2,
    命中观察 = 3,
    结算摘要 = 4,
    观察统计 = 5,
    观测材料 = 6,
    签名命中 = 7
};

enum class 缓存失效条件 : std::uint32_t {
    未设置 = 0,
    权威结构变化 = 1,
    手动清空 = 2,
    规则版本变化 = 3
};

struct 缓存命名空间 {
    缓存类型 类型 = 缓存类型::未设置;
    来源结构类型 来源 = 来源结构类型::未设置;
    std::uint64_t 来源版本 = 0;
    std::uint64_t 规则版本 = 0;
    缓存值类型 值类型 = 缓存值类型::未设置;
    bool 可持久化 = false;
    缓存失效条件 失效条件 = 缓存失效条件::未设置;
};

struct 结构统计快照 {
    缓存命名空间 命名空间;
    std::uint64_t 节点数 = 0;
    std::uint64_t 关系数 = 0;
    std::uint64_t 索引数 = 0;
};

struct 特征值内容哈希材料 {
    缓存命名空间 命名空间;
    节点句柄 特征值;
    bool 命名空间准入 = false;
    bool 已计算真实内容哈希 = false;
    bool 可裁决业务事实 = false;
};

struct 特征值命中观察材料 {
    缓存命名空间 命名空间;
    节点句柄 特征值;
    std::uint64_t 命中次数候选 = 0;
    bool 可裁决业务事实 = false;
};

struct 需求观察统计材料 {
    缓存命名空间 命名空间;
    节点句柄 需求;
    std::uint64_t 观察次数候选 = 0;
    bool 可裁决业务事实 = false;
};

struct 需求结算摘要材料 {
    缓存命名空间 命名空间;
    节点句柄 需求;
    std::uint64_t 摘要版本 = 0;
    bool 可裁决需求结算 = false;
    bool 可裁决业务事实 = false;
    节点句柄 结算记录;
};

struct 存在观测缓存材料 {
    缓存命名空间 命名空间;
    节点句柄 存在;
    bool 接入外部观察通道 = false;
    bool 可裁决业务事实 = false;
};

struct 二次特征签名命中材料 {
    缓存命名空间 命名空间;
    节点句柄 二次特征;
    std::uint64_t 组成项数量 = 0;
    bool 签名命中候选 = false;
    bool 仍需组成项复核 = true;
    bool 可裁决业务事实 = false;
};

class 统计服务 {
public:
    缓存命名空间 定义缓存命名空间(缓存类型 类型, 来源结构类型 来源, std::uint64_t 来源版本,
        std::uint64_t 规则版本, 缓存值类型 值类型, bool 可持久化, 缓存失效条件 失效条件) const {
        if (!缓存类型有效(类型)
            || !来源结构类型有效(来源)
            || 来源版本 == 0
            || 规则版本 == 0
            || !缓存值类型有效(值类型)
            || !缓存失效条件有效(失效条件)) {
            return {};
        }
        return 缓存命名空间{类型, 来源, 来源版本, 规则版本, 值类型, 可持久化, 失效条件};
    }

    结构统计快照 创建统计快照结构(缓存命名空间 命名空间, std::uint64_t 节点数,
        std::uint64_t 关系数, std::uint64_t 索引数) const {
        if (!缓存命名空间有效(命名空间)
            || 命名空间.类型 != 缓存类型::结构统计
            || 命名空间.来源 != 来源结构类型::结构仓库集合
            || 命名空间.值类型 != 缓存值类型::结构数量) {
            return {};
        }
        return 结构统计快照{命名空间, 节点数, 关系数, 索引数};
    }

    bool 缓存命名空间是否有效(缓存命名空间 命名空间) const {
        return 缓存命名空间有效(命名空间);
    }

    bool 结构统计快照是否有效(结构统计快照 快照) const {
        return 结构统计快照有效(快照);
    }

    特征值内容哈希材料 读取特征值内容哈希材料(缓存命名空间 命名空间, 节点句柄 特征值) const {
        if (!句柄有效(特征值)
            || !二级缓存命名空间匹配(命名空间, 缓存类型::特征值内容哈希, 来源结构类型::特征值, 缓存值类型::哈希值)) {
            return {};
        }
        return 特征值内容哈希材料{命名空间, 特征值, true, false, false};
    }

    特征值命中观察材料 读取特征值命中观察材料(缓存命名空间 命名空间, 节点句柄 特征值,
        std::uint64_t 命中次数候选) const {
        if (!句柄有效(特征值)
            || !二级缓存命名空间匹配(命名空间, 缓存类型::特征值命中观察, 来源结构类型::特征值, 缓存值类型::命中观察)) {
            return {};
        }
        return 特征值命中观察材料{命名空间, 特征值, 命中次数候选, false};
    }

    需求观察统计材料 读取需求观察统计材料(缓存命名空间 命名空间, 节点句柄 需求,
        std::uint64_t 观察次数候选) const {
        if (!句柄有效(需求)
            || !二级缓存命名空间匹配(命名空间, 缓存类型::需求观察统计, 来源结构类型::需求, 缓存值类型::观察统计)) {
            return {};
        }
        return 需求观察统计材料{命名空间, 需求, 观察次数候选, false};
    }

    需求结算摘要材料 读取需求结算摘要材料(缓存命名空间 命名空间, 节点句柄 需求,
        std::uint64_t 摘要版本) const {
        if (!句柄有效(需求)
            || 摘要版本 == 0
            || !二级缓存命名空间匹配(命名空间, 缓存类型::需求结算摘要, 来源结构类型::需求, 缓存值类型::结算摘要)) {
            return {};
        }
        return 需求结算摘要材料{命名空间, 需求, 摘要版本, false, false};
    }

    bool 写入需求结算摘要缓存(
        缓存命名空间 命名空间,
        节点句柄 需求,
        节点句柄 结算记录,
        std::uint64_t 摘要版本) {
        if (!句柄有效(需求)
            || !句柄有效(结算记录)
            || 摘要版本 == 0
            || 命名空间.可持久化
            || 命名空间.失效条件 != 缓存失效条件::手动清空
            || !二级缓存命名空间匹配(
                命名空间, 缓存类型::需求结算摘要, 来源结构类型::需求, 缓存值类型::结算摘要)) {
            return false;
        }
        const 需求结算摘要材料 新材料{
            命名空间, 需求, 摘要版本, false, false, 结算记录};
        std::lock_guard<std::mutex> 锁(需求结算摘要缓存锁_);
        需求结算摘要缓存_ = 新材料;
        return true;
    }

    std::optional<需求结算摘要材料> 读取需求结算摘要缓存(
        缓存命名空间 命名空间,
        节点句柄 需求,
        节点句柄 结算记录) const {
        if (!句柄有效(需求)
            || !句柄有效(结算记录)
            || !二级缓存命名空间匹配(
                命名空间, 缓存类型::需求结算摘要, 来源结构类型::需求, 缓存值类型::结算摘要)) {
            return std::nullopt;
        }
        std::lock_guard<std::mutex> 锁(需求结算摘要缓存锁_);
        if (!需求结算摘要缓存_.has_value()
            || !同一缓存命名空间(需求结算摘要缓存_->命名空间, 命名空间)
            || 需求结算摘要缓存_->需求 != 需求
            || 需求结算摘要缓存_->结算记录 != 结算记录) {
            return std::nullopt;
        }
        return 需求结算摘要缓存_;
    }

    bool 清空需求结算摘要缓存(缓存命名空间 命名空间) {
        if (!二级缓存命名空间匹配(
            命名空间, 缓存类型::需求结算摘要, 来源结构类型::需求, 缓存值类型::结算摘要)) {
            return false;
        }
        std::lock_guard<std::mutex> 锁(需求结算摘要缓存锁_);
        if (需求结算摘要缓存_.has_value()
            && 同一缓存命名空间(需求结算摘要缓存_->命名空间, 命名空间)) {
            需求结算摘要缓存_.reset();
        }
        return true;
    }

    bool 需求结算摘要缓存缺失仍不可裁决业务(缓存命名空间 命名空间) const {
        std::lock_guard<std::mutex> 锁(需求结算摘要缓存锁_);
        const bool 命中当前命名空间 = 需求结算摘要缓存_.has_value()
            && 同一缓存命名空间(需求结算摘要缓存_->命名空间, 命名空间);
        return 二级缓存命名空间匹配(
                命名空间, 缓存类型::需求结算摘要, 来源结构类型::需求, 缓存值类型::结算摘要)
            && !命中当前命名空间
            && !缓存可裁决业务事实(命名空间);
    }

    存在观测缓存材料 读取存在观测缓存材料(缓存命名空间 命名空间, 节点句柄 存在) const {
        if (!句柄有效(存在)
            || !二级缓存命名空间匹配(命名空间, 缓存类型::存在观测缓存, 来源结构类型::存在, 缓存值类型::观测材料)) {
            return {};
        }
        return 存在观测缓存材料{命名空间, 存在, false, false};
    }

    二次特征签名命中材料 读取二次特征签名命中材料(缓存命名空间 命名空间, 节点句柄 二次特征,
        std::uint64_t 组成项数量, bool 签名命中候选) const {
        if (!句柄有效(二次特征)
            || 组成项数量 == 0
            || !二级缓存命名空间匹配(
                命名空间, 缓存类型::二次特征签名观察, 来源结构类型::二次特征, 缓存值类型::签名命中)) {
            return {};
        }
        return 二次特征签名命中材料{命名空间, 二次特征, 组成项数量, 签名命中候选, true, false};
    }

    结构统计快照 读取权威结构统计快照(缓存命名空间 命名空间, const 节点仓库& 节点,
        const 关系仓库& 关系, const 索引仓库& 索引) const {
        return 创建统计快照结构(命名空间, 节点.有效节点数量(), 关系.有效关系数量(), 索引.有效主键数量());
    }

    结构统计快照 重建结构统计缓存(const 节点仓库& 节点, const 关系仓库& 关系, const 索引仓库& 索引) {
        const auto 命名空间 = 定义缓存命名空间(缓存类型::结构统计, 来源结构类型::结构仓库集合,
            1, 1, 缓存值类型::结构数量, false, 缓存失效条件::权威结构变化);
        auto 快照 = 创建统计快照结构(命名空间, 节点.有效节点数量(), 关系.有效关系数量(), 索引.有效主键数量());
        if (!结构统计快照有效(快照)) {
            return {};
        }
        结构统计缓存_ = 快照;
        return 快照;
    }

    std::optional<结构统计快照> 读取结构统计缓存(缓存命名空间 命名空间) const {
        if (!缓存命名空间有效(命名空间)
            || !结构统计缓存_.has_value()
            || !同一缓存命名空间(结构统计缓存_->命名空间, 命名空间)) {
            return std::nullopt;
        }
        return 结构统计缓存_;
    }

    bool 结构统计缓存缺失仍不可裁决业务(缓存命名空间 命名空间) const {
        return 缓存命名空间有效(命名空间)
            && !读取结构统计缓存(命名空间).has_value()
            && !缓存可裁决业务事实(命名空间);
    }

    bool 清空结构统计缓存(缓存命名空间 命名空间) {
        if (!缓存命名空间有效(命名空间)
            || 命名空间.类型 != 缓存类型::结构统计) {
            return false;
        }
        if (结构统计缓存_.has_value() && 同一缓存命名空间(结构统计缓存_->命名空间, 命名空间)) {
            结构统计缓存_.reset();
        }
        return true;
    }

    bool 清空全部缓存壳() {
        结构统计缓存_.reset();
        std::lock_guard<std::mutex> 锁(需求结算摘要缓存锁_);
        需求结算摘要缓存_.reset();
        return true;
    }

    bool 清空二级缓存壳() {
        std::lock_guard<std::mutex> 锁(需求结算摘要缓存锁_);
        需求结算摘要缓存_.reset();
        return true;
    }

    bool 缓存可裁决业务事实(缓存命名空间 命名空间) const {
        if (!缓存命名空间有效(命名空间)) {
            return false;
        }
        return false;
    }

private:
    bool 缓存命名空间有效(const 缓存命名空间& 命名空间) const {
        return 缓存类型有效(命名空间.类型)
            && 来源结构类型有效(命名空间.来源)
            && 命名空间.来源版本 != 0
            && 命名空间.规则版本 != 0
            && 缓存值类型有效(命名空间.值类型)
            && 缓存失效条件有效(命名空间.失效条件);
    }

    bool 结构统计快照有效(const 结构统计快照& 快照) const {
        return 缓存命名空间有效(快照.命名空间)
            && 快照.命名空间.类型 == 缓存类型::结构统计
            && 快照.命名空间.来源 == 来源结构类型::结构仓库集合
            && 快照.命名空间.值类型 == 缓存值类型::结构数量;
    }

    bool 同一缓存命名空间(const 缓存命名空间& 左, const 缓存命名空间& 右) const {
        return 左.类型 == 右.类型
            && 左.来源 == 右.来源
            && 左.来源版本 == 右.来源版本
            && 左.规则版本 == 右.规则版本
            && 左.值类型 == 右.值类型
            && 左.可持久化 == 右.可持久化
            && 左.失效条件 == 右.失效条件;
    }

    bool 二级缓存命名空间匹配(const 缓存命名空间& 命名空间, 缓存类型 类型, 来源结构类型 来源,
        缓存值类型 值类型) const {
        return 缓存命名空间有效(命名空间)
            && 命名空间.类型 == 类型
            && 命名空间.来源 == 来源
            && 命名空间.值类型 == 值类型
            && !缓存可裁决业务事实(命名空间);
    }

    bool 缓存类型有效(缓存类型 类型) const {
        return 类型 == 缓存类型::结构统计
            || 类型 == 缓存类型::特征值内容哈希
            || 类型 == 缓存类型::特征值命中观察
            || 类型 == 缓存类型::需求结算摘要
            || 类型 == 缓存类型::需求观察统计
            || 类型 == 缓存类型::存在观测缓存
            || 类型 == 缓存类型::二次特征签名观察;
    }

    bool 来源结构类型有效(来源结构类型 来源) const {
        return 来源 == 来源结构类型::结构仓库集合
            || 来源 == 来源结构类型::节点仓库
            || 来源 == 来源结构类型::关系仓库
            || 来源 == 来源结构类型::索引仓库
            || 来源 == 来源结构类型::特征值
            || 来源 == 来源结构类型::需求
            || 来源 == 来源结构类型::存在
            || 来源 == 来源结构类型::二次特征;
    }

    bool 缓存值类型有效(缓存值类型 类型) const {
        return 类型 == 缓存值类型::结构数量
            || 类型 == 缓存值类型::哈希值
            || 类型 == 缓存值类型::命中观察
            || 类型 == 缓存值类型::结算摘要
            || 类型 == 缓存值类型::观察统计
            || 类型 == 缓存值类型::观测材料
            || 类型 == 缓存值类型::签名命中;
    }

    bool 缓存失效条件有效(缓存失效条件 条件) const {
        return 条件 == 缓存失效条件::权威结构变化
            || 条件 == 缓存失效条件::手动清空
            || 条件 == 缓存失效条件::规则版本变化;
    }

    std::optional<结构统计快照> 结构统计缓存_;
    mutable std::mutex 需求结算摘要缓存锁_;
    std::optional<需求结算摘要材料> 需求结算摘要缓存_;
};

}
