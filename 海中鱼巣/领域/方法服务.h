// 文件规则：方法服务是方法登记和执行候选入口；方法结果必须经结构承载。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "特征值服务.h"
#include "状态服务.h"

namespace 海中鱼巣 {

class 方法服务 {
public:
    方法服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 登记方法() {
        return 节点_.创建节点(节点类型::方法, 主信息_.创建主信息());
    }

    节点句柄 形成I64写值授权(节点句柄 方法节点, 节点句柄 特征值节点, 状态服务& 状态, 节点句柄 场景, 节点句柄 方法存在) {
        const auto 方法记录 = 节点_.读取节点(方法节点);
        const auto 特征值记录 = 节点_.读取节点(特征值节点);
        if (!方法记录.has_value() || 方法记录->类型 != 节点类型::方法
            || !特征值记录.has_value() || 特征值记录->类型 != 节点类型::特征值) {
            return {};
        }
        return 状态.创建实例状态(场景, 方法存在, 1);
    }

    bool 执行I64写入方法(节点句柄 执行前授权, 特征值服务& 特征值, 节点句柄 特征值节点, std::int64_t 新值) {
        const auto 授权记录 = 节点_.读取节点(执行前授权);
        if (!授权记录.has_value() || 授权记录->类型 != 节点类型::状态) {
            return false;
        }
        return 特征值.写入I64值(特征值节点, 新值);
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
