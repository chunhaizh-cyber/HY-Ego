    std::optional<std::vector<方法结果规格索引来源材料>>
        读取活跃方法结果规格索引来源组(
            const 状态服务& 状态,
            const 特征服务& 特征) const {
        std::shared_lock<std::shared_mutex> 登记读锁(登记锁_);
        if (!登记根材料_.has_value()) {
            return std::nullopt;
        }
        if (!方法登记根完整_已加锁(状态)) {
            解锁并报告逻辑错误(
                登记读锁, L"读取方法结果规格索引来源时登记根结构不符合内部预期。");
            return std::nullopt;
        }

        std::vector<方法结果规格索引来源材料> 来源组;
        const auto 登记关系组 = 关系_.获取关系记录组(
            登记根材料_->登记根, 关系类型::归属);
        for (const auto& 登记关系记录 : 登记关系组) {
            if (!节点类型匹配(登记关系记录.目标节点, 节点类型::方法)) {
                解锁并报告逻辑错误(
                    登记读锁, L"读取方法结果规格索引来源时登记根出现非方法目标。");
                return std::nullopt;
            }
            const auto 登记项 = 读取方法登记项_已加锁(
                登记关系记录.目标节点, 状态, 登记关系记录);
            if (!登记项.has_value() || !登记项->完整()) {
                解锁并报告逻辑错误(
                    登记读锁, L"读取方法结果规格索引来源时登记项无法完整读回。");
                return std::nullopt;
            }
            if (登记项->生命周期状态 != 方法生命周期状态::活跃) {
                continue;
            }

            std::unique_lock<std::mutex> 规格读锁(读取规格写锁(登记项->方法首));
            const auto 结果节点组 = 读取方法结果节点组_已确认方法首(
                登记项->方法首, 状态);
            if (!结果节点组.has_value()) {
                解锁并报告逻辑错误(
                    登记读锁,
                    规格读锁,
                    L"读取方法结果规格索引来源时结果节点结构不符合内部预期。");
                return std::nullopt;
            }
            for (const auto& 结果节点 : 结果节点组.value()) {
                const bool 特征槽位存在 = 关系槽位已占用(
                    结果节点, 关系类型::引用, 结果目标特征顺序号);
                const bool 状态槽位存在 = 关系槽位已占用(
                    结果节点, 关系类型::引用, 结果目标状态顺序号);
                if (!特征槽位存在 && !状态槽位存在) {
                    continue;
                }
                if (特征槽位存在 != 状态槽位存在) {
                    解锁并报告逻辑错误(
                        登记读锁,
                        规格读锁,
                        L"读取方法结果规格索引来源时发现未完成的结果规格发布。");
                    return std::nullopt;
                }
                const auto 结果规格 = 读取方法结果规格_已确认方法首(
                    登记项->方法首, 结果节点, 状态, 特征);
                if (!结果规格.has_value() || !结果规格->完整()) {
                    解锁并报告逻辑错误(
                        登记读锁,
                        规格读锁,
                        L"读取方法结果规格索引来源时完整结果规格无法读回。");
                    return std::nullopt;
                }
                来源组.push_back({登记项.value(), 结果规格.value()});
            }
        }
        登记读锁.unlock();
        std::sort(来源组.begin(), 来源组.end(),
            [](const 方法结果规格索引来源材料& 左,
                const 方法结果规格索引来源材料& 右) {
                if (!(左.登记项.方法首 == 右.登记项.方法首)) {
                    return 节点句柄稳定小于(
                        左.登记项.方法首, 右.登记项.方法首);
                }
                return 节点句柄稳定小于(
                    左.结果规格.结果节点, 右.结果规格.结果节点);
            });
        for (std::size_t 索引 = 0; 索引 < 来源组.size(); ++索引) {
            if (!来源组[索引].完整()
                || (索引 != 0
                    && 来源组[索引 - 1].登记项.方法首 == 来源组[索引].登记项.方法首
                    && 来源组[索引 - 1].结果规格.结果节点
                        == 来源组[索引].结果规格.结果节点)) {
                (void)追根因检查(
                    false, L"读取方法结果规格索引来源后出现不完整或重复项。");
                return std::nullopt;
            }
        }
        return 来源组;
    }

    std::optional<方法条件集合策略材料> 登记方法条件集合策略(
        节点句柄 方法首节点,
        方法条件集合策略 策略,
        状态服务& 状态,
        const 特征服务& 特征) {
        if (策略 != 方法条件集合策略::全部满足
            && 策略 != 方法条件集合策略::显式无条件) {
            return std::nullopt;
        }
        std::shared_lock<std::shared_mutex> 登记读锁(登记锁_);
        if (!方法首活跃_已加锁(方法首节点, 状态)) {
            return std::nullopt;
        }
        std::unique_lock<std::mutex> 规格写锁(读取规格写锁(方法首节点));
        if (关系槽位已占用(方法首节点, 关系类型::模板, 方法条件集合策略顺序号)) {
            return std::nullopt;
        }
        const auto 条件节点组 = 读取方法条件节点组_已加锁(方法首节点, 状态);
        if (!条件节点组.has_value()) {
            解锁并报告逻辑错误(登记读锁, 规格写锁, L"登记方法条件集合策略时条件节点结构不符合内部预期。");
            return std::nullopt;
        }
        if (策略 == 方法条件集合策略::显式无条件) {
            if (!条件节点组->empty()) {
                return std::nullopt;
            }
        } else {
            bool 存在完整条件规格 = false;
            for (const auto& 条件节点 : 条件节点组.value()) {
                if (读取方法条件规格_已加锁(
                    方法首节点, 条件节点, 状态, 特征).has_value()) {
                    存在完整条件规格 = true;
                    break;
                }
            }
            if (!存在完整条件规格) {
                return std::nullopt;
            }
        }

        const auto 策略状态 = 状态.创建抽象状态(static_cast<std::int64_t>(策略));
        if (!句柄有效(策略状态)) {
            解锁并报告逻辑错误(登记读锁, 规格写锁, L"登记方法条件集合策略时状态创建不及预期。");
            return std::nullopt;
        }
        const auto 策略关系 = 关系_.创建关系(
            关系类型::模板, 方法首节点, 策略状态, 方法条件集合策略顺序号);
        if (!句柄有效(策略关系)) {
            解锁并报告逻辑错误(登记读锁, 规格写锁, L"登记方法条件集合策略时关系写入不及预期。");
            return std::nullopt;
        }
        const auto 读回材料 = 读取方法条件集合策略_已加锁(方法首节点, 状态);
        if (!读回材料.has_value() || !读回材料->完整()
            || 读回材料->策略 != 策略
            || !(读回材料->策略状态 == 策略状态)
            || !(读回材料->策略关系 == 策略关系)) {
            解锁并报告逻辑错误(登记读锁, 规格写锁, L"登记方法条件集合策略后完整读回不符合内部预期。");
            return std::nullopt;
        }
        return 读回材料;
    }

