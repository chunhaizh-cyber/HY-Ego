    bool 关系槽位已占用(
        节点句柄 源节点, 关系类型 类型, std::int64_t 顺序号) const {
        const auto 关系记录组 = 关系_.获取关系记录组(源节点, 类型);
        return std::any_of(关系记录组.begin(), 关系记录组.end(),
            [顺序号](const 关系记录& 记录) { return 记录.顺序号 == 顺序号; });
    }

    std::optional<关系记录> 读取唯一槽位关系(
        节点句柄 源节点,
        关系类型 类型,
        std::int64_t 顺序号,
        节点类型 目标类型) const {
        std::optional<关系记录> 唯一记录;
        const auto 关系记录组 = 关系_.获取关系记录组(源节点, 类型);
        for (const auto& 记录 : 关系记录组) {
            if (记录.顺序号 != 顺序号) {
                continue;
            }
            if (!节点类型匹配(记录.目标节点, 目标类型) || 唯一记录.has_value()) {
                return std::nullopt;
            }
            唯一记录 = 记录;
        }
        return 唯一记录;
    }

