    std::vector<方法登记项材料> 读取全部方法登记项(const 状态服务& 状态) const {
        std::shared_lock<std::shared_mutex> 锁(登记锁_);
        std::vector<方法登记项材料> 登记项组;
        if (!登记根材料_.has_value()) {
            return 登记项组;
        }
        if (!方法登记根完整_已加锁(状态)) {
            解锁并报告逻辑错误(锁, L"读取全部方法登记项时登记根结构不符合内部预期。");
            return {};
        }
        const auto 关系记录组 = 关系_.获取关系记录组(
            登记根材料_->登记根, 关系类型::归属);
        登记项组.reserve(关系记录组.size());
        for (const auto& 记录 : 关系记录组) {
            if (!节点类型匹配(记录.目标节点, 节点类型::方法)) {
                解锁并报告逻辑错误(锁, L"方法登记根出现非方法目标。");
                return {};
            }
            const auto 登记项 = 读取方法登记项_已加锁(记录.目标节点, 状态, 记录);
            if (!登记项.has_value()) {
                解锁并报告逻辑错误(锁, L"方法登记根中的登记项无法完整读回。");
                return {};
            }
            登记项组.push_back(登记项.value());
        }
        std::sort(登记项组.begin(), 登记项组.end(),
            [](const 方法登记项材料& 左, const 方法登记项材料& 右) {
                return 节点句柄稳定小于(左.方法首, 右.方法首);
            });
        for (std::size_t 索引 = 1; 索引 < 登记项组.size(); ++索引) {
            if (登记项组[索引 - 1].方法首 == 登记项组[索引].方法首) {
                解锁并报告逻辑错误(锁, L"读取全部方法登记项时出现重复方法首。");
                return {};
            }
        }
        return 登记项组;
    }

    std::vector<节点句柄> 读取活跃方法首组(const 状态服务& 状态) const {
        std::vector<节点句柄> 方法首组;
        const auto 登记项组 = 读取全部方法登记项(状态);
        方法首组.reserve(登记项组.size());
        for (const auto& 登记项 : 登记项组) {
            if (登记项.生命周期状态 == 方法生命周期状态::活跃) {
                方法首组.push_back(登记项.方法首);
            }
        }
        return 方法首组;
    }

