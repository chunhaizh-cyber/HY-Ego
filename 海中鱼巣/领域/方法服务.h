struct 方法登记根材料 {
    std::uint64_t 稳定非名称键 = 0;
    节点句柄 登记根;
    节点句柄 活跃状态;
    节点句柄 失效状态;

    bool 完整() const {
        return 稳定非名称键 != 0
            && 句柄有效(登记根)
            && 句柄有效(活跃状态)
            && 句柄有效(失效状态)
            && !(登记根 == 活跃状态)
            && !(登记根 == 失效状态)
            && !(活跃状态 == 失效状态);
    }
};

struct 方法登记项材料 {
    节点句柄 登记根;
    节点句柄 方法首;
    关系句柄 登记关系;
    方法生命周期状态 生命周期状态 = 方法生命周期状态::未定义;
    关系句柄 生命周期关系;
    std::uint32_t 生命周期版本 = 0;

    bool 完整() const {
        return 句柄有效(登记根)
            && 句柄有效(方法首)
            && 句柄有效(登记关系)
            && 句柄有效(生命周期关系)
            && (生命周期状态 == 方法生命周期状态::活跃
                || 生命周期状态 == 方法生命周期状态::已失效)
            && 生命周期版本 == 生命周期关系.版本号;
    }
};

struct 方法条件集合策略材料 {
    节点句柄 方法首;
    方法条件集合策略 策略 = 方法条件集合策略::未声明;
    节点句柄 策略状态;
    关系句柄 策略关系;
    std::uint32_t 规格规则版本 = 0;

    bool 完整() const {
        return 句柄有效(方法首)
            && (策略 == 方法条件集合策略::全部满足
                || 策略 == 方法条件集合策略::显式无条件)
            && 句柄有效(策略状态)
            && 句柄有效(策略关系)
            && 规格规则版本 == 1;
    }
};

struct 方法条件规格材料 {
    节点句柄 方法首;
    节点句柄 条件节点;
    节点句柄 条件场景;
    节点句柄 特征定义;
    节点句柄 期望特征值;
    关系句柄 特征关系;
    关系句柄 期望值关系;
    特征值原始类型 期望值原始类型 = 特征值原始类型::未建立;
    std::uint64_t 期望值原始版本 = 0;
    std::uint32_t 规格规则版本 = 0;

    bool 完整() const {
        const bool 原始类型有效 = 期望值原始类型 == 特征值原始类型::I64
            || 期望值原始类型 == 特征值原始类型::VecI64
            || 期望值原始类型 == 特征值原始类型::VecU64;
        return 句柄有效(方法首)
            && 句柄有效(条件节点)
            && 句柄有效(条件场景)
            && 句柄有效(特征定义)
            && 句柄有效(期望特征值)
            && 句柄有效(特征关系)
            && 句柄有效(期望值关系)
            && 原始类型有效
            && 期望值原始版本 != 0
            && 规格规则版本 == 1;
    }
};

