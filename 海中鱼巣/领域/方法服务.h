// 文件规则：方法服务是方法登记和执行候选入口；方法结果必须经结构承载。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"
#include "场景服务.h"
#include "存在服务.h"
#include "状态服务.h"
#include "特征服务.h"

#include <cstdint>
#include <optional>
#include <vector>

namespace 海中鱼巣 {

enum class 方法角色状态 : std::int64_t {
    未定义 = 0,
    方法首 = 1,
    方法条件 = 2,
    方法结果 = 3,
    条件结果对 = 4
};

class 方法服务 {
public:
    方法服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系)
        : 主信息_(主信息), 节点_(节点), 关系_(关系) {
    }

    节点句柄 登记方法() {
        return 节点_.创建节点(节点类型::方法, 主信息_.创建主信息());
    }

    节点句柄 创建方法首节点(存在服务& 存在, 状态服务& 状态) {
        const auto 方法节点 = 登记方法();
        if (!句柄有效(方法节点)) {
            return {};
        }
        if (!绑定方法角色状态(方法节点, 方法角色状态::方法首, 状态)) {
            return {};
        }
        const auto 方法虚拟存在 = 存在.创建虚拟存在(方法节点);
        if (!句柄有效(方法虚拟存在)) {
            return {};
        }
        const auto 虚拟存在关系 = 关系_.创建关系(关系类型::引用, 方法节点, 方法虚拟存在);
        if (!句柄有效(虚拟存在关系) || !有效方法首(方法节点, 状态)) {
            return {};
        }
        return 方法节点;
    }

    节点句柄 创建方法条件节点(节点句柄 方法首节点, 场景服务& 场景, 状态服务& 状态) {
        if (!有效方法首(方法首节点, 状态)) {
            return {};
        }
        const auto 方法条件节点 = 登记方法();
        if (!句柄有效(方法条件节点)) {
            return {};
        }
        if (!绑定方法角色状态(方法条件节点, 方法角色状态::方法条件, 状态)) {
            return {};
        }
        const auto 条件场景 = 场景.创建场景();
        if (!句柄有效(条件场景)) {
            return {};
        }
        const auto 条件场景关系 = 关系_.创建关系(关系类型::引用, 方法条件节点, 条件场景);
        const auto 方法首条件关系 = 关系_.创建关系(关系类型::模板, 方法首节点, 方法条件节点);
        if (!句柄有效(条件场景关系) || !句柄有效(方法首条件关系)
            || !有效方法条件节点(方法首节点, 方法条件节点, 状态)) {
            return {};
        }
        return 方法条件节点;
    }

    节点句柄 创建方法结果节点(节点句柄 方法首节点, 场景服务& 场景, 状态服务& 状态) {
        if (!有效方法首(方法首节点, 状态)) {
            return {};
        }
        const auto 方法结果节点 = 登记方法();
        if (!句柄有效(方法结果节点)) {
            return {};
        }
        if (!绑定方法角色状态(方法结果节点, 方法角色状态::方法结果, 状态)) {
            return {};
        }
        const auto 结果场景 = 场景.创建场景();
        if (!句柄有效(结果场景)) {
            return {};
        }
        const auto 结果场景关系 = 关系_.创建关系(关系类型::引用, 方法结果节点, 结果场景);
        const auto 方法首结果关系 = 关系_.创建关系(关系类型::模板, 方法首节点, 方法结果节点);
        if (!句柄有效(结果场景关系) || !句柄有效(方法首结果关系)
            || !有效方法结果节点(方法首节点, 方法结果节点, 状态)) {
            return {};
        }
        return 方法结果节点;
    }

    std::optional<方法角色状态> 读取方法角色状态(节点句柄 方法节点, const 状态服务& 状态) const {
        if (!节点类型匹配(方法节点, 节点类型::方法)) {
            return std::nullopt;
        }
        const auto 角色状态节点 = 读取唯一目标(方法节点, 关系类型::模板, 节点类型::状态);
        if (!角色状态节点.has_value()) {
            return std::nullopt;
        }
        const auto 状态值 = 状态.读取状态值(角色状态节点.value());
        if (!状态值.has_value()) {
            return std::nullopt;
        }
        const auto 角色 = static_cast<方法角色状态>(状态值.value());
        if (!方法角色状态有效(角色)) {
            return std::nullopt;
        }
        return 角色;
    }

    std::optional<节点句柄> 读取方法虚拟存在(节点句柄 方法节点, const 状态服务& 状态) const {
        if (!有效方法首(方法节点, 状态)) {
            return std::nullopt;
        }
        return 读取唯一目标(方法节点, 关系类型::引用, 节点类型::存在);
    }

    std::vector<节点句柄> 读取方法条件节点集合(节点句柄 方法首节点, const 状态服务& 状态) const {
        return 读取方法角色节点集合(方法首节点, 方法角色状态::方法条件, 状态);
    }

    std::vector<节点句柄> 读取方法结果节点集合(节点句柄 方法首节点, const 状态服务& 状态) const {
        return 读取方法角色节点集合(方法首节点, 方法角色状态::方法结果, 状态);
    }

    std::optional<节点句柄> 读取方法条件场景(节点句柄 方法首节点, 节点句柄 方法条件节点,
        const 状态服务& 状态) const {
        if (!有效方法条件节点(方法首节点, 方法条件节点, 状态)) {
            return std::nullopt;
        }
        return 读取唯一目标(方法条件节点, 关系类型::引用, 节点类型::场景);
    }

    std::optional<节点句柄> 读取方法结果场景(节点句柄 方法首节点, 节点句柄 方法结果节点,
        const 状态服务& 状态) const {
        if (!有效方法结果节点(方法首节点, 方法结果节点, 状态)) {
            return std::nullopt;
        }
        return 读取唯一目标(方法结果节点, 关系类型::引用, 节点类型::场景);
    }

    bool 执行特征状态写入方法(节点句柄 任务节点, 节点句柄 方法节点, 特征服务& 特征,
        节点句柄 目标特征, 节点句柄 特征值节点, std::int64_t 新状态值) {
        const auto 任务记录 = 节点_.读取节点(任务节点);
        const auto 方法记录 = 节点_.读取节点(方法节点);
        if (!任务记录.has_value() || 任务记录->类型 != 节点类型::任务
            || !方法记录.has_value() || 方法记录->类型 != 节点类型::方法) {
            return false;
        }
        if (!关系_.存在关系(关系类型::引用, 任务节点, 方法节点)) {
            return false;
        }
        return 特征.写入I64特征值(目标特征, 特征值节点, 新状态值);
    }

private:
    bool 绑定方法角色状态(节点句柄 方法节点, 方法角色状态 角色, 状态服务& 状态) {
        if (!节点类型匹配(方法节点, 节点类型::方法) || !方法角色状态有效(角色)) {
            return false;
        }
        const auto 角色状态 = 状态.创建抽象状态(static_cast<std::int64_t>(角色));
        if (!句柄有效(角色状态)) {
            return false;
        }
        const auto 角色关系 = 关系_.创建关系(关系类型::模板, 方法节点, 角色状态);
        return 句柄有效(角色关系);
    }

    bool 有效方法首(节点句柄 方法节点, const 状态服务& 状态) const {
        return 读取方法角色状态值(方法节点, 状态) == 方法角色状态::方法首
            && 读取唯一目标(方法节点, 关系类型::引用, 节点类型::存在).has_value();
    }

    bool 有效方法条件节点(节点句柄 方法首节点, 节点句柄 方法条件节点, const 状态服务& 状态) const {
        return 有效方法首(方法首节点, 状态)
            && 读取方法角色状态值(方法条件节点, 状态) == 方法角色状态::方法条件
            && 关系_.存在关系(关系类型::模板, 方法首节点, 方法条件节点)
            && 读取唯一目标(方法条件节点, 关系类型::引用, 节点类型::场景).has_value();
    }

    bool 有效方法结果节点(节点句柄 方法首节点, 节点句柄 方法结果节点, const 状态服务& 状态) const {
        return 有效方法首(方法首节点, 状态)
            && 读取方法角色状态值(方法结果节点, 状态) == 方法角色状态::方法结果
            && 关系_.存在关系(关系类型::模板, 方法首节点, 方法结果节点)
            && 读取唯一目标(方法结果节点, 关系类型::引用, 节点类型::场景).has_value();
    }

    std::vector<节点句柄> 读取方法角色节点集合(节点句柄 方法首节点, 方法角色状态 角色,
        const 状态服务& 状态) const {
        std::vector<节点句柄> 角色节点组;
        if (!有效方法首(方法首节点, 状态) || !方法角色状态有效(角色)) {
            return 角色节点组;
        }
        const auto 目标组 = 关系_.获取目标节点组(方法首节点, 关系类型::模板);
        for (const auto& 目标 : 目标组) {
            if (!节点类型匹配(目标, 节点类型::方法) || 读取方法角色状态值(目标, 状态) != 角色) {
                continue;
            }
            if ((角色 == 方法角色状态::方法条件 && !有效方法条件节点(方法首节点, 目标, 状态))
                || (角色 == 方法角色状态::方法结果 && !有效方法结果节点(方法首节点, 目标, 状态))) {
                continue;
            }
            角色节点组.push_back(目标);
        }
        return 角色节点组;
    }

    std::optional<节点句柄> 读取唯一目标(节点句柄 源节点, 关系类型 类型, 节点类型 目标类型) const {
        if (!节点类型匹配(源节点, 节点类型::方法)) {
            return std::nullopt;
        }
        const auto 目标组 = 关系_.获取目标节点组(源节点, 类型);
        std::optional<节点句柄> 唯一目标;
        for (const auto& 目标 : 目标组) {
            if (!节点类型匹配(目标, 目标类型)) {
                continue;
            }
            if (唯一目标.has_value()) {
                return std::nullopt;
            }
            唯一目标 = 目标;
        }
        return 唯一目标;
    }

    方法角色状态 读取方法角色状态值(节点句柄 方法节点, const 状态服务& 状态) const {
        if (!节点类型匹配(方法节点, 节点类型::方法)) {
            return 方法角色状态::未定义;
        }
        const auto 角色状态节点 = 读取唯一目标(方法节点, 关系类型::模板, 节点类型::状态);
        if (!角色状态节点.has_value()) {
            return 方法角色状态::未定义;
        }
        const auto 状态值 = 状态.读取状态值(角色状态节点.value());
        if (!状态值.has_value()) {
            return 方法角色状态::未定义;
        }
        const auto 角色 = static_cast<方法角色状态>(状态值.value());
        return 方法角色状态有效(角色) ? 角色 : 方法角色状态::未定义;
    }

    bool 方法角色状态有效(方法角色状态 角色) const {
        return 角色 == 方法角色状态::方法首
            || 角色 == 方法角色状态::方法条件
            || 角色 == 方法角色状态::方法结果
            || 角色 == 方法角色状态::条件结果对;
    }

    bool 节点类型匹配(节点句柄 节点句柄值, 节点类型 类型) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        return 记录.has_value() && 记录->类型 == 类型;
    }

    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
};

}
