    template <typename 锁类型>
    static void 解锁并报告逻辑错误(锁类型& 锁, const wchar_t* 说明) {
        if (锁.owns_lock()) {
            锁.unlock();
        }
        (void)追根因检查(false, 说明);
    }

    template <typename 第一锁类型, typename 第二锁类型>
    static void 解锁并报告逻辑错误(
        第一锁类型& 第一锁, 第二锁类型& 第二锁, const wchar_t* 说明) {
        if (第二锁.owns_lock()) {
            第二锁.unlock();
        }
        if (第一锁.owns_lock()) {
            第一锁.unlock();
        }
        (void)追根因检查(false, 说明);
    }

    std::mutex& 读取规格写锁(const 节点句柄& 方法首节点) const {
        const std::uint64_t 混合值 = 方法首节点.仓库编号
            ^ (方法首节点.节点编号 * 0x9E37'79B9'7F4A'7C15ULL)
            ^ static_cast<std::uint64_t>(方法首节点.版本号);
        return 规格写锁组_[static_cast<std::size_t>(混合值 % 规格写锁数量)];
    }

    static bool 节点句柄稳定小于(const 节点句柄& 左, const 节点句柄& 右) {
        if (左.仓库编号 != 右.仓库编号) {
            return 左.仓库编号 < 右.仓库编号;
        }
        if (左.节点编号 != 右.节点编号) {
            return 左.节点编号 < 右.节点编号;
        }
        return 左.版本号 < 右.版本号;
    }

    bool 方法登记根完整_已加锁(const 状态服务& 状态) const {
        if (!登记根材料_.has_value() || !登记根材料_->完整() || 关系仓库编号_ == 0
            || 读取方法角色状态值(登记根材料_->登记根, 状态) != 方法角色状态::方法登记根
            || 状态.状态是否实例状态(登记根材料_->活跃状态)
            || 状态.状态是否实例状态(登记根材料_->失效状态)) {
            return false;
        }
        const auto 活跃状态值 = 状态.读取状态值(登记根材料_->活跃状态);
        const auto 失效状态值 = 状态.读取状态值(登记根材料_->失效状态);
        const auto 活跃状态目标 = 读取唯一目标(
            登记根材料_->登记根, 关系类型::模板, 节点类型::状态, 根活跃状态顺序号);
        const auto 失效状态目标 = 读取唯一目标(
            登记根材料_->登记根, 关系类型::模板, 节点类型::状态, 根失效状态顺序号);
        return 活跃状态值 == std::optional<std::int64_t>{
                static_cast<std::int64_t>(方法生命周期状态::活跃)}
            && 失效状态值 == std::optional<std::int64_t>{
                static_cast<std::int64_t>(方法生命周期状态::已失效)}
            && 活跃状态目标 == std::optional<节点句柄>{登记根材料_->活跃状态}
            && 失效状态目标 == std::optional<节点句柄>{登记根材料_->失效状态};
    }

