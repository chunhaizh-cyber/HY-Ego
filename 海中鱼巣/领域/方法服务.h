enum class 方法角色状态 : std::int64_t {
    未定义 = 0,
    方法首 = 1,
    方法条件 = 2,
    方法结果 = 3,
    条件结果对 = 4,
    动作入口 = 5,
    方法登记根 = 6
};

enum class 方法生命周期状态 : std::int64_t {
    未定义 = 0,
    活跃 = 1,
    已失效 = 2
};

enum class 动作入口类型 : std::int64_t {
    未设置 = 0,
    本能方法入口 = 1,
    外部实现入口 = 2,
    动作序列入口 = 3,
    被动状态提交入口 = 4,
    被动整理入口 = 5
};

enum class 动作入口状态 : std::int64_t {
    未设置 = 0,
    主动可调用 = 1,
    被动状态提交 = 2,
    被动整理 = 3,
    外部入口待确认 = 4,
    动作序列待确认 = 5,
    已失效 = 6
};

