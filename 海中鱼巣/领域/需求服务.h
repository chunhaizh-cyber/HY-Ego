    需求结算结果 结算目标状态需求(节点句柄 需求节点, 特征服务& 特征, 节点句柄 目标特征,
        节点句柄 当前特征值, 状态服务& 状态) const {
        if (!节点类型匹配(需求节点, 节点类型::需求)
            || !关系_.存在关系(关系类型::引用, 需求节点, 目标特征)
            || !关系_.存在关系(关系类型::引用, 需求节点, 当前特征值)) {
            return 需求结算结果::证据不足;
        }
        const auto 目标状态 = 读取需求目标状态(需求节点);
        if (!目标状态.has_value()) {
            return 需求结算结果::证据不足;
        }
        const auto 目标状态值 = 状态.读取状态值(目标状态.value());
        const auto 当前状态材料 = 特征.读取I64特征值(目标特征, 当前特征值);
        if (!目标状态值.has_value() || !当前状态材料.has_value()) {
            return 需求结算结果::证据不足;
        }
        return 当前状态材料.value() >= 目标状态值.value() ? 需求结算结果::已满足 : 需求结算结果::未满足;
    }

    需求结算结果 按实例特征槽位结算目标状态需求(
        节点句柄 需求节点,
        const 特征服务& 特征,
        节点句柄 实例特征槽位,
        节点句柄 当前特征值,
        const 状态服务& 状态) const {
        if (!节点类型匹配(需求节点, 节点类型::需求)) {
            return 需求结算结果::证据不足;
        }
        const auto 目标宿主 = 读取需求目标宿主(需求节点);
        const auto 特征定义 = 读取需求目标特征(需求节点);
        const auto 正式当前特征值 = 读取需求当前特征状态材料(需求节点);
        const auto 目标状态 = 读取需求目标状态(需求节点);
        if (!目标宿主.has_value()
            || !特征定义.has_value()
            || !正式当前特征值.has_value()
            || 正式当前特征值.value() != 当前特征值
            || !目标状态.has_value()) {
            return 需求结算结果::证据不足;
        }
        const auto 正式实例槽位 = 特征.读取宿主特征槽位(
            目标宿主.value(), 特征定义.value());
        if (正式实例槽位 != std::optional<节点句柄>{实例特征槽位}) {
            return 需求结算结果::证据不足;
        }
        const auto 当前状态材料 = 特征.读取I64特征值(实例特征槽位, 当前特征值);
        const auto 目标状态值 = 状态.读取状态值(目标状态.value());
        if (!当前状态材料.has_value() || !目标状态值.has_value()) {
            return 需求结算结果::证据不足;
        }
        return 当前状态材料.value() >= 目标状态值.value()
            ? 需求结算结果::已满足
            : 需求结算结果::未满足;
    }

    节点句柄 记录结算状态(节点句柄 需求节点, 需求结算结果 结果, std::uint64_t 发生时间戳,
        状态服务& 状态, 节点句柄 场景, 节点句柄 存在) {
        const auto 需求记录 = 节点_.读取节点(需求节点);
        if (!需求记录.has_value() || 需求记录->类型 != 节点类型::需求
            || 结果 == 需求结算结果::未结算
            || 发生时间戳 == 0) {
            return {};
        }
        return 状态.创建实例状态(场景, 存在, 发生时间戳, static_cast<std::int64_t>(结果));
    }

    节点句柄 记录需求结算(节点句柄 需求节点, 节点句柄 来源任务, 节点句柄 目标状态,
        节点句柄 实际状态, 需求结算结果 结果, std::uint64_t 发生时间戳, 状态服务& 状态,
        动态服务& 动态, 因果服务& 因果, 节点句柄 场景, 节点句柄 结算主体存在,
        节点句柄 结算动态证据, std::optional<节点句柄> 因果引用 = std::nullopt) {
        (void)因果;
        if (!节点类型匹配(需求节点, 节点类型::需求)
            || !节点类型匹配(来源任务, 节点类型::任务)
            || !节点类型匹配(目标状态, 节点类型::状态)
            || !节点类型匹配(实际状态, 节点类型::状态)
            || !节点类型匹配(场景, 节点类型::场景)
            || !节点类型匹配(结算主体存在, 节点类型::存在)
            || !动态.动态是否实例动态(结算动态证据)
            || !发生时间戳可用(发生时间戳)
            || 结果 == 需求结算结果::未结算) {
            return {};
        }
        const auto 需求目标状态 = 读取需求目标状态(需求节点);
        if (!需求目标状态.has_value() || !(需求目标状态.value() == 目标状态)) {
            return {};
        }
        if (因果引用.has_value() && !节点类型匹配(因果引用.value(), 节点类型::因果引用)) {
            return {};
        }
        const auto 比较结果 = 比较目标状态和实际状态(目标状态, 实际状态, 状态);
        if (比较结果 != 结果) {
            return {};
        }
        const auto 结算状态 = 记录结算状态(需求节点, 结果, 发生时间戳, 状态, 场景, 结算主体存在);
        if (!追根因检查(句柄有效(结算状态), L"记录需求结算时结算状态创建不及预期。")) {
            return {};
        }
        const auto 结算记录关系 = 关系_.创建关系(
            关系类型::归属, 需求节点, 结算状态, static_cast<std::int64_t>(发生时间戳));
        const auto 来源任务关系 = 关系_.创建关系(关系类型::引用, 结算状态, 来源任务, 结算来源任务顺序号);
        const auto 目标状态关系 = 关系_.创建关系(关系类型::模板, 结算状态, 目标状态, 结算目标状态顺序号);
        const auto 实际状态关系 = 关系_.创建关系(关系类型::引用, 结算状态, 实际状态, 结算实际状态顺序号);
        const auto 动态证据关系 = 关系_.创建关系(关系类型::因果来源, 结算状态, 结算动态证据, 结算动态证据顺序号);
        if (!追根因检查(句柄有效(结算记录关系)
            && 句柄有效(来源任务关系)
            && 句柄有效(目标状态关系)
            && 句柄有效(实际状态关系)
            && 句柄有效(动态证据关系),
            L"记录需求结算时结算关系组写入不及预期。")) {
            return {};
        }
        if (因果引用.has_value()) {
            const auto 因果引用关系 = 关系_.创建关系(关系类型::引用, 结算状态, 因果引用.value(), 结算因果引用顺序号);
            if (!追根因检查(句柄有效(因果引用关系), L"记录需求结算时可选因果引用关系写入不及预期。")) {
                return {};
            }
        }
        return 追根因检查(结算记录完整(结算状态), L"记录需求结算后读回结算记录不完整。") ? 结算状态 : 节点句柄{};
    }

    std::optional<节点句柄> 读取最近结算记录(节点句柄 需求节点, const 状态服务& 状态) const {
        if (!节点类型匹配(需求节点, 节点类型::需求)) {
            return std::nullopt;
        }
        const auto 记录关系组 = 关系_.获取关系记录组(需求节点, 关系类型::归属);
        std::optional<关系记录> 最近关系;
        std::uint64_t 最近时间戳 = 0;
        std::uint64_t 最近创建序号 = 0;
        for (const auto& 记录 : 记录关系组) {
            if (!结算记录完整(记录.目标节点)) {
                continue;
            }
            const auto 时间戳 = 状态.读取发生时间戳(记录.目标节点);
            const auto 节点记录 = 节点_.读取节点(记录.目标节点);
            if (!时间戳.has_value() || !节点记录.has_value()) {
                continue;
            }
            const bool 更新为最近 =
                !最近关系.has_value()
                || 时间戳.value() > 最近时间戳
                || (时间戳.value() == 最近时间戳 && 记录.顺序号 > 最近关系->顺序号)
                || (时间戳.value() == 最近时间戳 && 记录.顺序号 == 最近关系->顺序号
                    && 节点记录->创建序号 > 最近创建序号);
            const bool 无法区分 =
                最近关系.has_value()
                && 时间戳.value() == 最近时间戳
                && 记录.顺序号 == 最近关系->顺序号
                && 节点记录->创建序号 == 最近创建序号
                && !(记录.目标节点 == 最近关系->目标节点);
            if (无法区分) {
                return std::nullopt;
            }
            if (更新为最近) {
                最近关系 = 记录;
                最近时间戳 = 时间戳.value();
                最近创建序号 = 节点记录->创建序号;
            }
        }
        if (!最近关系.has_value()) {
            return std::nullopt;
        }
        return 最近关系->目标节点;
    }

