// 文件规则：需求服务只建立需求入口；需求满足裁决必须由结构读数生成。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "特征值服务.h"

namespace 海中鱼巣 {

enum class 需求结算结果 : std::uint32_t {
    未结算 = 0,
    已满足 = 1,
    未满足 = 2,
    证据不足 = 3
};

class 需求服务 {
public:
    需求服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 创建需求() {
        return 节点_.创建节点(节点类型::需求, 主信息_.创建主信息());
    }

    节点句柄 创建I64目标需求(std::int64_t 目标值) {
        const auto 主信息句柄 = 主信息_.创建主信息();
        主信息_.写入I64值(主信息句柄, 目标值);
        return 节点_.创建节点(节点类型::需求, 主信息句柄);
    }

    需求结算结果 结算I64目标(节点句柄 需求节点, 特征值服务& 特征值, 节点句柄 特征值节点) const {
        const auto 需求记录 = 节点_.读取节点(需求节点);
        if (!需求记录.has_value() || 需求记录->类型 != 节点类型::需求) {
            return 需求结算结果::证据不足;
        }
        const auto 目标值 = 主信息_.读取I64值(需求记录->主信息);
        const auto 当前值 = 特征值.读取I64值(特征值节点);
        if (!目标值.has_value() || !当前值.has_value()) {
            return 需求结算结果::证据不足;
        }
        return 当前值.value() == 目标值.value() ? 需求结算结果::已满足 : 需求结算结果::未满足;
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
