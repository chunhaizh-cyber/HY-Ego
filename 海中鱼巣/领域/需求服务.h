// 文件规则：需求服务只建立需求入口；需求满足裁决必须由结构读数生成。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"
#include "特征服务.h"
#include "状态服务.h"

#include <cstdint>
#include <optional>

namespace 海中鱼巣 {

enum class 需求结算结果 : std::uint32_t {
    未结算 = 0,
    已满足 = 1,
    未满足 = 2,
    证据不足 = 3
};

class 需求服务 {
public:
    需求服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系)
        : 主信息_(主信息), 节点_(节点), 关系_(关系) {
    }

    节点句柄 创建需求() {
        return 节点_.创建节点(节点类型::需求, 主信息_.创建主信息());
    }

    节点句柄 创建目标状态需求(节点句柄 目标存在, 节点句柄 目标特征, 特征服务& 特征,
        节点句柄 当前特征值, 状态服务& 状态, std::int64_t 目标状态值) {
        if (!节点类型匹配(目标存在, 节点类型::存在)
            || !节点类型匹配(目标特征, 节点类型::特征)
            || !特征.读取I64特征值(目标特征, 当前特征值).has_value()) {
            return {};
        }
        const auto 目标状态 = 状态.创建抽象状态(目标状态值);
        if (!句柄有效(目标状态)) {
            return {};
        }
        const auto 需求节点 = 创建需求();
        if (!句柄有效(需求节点)) {
            return {};
        }
        const auto 存在关系 = 关系_.创建关系(关系类型::引用, 需求节点, 目标存在);
        const auto 特征关系 = 关系_.创建关系(关系类型::引用, 需求节点, 目标特征);
        const auto 当前值关系 = 关系_.创建关系(关系类型::引用, 需求节点, 当前特征值);
        const auto 目标状态关系 = 关系_.创建关系(关系类型::模板, 需求节点, 目标状态);
        if (!句柄有效(存在关系) || !句柄有效(特征关系)
            || !句柄有效(当前值关系) || !句柄有效(目标状态关系)) {
            return {};
        }
        return 需求节点;
    }

    std::optional<节点句柄> 读取需求目标状态(节点句柄 需求节点) const {
        if (!节点类型匹配(需求节点, 节点类型::需求)) {
            return std::nullopt;
        }
        const auto 目标组 = 关系_.获取目标节点组(需求节点, 关系类型::模板);
        std::optional<节点句柄> 目标状态;
        for (const auto& 目标 : 目标组) {
            if (!节点类型匹配(目标, 节点类型::状态)) {
                continue;
            }
            if (目标状态.has_value()) {
                return std::nullopt;
            }
            目标状态 = 目标;
        }
        return 目标状态;
    }

    需求结算结果 结算目标状态需求(节点句柄 需求节点, 特征服务& 特征, 节点句柄 目标特征,
        节点句柄 当前特征值, 状态服务& 状态) const {
        if (!节点类型匹配(需求节点, 节点类型::需求)
            || !关系_.存在关系(关系类型::引用, 需求节点, 目标特征)
            || !关系_.存在关系(关系类型::引用, 需求节点, 当前特征值)) {
            return 需求结算结果::证据不足;
        }
        const auto 目标状态 = 读取需求目标状态(需求节点);
        if (!目标状态.has_value()) {
            return 需求结算结果::证据不足;
        }
        const auto 目标状态值 = 状态.读取状态值(目标状态.value());
        const auto 当前状态材料 = 特征.读取I64特征值(目标特征, 当前特征值);
        if (!目标状态值.has_value() || !当前状态材料.has_value()) {
            return 需求结算结果::证据不足;
        }
        return 当前状态材料.value() >= 目标状态值.value() ? 需求结算结果::已满足 : 需求结算结果::未满足;
    }

    节点句柄 记录结算状态(节点句柄 需求节点, 需求结算结果 结果, std::uint64_t 发生时间戳,
        状态服务& 状态, 节点句柄 场景, 节点句柄 存在) {
        const auto 需求记录 = 节点_.读取节点(需求节点);
        if (!需求记录.has_value() || 需求记录->类型 != 节点类型::需求 || 结果 == 需求结算结果::未结算) {
            return {};
        }
        return 状态.创建实例状态(场景, 存在, 发生时间戳, static_cast<std::int64_t>(结果));
    }

private:
    bool 节点类型匹配(节点句柄 节点句柄值, 节点类型 类型) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        return 记录.has_value() && 记录->类型 == 类型;
    }

    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
};

}
