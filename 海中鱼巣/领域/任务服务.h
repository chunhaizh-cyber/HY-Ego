// 文件规则：任务服务承接需求生成任务入口；线程不是动作来源。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"

namespace 海中鱼巣 {

class 任务服务 {
public:
    任务服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 创建任务() {
        return 节点_.创建节点(节点类型::任务, 主信息_.创建主信息());
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
