// 文件规则：任务服务承接需求生成任务入口；线程不是动作来源。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"

namespace 海中鱼巣 {

class 任务服务 {
public:
    任务服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 创建任务() {
        return 节点_.创建节点(节点类型::任务, 主信息_.创建主信息());
    }

    节点句柄 按需求创建任务(节点句柄 需求节点) {
        const auto 需求记录 = 节点_.读取节点(需求节点);
        if (!需求记录.has_value() || 需求记录->类型 != 节点类型::需求) {
            return {};
        }
        return 创建任务();
    }

    节点句柄 记录任务状态(节点句柄 任务节点, std::int64_t 状态值) {
        const auto 任务记录 = 节点_.读取节点(任务节点);
        if (!任务记录.has_value() || 任务记录->类型 != 节点类型::任务) {
            return {};
        }
        const auto 主信息句柄 = 主信息_.创建主信息();
        主信息_.写入I64值(主信息句柄, 状态值);
        return 节点_.创建节点(节点类型::状态, 主信息句柄);
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
