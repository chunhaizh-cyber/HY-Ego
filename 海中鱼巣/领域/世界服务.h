// 文件规则：世界服务只做基础信息聚合编排；除兼容基础信息通用节点外，不直接写原子基础结构。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "存在服务.h"
#include "场景服务.h"
#include "特征服务.h"
#include "状态服务.h"
#include "动态服务.h"
#include "二次特征服务.h"
#include "因果服务.h"

#include <cstdint>
#include <optional>

namespace 海中鱼巣 {

enum class 世界兼容壳复核状态 : std::uint32_t {
    可作为兼容壳 = 1,
    已明确类型应走原子服务 = 2,
    无效节点 = 3
};

struct 世界兼容壳复核结果 {
    节点句柄 节点;
    世界兼容壳复核状态 状态 = 世界兼容壳复核状态::无效节点;
};

class 世界服务 {
public:
    世界服务(主信息仓库& 主信息, 节点仓库& 节点, 存在服务& 存在, 场景服务& 场景,
        特征服务& 特征, 状态服务& 状态, 动态服务& 动态,
        二次特征服务& 二次特征, 因果服务& 因果)
        : 主信息_(主信息), 节点_(节点), 存在_(存在), 场景_(场景), 特征_(特征),
        状态_(状态), 动态_(动态), 二次特征_(二次特征), 因果_(因果) {
    }

    节点句柄 创建基础信息() {
        const auto 主信息句柄 = 主信息_.创建主信息();
        const auto 基础信息 = 节点_.创建节点(节点类型::基础信息, 主信息句柄);
        const auto 记录 = 节点_.读取节点(基础信息);
        if (!记录.has_value() || 记录->类型 != 节点类型::基础信息
            || !主信息_.主信息是否有效(记录->主信息)) {
            return {};
        }
        return 基础信息;
    }

    节点句柄 创建存在() {
        return 存在_.创建实际存在();
    }

    节点句柄 创建场景() {
        return 场景_.创建场景();
    }

    节点句柄 创建现实场景() {
        return 创建场景();
    }

    节点句柄 创建场景存在(节点句柄 场景) {
        if (!场景_.场景是否有效(场景)) {
            return {};
        }
        const auto 存在 = 存在_.创建实际存在();
        if (!句柄有效(存在) || !句柄有效(场景_.接纳存在(场景, 存在))) {
            return {};
        }
        return 存在;
    }

    关系句柄 接纳存在(节点句柄 场景, 节点句柄 存在) {
        return 场景_.接纳存在(场景, 存在);
    }

    世界兼容壳复核结果 复核兼容壳用途(节点句柄 节点) const {
        const auto 记录 = 节点_.读取节点(节点);
        if (!记录.has_value()) {
            return {节点, 世界兼容壳复核状态::无效节点};
        }
        return 记录->类型 == 节点类型::基础信息
            ? 世界兼容壳复核结果{节点, 世界兼容壳复核状态::可作为兼容壳}
            : 世界兼容壳复核结果{节点, 世界兼容壳复核状态::已明确类型应走原子服务};
    }

    std::optional<节点句柄> 读取抽象状态候选(节点句柄 状态节点) const {
        const auto 记录 = 节点_.读取节点(状态节点);
        if (!记录.has_value() || 记录->类型 != 节点类型::状态 || 状态_.状态是否实例状态(状态节点)
            || !状态_.读取状态值(状态节点).has_value()) {
            return std::nullopt;
        }
        return 状态节点;
    }

    std::optional<节点句柄> 读取抽象动态候选(节点句柄 动态节点) const {
        const auto 记录 = 节点_.读取节点(动态节点);
        if (!记录.has_value() || 记录->类型 != 节点类型::动态 || 动态_.动态是否实例动态(动态节点)) {
            return std::nullopt;
        }
        return 动态节点;
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
    存在服务& 存在_;
    场景服务& 场景_;
    特征服务& 特征_;
    状态服务& 状态_;
    动态服务& 动态_;
    二次特征服务& 二次特征_;
    因果服务& 因果_;
};

}
