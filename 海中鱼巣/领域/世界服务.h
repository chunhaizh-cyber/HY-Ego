// 文件规则：世界服务只做基础信息聚合编排；除兼容基础信息通用节点外，不直接写原子基础结构。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "存在服务.h"
#include "场景服务.h"
#include "特征服务.h"
#include "状态服务.h"
#include "动态服务.h"
#include "二次特征服务.h"
#include "因果服务.h"

namespace 海中鱼巣 {

class 世界服务 {
public:
    世界服务(主信息仓库& 主信息, 节点仓库& 节点, 存在服务& 存在, 场景服务& 场景,
        特征服务& 特征, 状态服务& 状态, 动态服务& 动态,
        二次特征服务& 二次特征, 因果服务& 因果)
        : 主信息_(主信息), 节点_(节点), 存在_(存在), 场景_(场景), 特征_(特征),
        状态_(状态), 动态_(动态), 二次特征_(二次特征), 因果_(因果) {
    }

    节点句柄 创建基础信息() {
        return 节点_.创建节点(节点类型::基础信息, 主信息_.创建主信息());
    }

    节点句柄 创建存在() {
        return 存在_.创建实际存在();
    }

    节点句柄 创建场景() {
        return 场景_.创建场景();
    }

    节点句柄 创建现实场景() {
        return 创建场景();
    }

    节点句柄 创建场景存在(节点句柄 场景) {
        if (!场景_.场景是否有效(场景)) {
            return {};
        }
        const auto 存在 = 存在_.创建实际存在();
        if (!句柄有效(存在) || !句柄有效(场景_.接纳存在(场景, 存在))) {
            return {};
        }
        return 存在;
    }

    关系句柄 接纳存在(节点句柄 场景, 节点句柄 存在) {
        return 场景_.接纳存在(场景, 存在);
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
    存在服务& 存在_;
    场景服务& 场景_;
    特征服务& 特征_;
    状态服务& 状态_;
    动态服务& 动态_;
    二次特征服务& 二次特征_;
    因果服务& 因果_;
};

}
