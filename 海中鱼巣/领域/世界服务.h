// 文件规则：世界服务是基础信息、存在和场景的写入入口；不得绕过仓库封口。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"

namespace 海中鱼巣 {

class 世界服务 {
public:
    世界服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 创建基础信息() {
        return 创建世界节点(节点类型::基础信息);
    }

    节点句柄 创建存在() {
        return 创建世界节点(节点类型::存在);
    }

    节点句柄 创建场景() {
        return 创建世界节点(节点类型::场景);
    }

private:
    节点句柄 创建世界节点(节点类型 类型) {
        return 节点_.创建节点(类型, 主信息_.创建主信息());
    }

    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
