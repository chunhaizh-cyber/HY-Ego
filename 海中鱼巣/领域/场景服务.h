// 文件规则：场景服务只管理场景入口和场景内临时收纳；不得把显示输出写成场景事实。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"

namespace 海中鱼巣 {

class 场景服务 {
public:
    场景服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系)
        : 主信息_(主信息), 节点_(节点), 关系_(关系) {
    }

    节点句柄 创建场景() {
        return 节点_.创建节点(节点类型::场景, 主信息_.创建主信息());
    }

    关系句柄 接纳存在(节点句柄 场景, 节点句柄 存在) {
        if (!节点类型匹配(场景, 节点类型::场景) || !节点类型匹配(存在, 节点类型::存在)) {
            return {};
        }
        return 关系_.创建关系(关系类型::归属, 场景, 存在);
    }

    关系句柄 收纳实例状态(节点句柄 场景, 节点句柄 实例状态) {
        if (!节点类型匹配(场景, 节点类型::场景) || !节点类型匹配(实例状态, 节点类型::状态)) {
            return {};
        }
        return 关系_.创建关系(关系类型::运行期临时, 场景, 实例状态);
    }

    关系句柄 收纳实例动态(节点句柄 场景, 节点句柄 实例动态) {
        if (!节点类型匹配(场景, 节点类型::场景) || !节点类型匹配(实例动态, 节点类型::动态)) {
            return {};
        }
        return 关系_.创建关系(关系类型::运行期临时, 场景, 实例动态);
    }

    bool 场景是否有效(节点句柄 场景) const {
        return 节点类型匹配(场景, 节点类型::场景);
    }

private:
    bool 节点类型匹配(节点句柄 节点句柄值, 节点类型 类型) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        return 记录.has_value() && 记录->类型 == 类型;
    }

    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
};

}
