// 文件规则：语素服务只把外部词面请求转为结构入口；外部文本不得直接成为世界事实。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"
#include "../核心/索引仓库.h"
#include "../核心/容错检查.h"

#include <algorithm>
#include <cwctype>
#include <optional>
#include <string_view>
#include <vector>

namespace 海中鱼巣 {

enum class 语素准入状态 : std::uint32_t {
    可作为最小词单元 = 1,
    空文本 = 2,
    含空白 = 3,
    含组合分隔符 = 4,
    超出第一轮长度 = 5
};

struct 语素概念入口创建结果 {
    节点句柄 语素入口;
    主信息句柄 主信息;
    关系句柄 对应关系;
    关系句柄 概念关系;
    std::uint64_t 主键 = 0;
    bool 主键由本次绑定 = false;

    bool 成功() const {
        return 句柄有效(语素入口)
            && 句柄有效(主信息)
            && 句柄有效(对应关系)
            && 句柄有效(概念关系);
    }
};

class 语素服务 {
private:
    struct 语素入口创建结果 {
        节点句柄 语素入口;
        主信息句柄 主信息;
        关系句柄 对应关系;
        std::uint64_t 主键 = 0;
        bool 主键由本次绑定 = false;
    };

public:
    语素服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系, 索引仓库& 索引)
        : 主信息_(主信息), 节点_(节点), 关系_(关系), 索引_(索引) {
    }

    语素准入状态 判断文本是否可作为最小词单元(std::wstring_view 文本) const {
        if (文本.empty()) {
            return 语素准入状态::空文本;
        }
        std::uint64_t 有效字符数 = 0;
        for (const wchar_t 字符 : 文本) {
            if (std::iswspace(字符) != 0) {
                return 语素准入状态::含空白;
            }
            if (字符是组合分隔符(字符)) {
                return 语素准入状态::含组合分隔符;
            }
            ++有效字符数;
        }
        if (有效字符数 > 3) {
            return 语素准入状态::超出第一轮长度;
        }
        return 语素准入状态::可作为最小词单元;
    }

    节点句柄 创建语素入口(节点句柄 对应信息节点, std::uint64_t 主键 = 0) {
        const bool 对应信息可绑定 = 节点是可作为语素对应信息(对应信息节点);
        if (!对应信息可绑定) {
            海中鱼巣容错检查(对应信息可绑定, L"创建语素入口收到不允许绑定的信息节点。");
            return {};
        }
        const auto 结果 = 创建语素入口并保留收口材料(对应信息节点, 主键);
        return 结果.语素入口;
    }

    节点句柄 创建概念入口(std::wstring_view 文本, 节点句柄 对应信息节点, 节点句柄 概念节点, std::uint64_t 主键 = 0) {
        return 创建概念入口并保留收口材料(文本, 对应信息节点, 概念节点, 主键).语素入口;
    }

    语素概念入口创建结果 创建概念入口并保留收口材料(
        std::wstring_view 文本,
        节点句柄 对应信息节点,
        节点句柄 概念节点,
        std::uint64_t 主键 = 0) {
        const bool 文本可准入 = 判断文本是否可作为最小词单元(文本) == 语素准入状态::可作为最小词单元;
        if (!文本可准入) {
            海中鱼巣容错检查(文本可准入, L"创建概念入口收到不可作为最小词单元的文本。");
            return {};
        }
        const bool 对应信息可绑定 = 节点是可作为语素对应信息(对应信息节点);
        if (!对应信息可绑定) {
            海中鱼巣容错检查(对应信息可绑定, L"创建概念入口收到不允许绑定的信息节点。");
            return {};
        }
        const bool 概念节点可追溯 = 节点是可作为概念追溯目标(概念节点);
        if (!概念节点可追溯) {
            海中鱼巣容错检查(概念节点可追溯, L"创建概念入口收到无效概念追溯目标。");
            return {};
        }
        auto 结果 = 创建语素入口并保留收口材料(对应信息节点, 主键);
        if (!句柄有效(结果.语素入口)) {
            return {};
        }
        const auto 概念关系 = 关系_.创建关系(关系类型::语素概念追溯, 结果.语素入口, 概念节点);
        const bool 概念追溯可读回 = 句柄有效(概念关系) && 节点组包含(读取入口概念追溯组(结果.语素入口), 概念节点);
        if (!概念追溯可读回) {
            海中鱼巣容错检查(概念追溯可读回, L"创建概念入口无法读回概念追溯关系。");
            清理本次创建的语素入口(结果);
            return {};
        }
        return {
            结果.语素入口,
            结果.主信息,
            结果.对应关系,
            概念关系,
            结果.主键,
            结果.主键由本次绑定
        };
    }

    std::optional<节点句柄> 读取入口绑定目标(节点句柄 语素入口) const {
        const auto 目标组 = 读取入口绑定目标组(语素入口);
        if (目标组.empty()) {
            return std::nullopt;
        }
        return 目标组.front();
    }

    std::optional<节点句柄> 读取入口概念追溯(节点句柄 语素入口) const {
        const auto 概念组 = 读取入口概念追溯组(语素入口);
        if (概念组.empty()) {
            return std::nullopt;
        }
        return 概念组.front();
    }

    bool 追加语素对应信息(节点句柄 语素入口, 节点句柄 对应信息节点) {
        const bool 入口有效 = 节点是语素入口(语素入口);
        if (!入口有效) {
            海中鱼巣容错检查(入口有效, L"追加语素对应信息收到无效语素入口。");
            return false;
        }
        const bool 对应信息可绑定 = 节点是可作为语素对应信息(对应信息节点);
        if (!对应信息可绑定) {
            海中鱼巣容错检查(对应信息可绑定, L"追加语素对应信息收到不允许绑定的信息节点。");
            return false;
        }
        if (关系_.存在关系(关系类型::语素对应信息, 语素入口, 对应信息节点)) {
            return false;
        }
        const auto 新关系 = 关系_.创建关系(关系类型::语素对应信息, 语素入口, 对应信息节点);
        const bool 写入并读回 = 句柄有效(新关系) && 节点组包含(读取入口绑定目标组(语素入口), 对应信息节点);
        海中鱼巣容错检查(写入并读回, L"追加语素对应信息后无法读回目标节点。");
        return 写入并读回;
    }

    bool 追加语素概念追溯(节点句柄 语素入口, 节点句柄 概念节点) {
        const bool 入口有效 = 节点是语素入口(语素入口);
        if (!入口有效) {
            海中鱼巣容错检查(入口有效, L"追加语素概念追溯收到无效语素入口。");
            return false;
        }
        const bool 概念节点可追溯 = 节点是可作为概念追溯目标(概念节点);
        if (!概念节点可追溯) {
            海中鱼巣容错检查(概念节点可追溯, L"追加语素概念追溯收到无效概念追溯目标。");
            return false;
        }
        if (关系_.存在关系(关系类型::语素概念追溯, 语素入口, 概念节点)) {
            return false;
        }
        const auto 新关系 = 关系_.创建关系(关系类型::语素概念追溯, 语素入口, 概念节点);
        const bool 写入并读回 = 句柄有效(新关系) && 节点组包含(读取入口概念追溯组(语素入口), 概念节点);
        海中鱼巣容错检查(写入并读回, L"追加语素概念追溯后无法读回目标节点。");
        return 写入并读回;
    }

    std::vector<节点句柄> 读取入口绑定目标组(节点句柄 语素入口) const {
        if (!节点是语素入口(语素入口)) {
            return {};
        }
        return 关系_.获取目标节点组(语素入口, 关系类型::语素对应信息);
    }

    std::vector<节点句柄> 读取入口概念追溯组(节点句柄 语素入口) const {
        if (!节点是语素入口(语素入口)) {
            return {};
        }
        return 关系_.获取目标节点组(语素入口, 关系类型::语素概念追溯);
    }

    std::vector<节点句柄> 读取概念名称入口组(节点句柄 概念节点) const {
        if (!节点是可作为概念追溯目标(概念节点)) {
            return {};
        }
        auto 入口组 = 关系_.获取来源节点组(概念节点, 关系类型::语素概念追溯);
        入口组.erase(
            std::remove_if(入口组.begin(), 入口组.end(), [this](const auto& 入口) {
                return !节点是语素入口(入口);
            }),
            入口组.end());
        std::sort(入口组.begin(), 入口组.end(), [](const auto& 左, const auto& 右) {
            if (左.仓库编号 != 右.仓库编号) {
                return 左.仓库编号 < 右.仓库编号;
            }
            if (左.节点编号 != 右.节点编号) {
                return 左.节点编号 < 右.节点编号;
            }
            return 左.版本号 < 右.版本号;
        });
        入口组.erase(std::unique(入口组.begin(), 入口组.end()), 入口组.end());
        return 入口组;
    }

    std::vector<节点句柄> 读取概念名称入口组(
        节点句柄 概念节点,
        const 结构事务令牌& 令牌) const {
        if (!节点是可作为概念追溯目标(概念节点, 令牌)) {
            return {};
        }
        auto 入口组 = 关系_.获取来源节点组(
            概念节点, 关系类型::语素概念追溯, 令牌);
        入口组.erase(
            std::remove_if(入口组.begin(), 入口组.end(), [this, &令牌](const auto& 入口) {
                return !节点是语素入口(入口, 令牌);
            }),
            入口组.end());
        std::sort(入口组.begin(), 入口组.end(), [](const auto& 左, const auto& 右) {
            if (左.仓库编号 != 右.仓库编号) return 左.仓库编号 < 右.仓库编号;
            if (左.节点编号 != 右.节点编号) return 左.节点编号 < 右.节点编号;
            return 左.版本号 < 右.版本号;
        });
        入口组.erase(std::unique(入口组.begin(), 入口组.end()), 入口组.end());
        return 入口组;
    }

    bool 语素入口同一(节点句柄 左入口, 节点句柄 右入口) const {
        return 左入口 == 右入口 && 节点_.节点是否有效(左入口);
    }

private:
    bool 节点是语素入口(节点句柄 节点句柄值) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        return 记录.has_value() && 记录->类型 == 节点类型::语素;
    }

    bool 节点是语素入口(节点句柄 节点句柄值, const 结构事务令牌& 令牌) const {
        const auto 记录 = 节点_.读取节点(节点句柄值, 令牌);
        return 记录.has_value() && 记录->类型 == 节点类型::语素;
    }

    bool 字符是组合分隔符(wchar_t 字符) const {
        return 字符 == L'/' || 字符 == L'\\' || 字符 == L'-' || 字符 == L'_'
            || 字符 == L'+' || 字符 == L',' || 字符 == L'.' || 字符 == L';'
            || 字符 == L':' || 字符 == L'，' || 字符 == L'。' || 字符 == L'；'
            || 字符 == L'：' || 字符 == L'、';
    }

    bool 节点组包含(const std::vector<节点句柄>& 节点组, 节点句柄 目标) const {
        for (const auto& 节点值 : 节点组) {
            if (节点值 == 目标) {
                return true;
            }
        }
        return false;
    }

    bool 节点是可作为语素对应信息(节点句柄 节点句柄值) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        if (!记录.has_value()) {
            return false;
        }
        return 记录->类型 == 节点类型::基础信息
            || 记录->类型 == 节点类型::存在
            || 记录->类型 == 节点类型::场景
            || 记录->类型 == 节点类型::特征
            || (记录->类型 == 节点类型::状态 && !关系_.存在目标关系(关系类型::运行期临时, 节点句柄值))
            || (记录->类型 == 节点类型::动态 && !关系_.存在目标关系(关系类型::运行期临时, 节点句柄值))
            || 记录->类型 == 节点类型::二次特征
            || 记录->类型 == 节点类型::因果引用
            || 记录->类型 == 节点类型::需求
            || 记录->类型 == 节点类型::任务
            || 记录->类型 == 节点类型::方法;
    }

    bool 节点是可作为概念追溯目标(节点句柄 节点句柄值) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        if (!记录.has_value()) {
            return false;
        }
        return 记录->类型 == 节点类型::基础信息
            || 记录->类型 == 节点类型::存在
            || 记录->类型 == 节点类型::特征
            || (记录->类型 == 节点类型::状态 && !关系_.存在目标关系(关系类型::运行期临时, 节点句柄值))
            || (记录->类型 == 节点类型::动态 && !关系_.存在目标关系(关系类型::运行期临时, 节点句柄值))
            || 记录->类型 == 节点类型::二次特征
            || 记录->类型 == 节点类型::因果引用;
    }

    bool 节点是可作为概念追溯目标(
        节点句柄 节点句柄值,
        const 结构事务令牌& 令牌) const {
        const auto 记录 = 节点_.读取节点(节点句柄值, 令牌);
        if (!记录.has_value()) {
            return false;
        }
        return 记录->类型 == 节点类型::基础信息
            || 记录->类型 == 节点类型::存在
            || 记录->类型 == 节点类型::特征
            || (记录->类型 == 节点类型::状态
                && !关系_.存在目标关系(关系类型::运行期临时, 节点句柄值, 令牌))
            || (记录->类型 == 节点类型::动态
                && !关系_.存在目标关系(关系类型::运行期临时, 节点句柄值, 令牌))
            || 记录->类型 == 节点类型::二次特征
            || 记录->类型 == 节点类型::因果引用;
    }

    语素入口创建结果 创建语素入口并保留收口材料(节点句柄 对应信息节点, std::uint64_t 主键) {
        语素入口创建结果 结果;
        结果.主键 = 主键;
        if (主键 != 0 && 索引_.按主键查节点(主键).has_value()) {
            return 结果;
        }

        结果.主信息 = 主信息_.创建主信息();
        if (!句柄有效(结果.主信息)) {
            return {};
        }

        结果.语素入口 = 节点_.创建节点(节点类型::语素, 结果.主信息);
        if (!句柄有效(结果.语素入口)) {
            清理本次创建的语素入口(结果);
            return {};
        }

        结果.对应关系 = 关系_.创建关系(关系类型::语素对应信息, 结果.语素入口, 对应信息节点);
        if (!句柄有效(结果.对应关系)) {
            清理本次创建的语素入口(结果);
            return {};
        }

        if (主键 != 0) {
            结果.主键由本次绑定 = 索引_.绑定主键(主键, 结果.语素入口);
            if (!结果.主键由本次绑定) {
                清理本次创建的语素入口(结果);
                return {};
            }
        }

        const bool 入口可读 = 节点是语素入口(结果.语素入口);
        const bool 关系可读 = 节点组包含(读取入口绑定目标组(结果.语素入口), 对应信息节点);
        const bool 主键可读 = 主键 == 0 || (索引_.按主键查节点(主键).has_value() && 索引_.按主键查节点(主键).value() == 结果.语素入口);
        const bool 创建结果可读回 = 入口可读 && 关系可读 && 主键可读;
        if (!创建结果可读回) {
            海中鱼巣容错检查(创建结果可读回, L"创建语素入口写入后无法读回完整结构。");
            清理本次创建的语素入口(结果);
            return {};
        }
        return 结果;
    }

    void 清理本次创建的语素入口(const 语素入口创建结果& 结果) {
        if (结果.主键由本次绑定 && 结果.主键 != 0) {
            const auto 当前节点 = 索引_.按主键查节点(结果.主键);
            if (当前节点.has_value() && 当前节点.value() == 结果.语素入口) {
                索引_.删除主键(结果.主键);
            }
        }
        if (句柄有效(结果.对应关系)) {
            关系_.删除关系(结果.对应关系);
        }
        if (句柄有效(结果.语素入口)) {
            节点_.删除节点(结果.语素入口);
        }
        if (句柄有效(结果.主信息)) {
            主信息_.删除主信息(结果.主信息);
        }
    }

    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
    索引仓库& 索引_;
};

}
