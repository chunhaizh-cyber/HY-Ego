inline constexpr std::uint32_t 系统角色清单当前版本 = 1;
inline constexpr std::size_t 系统角色稳定键数量 = 21;

enum class 系统角色业务状态 : std::uint8_t {
    已提交 = 1,
    幂等读回 = 2,
    入口拒绝 = 3,
    幂等冲突 = 4,
    许可拒绝 = 5,
    版本漂移 = 6,
    内部不一致 = 7
};

enum class 系统角色稳定键用途 : std::uint8_t {
    未定义 = 0,
    世界根 = 1,
    自我场景 = 2,
    自我存在 = 3,
    安全特征定义 = 4,
    安全实例槽位 = 5,
    安全当前特征值 = 6,
    安全目标状态 = 7,
    安全根需求 = 8,
    服务特征定义 = 9,
    服务实例槽位 = 10,
    服务当前特征值 = 11,
    服务目标状态 = 12,
    服务根需求 = 13,
    概念存在根 = 14,
    概念动态根 = 15,
    概念关系根 = 16,
    概念因果根 = 17,
    方法登记根 = 18,
    方法登记角色状态 = 19,
    方法活跃状态 = 20,
    方法失效状态 = 21
};

struct 系统角色初始化参数 {
    std::uint32_t 清单版本 = 系统角色清单当前版本;
    std::uint64_t 世界根主键 = 0;
    std::uint64_t 自我场景主键 = 0;
    std::uint64_t 自我存在主键 = 0;
    std::uint64_t 安全特征定义主键 = 0;
    std::uint64_t 安全实例槽位主键 = 0;
    std::uint64_t 安全当前特征值主键 = 0;
    std::uint64_t 安全目标状态主键 = 0;
    std::uint64_t 安全根需求主键 = 0;
    std::uint64_t 服务特征定义主键 = 0;
    std::uint64_t 服务实例槽位主键 = 0;
    std::uint64_t 服务当前特征值主键 = 0;
    std::uint64_t 服务目标状态主键 = 0;
    std::uint64_t 服务根需求主键 = 0;
    std::uint64_t 概念存在根主键 = 0;
    std::uint64_t 概念动态根主键 = 0;
    std::uint64_t 概念关系根主键 = 0;
    std::uint64_t 概念因果根主键 = 0;
    std::uint64_t 方法登记根主键 = 0;
    std::uint64_t 方法登记角色状态主键 = 0;
    std::uint64_t 方法活跃状态主键 = 0;
    std::uint64_t 方法失效状态主键 = 0;
    std::int64_t 安全当前值 = 0;
    std::int64_t 安全目标值 = 0;
    std::int64_t 服务当前值 = 0;
    std::int64_t 服务目标值 = 0;
    std::int64_t 动态根材料 = 1;

    std::array<std::uint64_t, 系统角色稳定键数量> 读取稳定键组() const noexcept {
        return {
            世界根主键, 自我场景主键, 自我存在主键,
            安全特征定义主键, 安全实例槽位主键, 安全当前特征值主键,
            安全目标状态主键, 安全根需求主键,
            服务特征定义主键, 服务实例槽位主键, 服务当前特征值主键,
            服务目标状态主键, 服务根需求主键,
            概念存在根主键, 概念动态根主键, 概念关系根主键,
            概念因果根主键, 方法登记根主键, 方法登记角色状态主键,
            方法活跃状态主键, 方法失效状态主键};
    }

    bool 有效() const noexcept {
        if (清单版本 != 系统角色清单当前版本
            || 安全当前值 < 0 || 服务当前值 < 0
            || 安全目标值 <= 安全当前值 || 服务目标值 <= 服务当前值) {
            return false;
        }
        const auto 键组 = 读取稳定键组();
        for (std::size_t 左 = 0; 左 < 键组.size(); ++左) {
            if (键组[左] == 0) return false;
            for (std::size_t 右 = 左 + 1; 右 < 键组.size(); ++右) {
                if (键组[左] == 键组[右]) return false;
            }
        }
        return true;
    }
};

struct 系统角色身份材料 {
    系统角色稳定键用途 用途 = 系统角色稳定键用途::未定义;
    std::uint64_t 稳定主键 = 0;
    节点类型 类型 = 节点类型::未分类;
    节点句柄 节点;
    主信息句柄 主信息;

    bool 完整() const noexcept {
        return 用途 != 系统角色稳定键用途::未定义 && 稳定主键 != 0
            && 类型 != 节点类型::未分类 && 句柄有效(节点) && 句柄有效(主信息)
            && 节点.仓库编号 == 主信息.仓库编号;
    }

    bool operator==(const 系统角色身份材料&) const = default;
};

struct 系统角色关系材料 {
    关系句柄 关系;
    关系类型 类型 = 关系类型::普通父子;
    节点句柄 源节点;
    节点句柄 目标节点;
    std::int64_t 顺序号 = 0;
    记录状态 状态 = 记录状态::无效;

    bool 完整() const noexcept {
        return 句柄有效(关系) && 句柄有效(源节点) && 句柄有效(目标节点)
            && 状态 == 记录状态::有效
            && 关系.仓库编号 == 源节点.仓库编号
            && 关系.仓库编号 == 目标节点.仓库编号;
    }

    bool operator==(const 系统角色关系材料&) const = default;
};

struct 系统根需求角色材料 {
    系统角色身份材料 特征定义;
    系统角色身份材料 实例槽位;
    系统角色身份材料 当前特征值;
    系统角色身份材料 目标状态;
    系统角色身份材料 根需求;
    std::int64_t 当前值 = 0;
    std::int64_t 目标值 = 0;

    bool 完整() const noexcept {
        return 特征定义.完整() && 实例槽位.完整() && 当前特征值.完整()
            && 目标状态.完整() && 根需求.完整() && 当前值 >= 0 && 目标值 > 当前值;
    }

    bool operator==(const 系统根需求角色材料&) const = default;
};

struct 系统角色主体材料 {
    系统角色身份材料 世界根;
    系统角色身份材料 自我场景;
    系统角色身份材料 自我存在;
    系统角色关系材料 场景接纳自我关系;
    系统根需求角色材料 安全根需求;
    系统根需求角色材料 服务根需求;
    系统角色身份材料 概念存在根;
    系统角色身份材料 概念动态根;
    系统角色身份材料 概念关系根;
    系统角色身份材料 概念因果根;
    std::int64_t 动态根材料 = 0;
    系统角色身份材料 方法登记根;
    系统角色身份材料 方法登记角色状态;
    系统角色身份材料 方法活跃状态;
    系统角色身份材料 方法失效状态;

    std::array<const 系统角色身份材料*, 系统角色稳定键数量> 读取身份组() const noexcept {
        return {
            &世界根, &自我场景, &自我存在,
            &安全根需求.特征定义, &安全根需求.实例槽位,
            &安全根需求.当前特征值, &安全根需求.目标状态, &安全根需求.根需求,
            &服务根需求.特征定义, &服务根需求.实例槽位,
            &服务根需求.当前特征值, &服务根需求.目标状态, &服务根需求.根需求,
            &概念存在根, &概念动态根, &概念关系根, &概念因果根,
            &方法登记根, &方法登记角色状态, &方法活跃状态, &方法失效状态};
    }

    bool 完整() const noexcept {
        if (!世界根.完整() || !自我场景.完整() || !自我存在.完整()
            || !场景接纳自我关系.完整() || !安全根需求.完整()
            || !服务根需求.完整() || !概念存在根.完整()
            || !概念动态根.完整() || !概念关系根.完整() || !概念因果根.完整()
            || !方法登记根.完整() || !方法登记角色状态.完整()
            || !方法活跃状态.完整() || !方法失效状态.完整()) return false;

        const auto 身份组 = 读取身份组();
        const auto 仓库编号 = 身份组.front()->节点.仓库编号;
        for (std::size_t 左 = 0; 左 < 身份组.size(); ++左) {
            if (!身份组[左]->完整() || 身份组[左]->节点.仓库编号 != 仓库编号) return false;
            for (std::size_t 右 = 左 + 1; 右 < 身份组.size(); ++右) {
                if (身份组[左]->稳定主键 == 身份组[右]->稳定主键
                    || 身份组[左]->节点 == 身份组[右]->节点
                    || 身份组[左]->主信息 == 身份组[右]->主信息) return false;
            }
        }
        return 场景接纳自我关系.类型 == 关系类型::归属
            && 场景接纳自我关系.源节点 == 自我场景.节点
            && 场景接纳自我关系.目标节点 == 自我存在.节点
            && 场景接纳自我关系.顺序号 == 0;
    }

    bool operator==(const 系统角色主体材料&) const = default;
};

struct 系统角色清单 {
    std::uint32_t 版本 = 0;
    系统角色主体材料 主体;
    系统角色关系材料 世界根到场景关系;
    系统角色关系材料 场景到自我关系;

    bool 完整() const noexcept {
        return 版本 == 系统角色清单当前版本 && 主体.完整()
            && 世界根到场景关系.完整() && 场景到自我关系.完整()
            && 世界根到场景关系.类型 == 关系类型::普通父子
            && 世界根到场景关系.源节点 == 主体.世界根.节点
            && 世界根到场景关系.目标节点 == 主体.自我场景.节点
            && 世界根到场景关系.顺序号 == 0
            && 场景到自我关系.类型 == 关系类型::普通父子
            && 场景到自我关系.源节点 == 主体.自我场景.节点
            && 场景到自我关系.目标节点 == 主体.自我存在.节点
            && 场景到自我关系.顺序号 == 0;
    }

    bool 匹配参数(const 系统角色初始化参数& 参数) const noexcept {
        return 完整() && 参数.有效() && 参数.清单版本 == 版本
            && 参数.世界根主键 == 主体.世界根.稳定主键
            && 参数.自我场景主键 == 主体.自我场景.稳定主键
            && 参数.自我存在主键 == 主体.自我存在.稳定主键
            && 参数.安全特征定义主键 == 主体.安全根需求.特征定义.稳定主键
            && 参数.安全实例槽位主键 == 主体.安全根需求.实例槽位.稳定主键
            && 参数.安全当前特征值主键 == 主体.安全根需求.当前特征值.稳定主键
            && 参数.安全目标状态主键 == 主体.安全根需求.目标状态.稳定主键
            && 参数.安全根需求主键 == 主体.安全根需求.根需求.稳定主键
            && 参数.服务特征定义主键 == 主体.服务根需求.特征定义.稳定主键
            && 参数.服务实例槽位主键 == 主体.服务根需求.实例槽位.稳定主键
            && 参数.服务当前特征值主键 == 主体.服务根需求.当前特征值.稳定主键
            && 参数.服务目标状态主键 == 主体.服务根需求.目标状态.稳定主键
            && 参数.服务根需求主键 == 主体.服务根需求.根需求.稳定主键
            && 参数.概念存在根主键 == 主体.概念存在根.稳定主键
            && 参数.概念动态根主键 == 主体.概念动态根.稳定主键
            && 参数.概念关系根主键 == 主体.概念关系根.稳定主键
            && 参数.概念因果根主键 == 主体.概念因果根.稳定主键
            && 参数.方法登记根主键 == 主体.方法登记根.稳定主键
            && 参数.方法登记角色状态主键 == 主体.方法登记角色状态.稳定主键
            && 参数.方法活跃状态主键 == 主体.方法活跃状态.稳定主键
            && 参数.方法失效状态主键 == 主体.方法失效状态.稳定主键
            && 参数.安全当前值 == 主体.安全根需求.当前值
            && 参数.安全目标值 == 主体.安全根需求.目标值
            && 参数.服务当前值 == 主体.服务根需求.当前值
            && 参数.服务目标值 == 主体.服务根需求.目标值
            && 参数.动态根材料 == 主体.动态根材料;
    }

    bool operator==(const 系统角色清单&) const = default;
};

struct 系统角色键占用材料 {
    系统角色稳定键用途 用途 = 系统角色稳定键用途::未定义;
    std::uint64_t 稳定主键 = 0;
    节点类型 预期类型 = 节点类型::未分类;
    节点句柄 节点;
    主信息句柄 主信息;

    bool 已占用() const noexcept { return 句柄有效(节点); }
};

struct 系统角色预检结果 {
    系统角色业务状态 状态 = 系统角色业务状态::入口拒绝;
    std::array<系统角色键占用材料, 系统角色稳定键数量> 键组{};
    std::optional<关系句柄> 场景接纳自我关系;

    bool 成功() const noexcept { return 状态 == 系统角色业务状态::幂等读回; }

    const 系统角色键占用材料* 查找(系统角色稳定键用途 用途) const noexcept {
        for (const auto& 键 : 键组) if (键.用途 == 用途) return &键;
        return nullptr;
    }
};

struct 系统角色初始化结果 {
    系统角色业务状态 状态 = 系统角色业务状态::入口拒绝;
    系统角色清单 清单;

    bool 成功() const noexcept {
        return (状态 == 系统角色业务状态::已提交
                || 状态 == 系统角色业务状态::幂等读回)
            && 清单.完整();
    }

    bool 改变了结构() const noexcept { return 状态 == 系统角色业务状态::已提交; }
};

