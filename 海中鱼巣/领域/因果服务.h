// 文件规则：因果服务记录动态和因果引用入口；动作来源不得由线程或外部输入直接裁决。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/容错检查.h"
#include "动态服务.h"

#include <cstdint>
#include <optional>

namespace 海中鱼巣 {

enum class 因果候选状态 : std::uint32_t {
    可作为候选 = 1,
    缺少动态证据 = 2,
    动态材料不完整 = 3
};

struct 因果查询候选材料 {
    节点句柄 来源动态;
    因果候选状态 状态 = 因果候选状态::缺少动态证据;
};

struct 因果实例完成材料 {
    节点句柄 因果;
    节点句柄 来源动态;
    动态实例完成材料 来源动态材料;

    bool 完整() const {
        return 句柄有效(因果)
            && 句柄有效(来源动态)
            && 来源动态材料.完整()
            && 来源动态材料.动态 == 来源动态;
    }
};

class 因果服务 {
public:
    因果服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 记录因果引用() {
        const auto 主信息句柄 = 主信息_.创建主信息();
        const auto 因果引用 = 节点_.创建节点(节点类型::因果引用, 主信息句柄);
        const auto 记录 = 节点_.读取节点(因果引用);
        if (!追根因检查(记录.has_value() && 记录->类型 == 节点类型::因果引用
            && 主信息_.主信息是否有效(记录->主信息),
            L"记录因果引用后读回身份不符合内部预期。")) {
            return {};
        }
        return 因果引用;
    }

    节点句柄 创建因果概念根() {
        const auto 根节点 = 记录因果引用();
        const auto 记录 = 节点_.读取节点(根节点);
        if (!追根因检查(记录.has_value()
            && 记录->类型 == 节点类型::因果引用
            && 主信息_.主信息是否有效(记录->主信息),
            L"创建因果概念根后读回身份不符合内部预期。")) {
            return {};
        }
        return 根节点;
    }

    节点句柄 记录因果引用(节点句柄 来源动态) {
        const auto 动态记录 = 节点_.读取节点(来源动态);
        if (!动态记录.has_value() || 动态记录->类型 != 节点类型::动态) {
            return {};
        }
        return 记录因果引用();
    }

    std::optional<动态材料> 读取因果准入动态材料(节点句柄 来源动态, const 动态服务& 动态) const {
        return 动态.读取动态材料(来源动态);
    }

    std::optional<因果实例完成材料> 读取因果实例完成材料(
        节点句柄 因果节点,
        节点句柄 来源动态,
        const 动态服务& 动态) const {
        const auto 因果记录 = 节点_.读取节点(因果节点);
        const auto 动态完成材料 = 动态.读取动态实例完成材料(来源动态);
        if (!因果记录.has_value()
            || 因果记录->类型 != 节点类型::因果引用
            || !动态完成材料.has_value()) {
            return std::nullopt;
        }
        因果实例完成材料 材料{因果节点, 来源动态, 动态完成材料.value()};
        return 材料.完整() ? std::optional<因果实例完成材料>{材料} : std::nullopt;
    }

    bool 因果轻量准入是否有效(节点句柄 来源动态, const 动态服务& 动态) const {
        return 读取因果准入动态材料(来源动态, 动态).has_value();
    }

    节点句柄 记录因果引用(节点句柄 来源动态, const 动态服务& 动态) {
        const auto 动态材料值 = 读取因果准入动态材料(来源动态, 动态);
        if (!动态材料值.has_value()) {
            return {};
        }
        return 记录因果引用();
    }

    std::optional<概念结构所有者清理授权> 形成概念节点清理授权(
        const 概念结构所有者清理请求& 请求,
        const 结构事务令牌& 令牌) const {
        if (!请求.完整() || 请求.所有者 != 概念结构所有者类型::因果服务) return std::nullopt;
        const auto 记录 = 节点_.读取节点(请求.目标, 令牌);
        if (!记录.has_value() || 记录->类型 != 节点类型::因果引用 || 记录->主信息 != 请求.主信息) return std::nullopt;
        概念结构所有者清理授权 授权{请求.目标, 请求.主信息, 请求.所有者, 令牌.许可序号};
        return 授权.完整() ? std::optional<概念结构所有者清理授权>{授权} : std::nullopt;
    }

    因果查询候选材料 读取因果模板候选(节点句柄 来源动态, const 动态服务& 动态) const {
        return 读取因果查询候选(来源动态, 动态);
    }

    因果查询候选材料 读取因果查询候选(节点句柄 来源动态, const 动态服务& 动态) const {
        const auto 动态记录 = 节点_.读取节点(来源动态);
        if (!动态记录.has_value() || 动态记录->类型 != 节点类型::动态) {
            return {来源动态, 因果候选状态::缺少动态证据};
        }
        return 动态.动态证据材料是否完整(来源动态)
            ? 因果查询候选材料{来源动态, 因果候选状态::可作为候选}
            : 因果查询候选材料{来源动态, 因果候选状态::动态材料不完整};
    }

    因果查询候选材料 读取缺失证据候选(节点句柄 来源动态, const 动态服务& 动态) const {
        const auto 候选 = 读取因果查询候选(来源动态, 动态);
        if (候选.状态 == 因果候选状态::可作为候选) {
            return {来源动态, 因果候选状态::动态材料不完整};
        }
        return 候选;
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
