// 文件规则：因果服务记录动态和因果引用入口；动作来源不得由线程或外部输入直接裁决。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"

namespace 海中鱼巣 {

class 因果服务 {
public:
    因果服务(主信息仓库& 主信息, 节点仓库& 节点)
        : 主信息_(主信息), 节点_(节点) {
    }

    节点句柄 记录动态() {
        return 节点_.创建节点(节点类型::动态, 主信息_.创建主信息());
    }

    节点句柄 记录动态(节点句柄 来源动作, 节点句柄 被改变目标) {
        if (!节点_.节点是否有效(来源动作) || !节点_.节点是否有效(被改变目标)) {
            return {};
        }
        return 记录动态();
    }

    节点句柄 记录因果引用() {
        return 节点_.创建节点(节点类型::因果引用, 主信息_.创建主信息());
    }

    节点句柄 记录因果引用(节点句柄 动态, 节点句柄 被改变目标) {
        const auto 动态记录 = 节点_.读取节点(动态);
        if (!动态记录.has_value() || 动态记录->类型 != 节点类型::动态 || !节点_.节点是否有效(被改变目标)) {
            return {};
        }
        return 记录因果引用();
    }

private:
    主信息仓库& 主信息_;
    节点仓库& 节点_;
};

}
