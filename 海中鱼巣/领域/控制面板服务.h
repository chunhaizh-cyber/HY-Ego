// 文件规则：控制面板服务只组织人读只读材料和操作请求壳；不得写业务事实或接入外部通道。
#pragma once

#include "显示服务.h"

#include <array>
#include <cstdint>

namespace 海中鱼巣 {

enum class 控制面板请求状态 : std::uint32_t {
    可展示 = 0,
    拒绝越权读取 = 1,
    拒绝写业务事实 = 2,
    拒绝文本裁决 = 3,
    后续专项门禁 = 4,
    拒绝树分类 = 5
};

enum class 控制面板树分类 : std::uint32_t {
    世界树 = 0,
    概念树 = 1,
    语素树 = 2,
    需求树 = 3,
    任务树 = 4,
    方法树 = 5,
    状态详情 = 6,
    动态详情 = 7
};

struct 控制面板请求 {
    bool 来自服务只读入口 = false;
    bool 试图绕过服务读取 = false;
    bool 试图写业务事实 = false;
    bool 试图用文本裁决事实 = false;
    bool 请求数据库事实裁决 = false;
    bool 请求外部通道 = false;
};

struct 控制面板材料 {
    控制面板请求状态 状态 = 控制面板请求状态::拒绝越权读取;
    bool 人读材料 = true;
    bool 来源显示服务 = false;
    bool 允许写业务事实 = false;
};

struct 控制面板树视图材料 {
    控制面板请求状态 状态 = 控制面板请求状态::拒绝树分类;
    控制面板树分类 分类 = 控制面板树分类::世界树;
    bool 可作为树视图分类 = false;
    bool 只作为详情材料 = false;
};

struct 控制面板操作请求材料 {
    控制面板请求状态 状态 = 控制面板请求状态::拒绝越权读取;
    bool 已生成请求壳 = false;
    bool 已执行动作 = false;
};

struct 控制面板门禁材料 {
    控制面板请求状态 状态 = 控制面板请求状态::后续专项门禁;
    bool 后续专项 = true;
    bool 允许裁决运行期事实 = false;
};

class 控制面板服务 {
public:
    explicit 控制面板服务(const 显示服务& 显示) : 显示(显示) {}

    控制面板材料 生成只读总览材料(const 控制面板请求& 请求) const {
        const auto 状态 = 判定请求状态(请求);
        if (状态 != 控制面板请求状态::可展示) {
            return {状态, true, false, false};
        }

        const auto 展示 = 显示.生成只读展示材料({true, false, false, false});
        if (展示.状态 != 显示请求状态::可展示 || 展示.允许写业务事实) {
            return {控制面板请求状态::拒绝越权读取, true, false, false};
        }

        return {控制面板请求状态::可展示, 展示.展示材料人读, true, false};
    }

    控制面板树视图材料 生成树视图候选材料(控制面板树分类 分类) const {
        if (分类 == 控制面板树分类::状态详情 || 分类 == 控制面板树分类::动态详情) {
            return {控制面板请求状态::拒绝树分类, 分类, false, true};
        }
        return {控制面板请求状态::可展示, 分类, true, false};
    }

    std::array<控制面板树视图材料, 6> 生成六类树视图候选材料() const {
        return {
            生成树视图候选材料(控制面板树分类::世界树),
            生成树视图候选材料(控制面板树分类::概念树),
            生成树视图候选材料(控制面板树分类::语素树),
            生成树视图候选材料(控制面板树分类::需求树),
            生成树视图候选材料(控制面板树分类::任务树),
            生成树视图候选材料(控制面板树分类::方法树)
        };
    }

    控制面板操作请求材料 生成操作请求壳(const 控制面板请求& 请求) const {
        const auto 状态 = 判定请求状态(请求);
        if (状态 == 控制面板请求状态::拒绝写业务事实) {
            return {状态, false, false};
        }
        if (状态 != 控制面板请求状态::可展示) {
            return {状态, false, false};
        }
        return {控制面板请求状态::可展示, true, false};
    }

    控制面板门禁材料 生成数据库审计门禁() const {
        return {};
    }

    控制面板门禁材料 生成渲染通道门禁() const {
        return {};
    }

    控制面板门禁材料 生成外部感知门禁() const {
        return {};
    }

private:
    控制面板请求状态 判定请求状态(const 控制面板请求& 请求) const {
        if (!请求.来自服务只读入口 || 请求.试图绕过服务读取) {
            return 控制面板请求状态::拒绝越权读取;
        }
        if (请求.试图写业务事实) {
            return 控制面板请求状态::拒绝写业务事实;
        }
        if (请求.试图用文本裁决事实) {
            return 控制面板请求状态::拒绝文本裁决;
        }
        if (请求.请求数据库事实裁决 || 请求.请求外部通道) {
            return 控制面板请求状态::后续专项门禁;
        }
        return 控制面板请求状态::可展示;
    }

    const 显示服务& 显示;
};

}
