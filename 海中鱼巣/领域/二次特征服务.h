// 文件规则：二次特征服务只维护复合或关系特征结构；判断结果必须落到状态、动态或因果结构。
#pragma once

#include "../核心/节点仓库.h"
#include "../核心/主信息仓库.h"
#include "../核心/关系仓库.h"

#include <vector>

namespace 海中鱼巣 {

class 二次特征服务 {
public:
    二次特征服务(主信息仓库& 主信息, 节点仓库& 节点, 关系仓库& 关系)
        : 主信息_(主信息), 节点_(节点), 关系_(关系) {
    }

    节点句柄 创建二次特征() {
        return 节点_.创建节点(节点类型::二次特征, 主信息_.创建主信息());
    }

    节点句柄 创建组合二次特征(const std::vector<节点句柄>& 组成项组) {
        if (组成项组.empty()) {
            return {};
        }
        for (const auto& 组成项 : 组成项组) {
            if (!组成项可用(组成项)) {
                return {};
            }
        }
        const auto 二次特征 = 创建二次特征();
        if (!句柄有效(二次特征)) {
            return {};
        }
        for (const auto& 组成项 : 组成项组) {
            if (!句柄有效(追加组成项(二次特征, 组成项))) {
                return {};
            }
        }
        return 二次特征;
    }

    关系句柄 追加组成项(节点句柄 二次特征, 节点句柄 组成项) {
        if (!节点类型匹配(二次特征, 节点类型::二次特征) || !组成项可用(组成项)) {
            return {};
        }
        return 关系_.创建关系(关系类型::引用, 二次特征, 组成项);
    }

private:
    bool 节点类型匹配(节点句柄 节点句柄值, 节点类型 类型) const {
        const auto 记录 = 节点_.读取节点(节点句柄值);
        return 记录.has_value() && 记录->类型 == 类型;
    }

    bool 组成项可用(节点句柄 组成项) const {
        const auto 记录 = 节点_.读取节点(组成项);
        if (!记录.has_value()) {
            return false;
        }
        return 记录->类型 == 节点类型::基础信息
            || 记录->类型 == 节点类型::存在
            || 记录->类型 == 节点类型::场景
            || 记录->类型 == 节点类型::特征
            || 记录->类型 == 节点类型::状态
            || 记录->类型 == 节点类型::动态
            || 记录->类型 == 节点类型::二次特征
            || 记录->类型 == 节点类型::因果引用;
    }

    主信息仓库& 主信息_;
    节点仓库& 节点_;
    关系仓库& 关系_;
};

}
