inline bool 生产运行期配置::operator==(const 生产运行期配置& 右) const noexcept {
    return 配置版本 == 右.配置版本 && 域编号 == 右.域编号
        && 仓库编号 == 右.仓库编号 && 动作键 == 右.动作键
        && 系统角色.清单版本 == 右.系统角色.清单版本
        && 系统角色.读取稳定键组() == 右.系统角色.读取稳定键组()
        && 系统角色.安全当前值 == 右.系统角色.安全当前值
        && 系统角色.安全目标值 == 右.系统角色.安全目标值
        && 系统角色.服务当前值 == 右.系统角色.服务当前值
        && 系统角色.服务目标值 == 右.系统角色.服务目标值
        && 系统角色.动态根材料 == 右.系统角色.动态根材料
        && 概念活动 == 右.概念活动;
}

inline bool 生产运行期配置::有效() const noexcept {
    if (!(*this == 读取生产运行期固定配置())
        || !系统角色.有效() || !概念活动.有效()) {
        return false;
    }
    const auto 系统键组 = 系统角色.读取稳定键组();
    const auto 活动键组 = 概念活动.读取稳定键组();
    for (const auto 系统键 : 系统键组) {
        for (const auto 活动键 : 活动键组) {
            if (系统键 == 活动键) return false;
        }
    }
    return true;
}

struct 生产运行期启动请求 {
    std::uint32_t 请求版本 = 生产运行期启动请求当前版本;

    bool 有效() const noexcept {
        return 请求版本 == 生产运行期启动请求当前版本;
    }
};

