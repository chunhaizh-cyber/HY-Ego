// 文件规则：入口只做最小结构自检和人读输出；控制台输出不得承载机器逻辑。
#include "核心/主信息仓库.h"
#include "核心/节点仓库.h"
#include "核心/关系仓库.h"
#include "核心/索引仓库.h"
#include "核心/写入事务.h"
#include "领域/世界服务.h"
#include "领域/任务服务.h"
#include "领域/方法服务.h"
#include "领域/特征值服务.h"
#include "领域/特征服务.h"
#include "领域/因果服务.h"
#include "领域/语素服务.h"
#include "领域/需求服务.h"

#include <iostream>

int main() {
    海中鱼巣::主信息仓库 主信息;
    海中鱼巣::节点仓库 节点(主信息);
    海中鱼巣::关系仓库 关系(节点);
    海中鱼巣::索引仓库 索引(节点);
    海中鱼巣::写入事务 事务;
    海中鱼巣::语素服务 语素(主信息, 节点, 关系, 索引);
    海中鱼巣::世界服务 世界(主信息, 节点);
    海中鱼巣::特征服务 特征(主信息, 节点);
    海中鱼巣::特征值服务 特征值(主信息, 节点);
    海中鱼巣::需求服务 需求(主信息, 节点);
    海中鱼巣::任务服务 任务(主信息, 节点);
    海中鱼巣::方法服务 方法(主信息, 节点);
    海中鱼巣::因果服务 因果(主信息, 节点);

    事务.开始();
    const auto 根主信息 = 主信息.创建主信息();
    const auto 子主信息 = 主信息.创建主信息();
    const bool 无效主信息节点已拒绝 = !海中鱼巣::句柄有效(节点.创建节点(海中鱼巣::节点类型::基础信息, {}));
    const bool 未定义节点类型已拒绝 = !海中鱼巣::句柄有效(节点.创建节点(static_cast<海中鱼巣::节点类型>(999), 根主信息));
    const auto 根节点 = 节点.创建节点(海中鱼巣::节点类型::基础信息, 根主信息);
    const auto 子节点 = 节点.创建节点(海中鱼巣::节点类型::存在, 子主信息);
    const auto 新父节点 = 节点.创建节点(海中鱼巣::节点类型::场景, 主信息.创建主信息());
    const auto 待删除节点 = 节点.创建节点(海中鱼巣::节点类型::方法, 主信息.创建主信息());
    const auto 初始父关系 = 关系.创建关系(海中鱼巣::关系类型::普通父子, 根节点, 子节点);
    const bool 初始父关系有效 = 海中鱼巣::句柄有效(初始父关系);
    const auto 重复父关系 = 关系.创建关系(海中鱼巣::关系类型::普通父子, 新父节点, 子节点);
    const bool 重复父关系已拒绝 = !海中鱼巣::句柄有效(重复父关系);
    const bool 索引已绑定 = 索引.绑定主键(1, 根节点);
    const bool 主键冲突已拒绝 = !索引.绑定主键(1, 子节点);
    索引.绑定主键(2, 待删除节点);
    const bool 节点已重挂 = 关系.重挂节点(子节点, 新父节点);
    const bool 删除已执行 = 节点.删除节点(待删除节点);
    const bool 旧句柄已失效 = !节点.节点是否有效(待删除节点);
    const bool 删除节点索引不命中 = !索引.按主键查节点(2).has_value();
    const bool 已提交 = 事务.提交();

    const auto 子节点组 = 关系.获取子节点(新父节点);
    const auto 当前父节点 = 关系.获取父节点(子节点);
    const bool 结构可读 = 当前父节点.has_value() && 当前父节点.value() == 新父节点 && 索引.按主键查节点(1).has_value();

    const auto 无效关系 = 关系.创建关系(海中鱼巣::关系类型::引用, {}, 子节点);
    const auto 已删除节点关系 = 关系.创建关系(海中鱼巣::关系类型::引用, 根节点, 待删除节点);
    const auto 未定义关系 = 关系.创建关系(static_cast<海中鱼巣::关系类型>(999), 根节点, 子节点);
    const bool 无效关系已拒绝 = !海中鱼巣::句柄有效(无效关系);
    const bool 已删除节点关系已拒绝 = !海中鱼巣::句柄有效(已删除节点关系);
    const bool 未定义关系已拒绝 = !海中鱼巣::句柄有效(未定义关系);

    const auto 主信息待删除 = 主信息.创建主信息();
    const bool 主信息已删除 = 主信息.删除主信息(主信息待删除);
    const bool 已删除主信息写值已拒绝 = !主信息.写入I64值(主信息待删除, 1);
    const bool 无效主信息写值已拒绝 = !主信息.写入I64值({}, 1);

    海中鱼巣::写入事务 失败事务;
    const bool 未开始提交已拒绝 = !失败事务.提交();
    const bool 失败事务已开始 = 失败事务.开始();
    const bool 失败边界已记录 = 失败事务.标记失败(L"入口拒绝复验", L"模拟失败边界");
    const bool 失败边界可读 = 失败事务.读取失败阶段().has_value() && 失败事务.读取失败原因().has_value();
    const bool 失败后提交已拒绝 = !失败事务.提交();

    const auto 自我存在 = 世界.创建存在();
    const auto 现实场景 = 世界.创建场景();
    const auto 特征类型 = 特征.创建特征类型();
    const auto 目标特征值 = 特征值.创建特征值();
    const bool 初始特征值已写入 = 特征值.写入I64值(目标特征值, 1);
    const auto 缺失当前值特征值 = 特征值.创建特征值();
    const auto 缺失当前值需求 = 需求.创建I64目标需求(自我存在, 特征类型, 特征值, 缺失当前值特征值, 10);
    const bool 缺失当前值需求已拒绝 = !海中鱼巣::句柄有效(缺失当前值需求);
    const auto 无效目标需求 = 需求.创建I64目标需求({}, 特征类型, 特征值, 目标特征值, 10);
    const bool 无效目标需求已拒绝 = !海中鱼巣::句柄有效(无效目标需求);
    const auto 需求节点 = 需求.创建I64目标需求(自我存在, 特征类型, 特征值, 目标特征值, 10);
    const auto 无效需求任务 = 任务.按需求创建任务({});
    const bool 无效需求任务已拒绝 = !海中鱼巣::句柄有效(无效需求任务);
    const auto 任务节点 = 任务.按需求创建任务(需求节点);
    const auto 任务待执行状态 = 任务.记录任务状态(任务节点, 1);
    const auto 方法节点 = 方法.登记方法();
    const auto 改变前值 = 特征值.读取I64值(目标特征值);
    const bool 方法已执行 = 方法.执行I64写入方法(特征值, 目标特征值, 10);
    const auto 改变后值 = 特征值.读取I64值(目标特征值);
    const auto 缺来源动态 = 因果.记录动态({}, 目标特征值);
    const bool 缺来源动态已拒绝 = !海中鱼巣::句柄有效(缺来源动态);
    const auto 动态节点 = 因果.记录动态(方法节点, 目标特征值);
    const auto 缺动态因果引用 = 因果.记录因果引用({}, 目标特征值);
    const bool 缺动态因果引用已拒绝 = !海中鱼巣::句柄有效(缺动态因果引用);
    const auto 因果引用节点 = 因果.记录因果引用(动态节点, 目标特征值);
    关系.创建关系(海中鱼巣::关系类型::归属, 现实场景, 自我存在);
    关系.创建关系(海中鱼巣::关系类型::归属, 自我存在, 特征类型);
    关系.创建关系(海中鱼巣::关系类型::归属, 特征类型, 目标特征值);
    关系.创建关系(海中鱼巣::关系类型::引用, 需求节点, 目标特征值);
    关系.创建关系(海中鱼巣::关系类型::归属, 需求节点, 任务节点);
    关系.创建关系(海中鱼巣::关系类型::引用, 任务节点, 方法节点);
    关系.创建关系(海中鱼巣::关系类型::因果来源, 动态节点, 方法节点);
    关系.创建关系(海中鱼巣::关系类型::因果来源, 因果引用节点, 动态节点);
    关系.创建关系(海中鱼巣::关系类型::因果来源, 因果引用节点, 目标特征值);
    const auto 需求结算 = 需求.结算I64目标(需求节点, 特征值, 目标特征值);
    const auto 任务已完成状态 = 任务.记录任务状态(任务节点, 2);
    const auto 需求结算状态 = 需求.记录结算状态(需求节点, 需求结算);
    关系.创建关系(海中鱼巣::关系类型::引用, 任务节点, 任务待执行状态);
    关系.创建关系(海中鱼巣::关系类型::引用, 任务节点, 任务已完成状态);
    关系.创建关系(海中鱼巣::关系类型::引用, 需求节点, 需求结算状态);
    const bool 任务状态变化可读 =
        海中鱼巣::句柄有效(任务待执行状态)
        && 海中鱼巣::句柄有效(任务已完成状态)
        && 任务待执行状态 != 任务已完成状态;
    const bool 需求结算结构可读 = 海中鱼巣::句柄有效(需求结算状态);
    const bool 最小闭环通过 =
        初始特征值已写入
        && 方法已执行
        && 改变前值.has_value()
        && 改变前值.value() == 1
        && 改变后值.has_value()
        && 改变后值.value() == 10
        && 需求结算 == 海中鱼巣::需求结算结果::已满足
        && 海中鱼巣::句柄有效(自我存在)
        && 海中鱼巣::句柄有效(现实场景)
        && 海中鱼巣::句柄有效(特征类型)
        && 海中鱼巣::句柄有效(目标特征值)
        && 海中鱼巣::句柄有效(需求节点)
        && 海中鱼巣::句柄有效(任务节点)
        && 海中鱼巣::句柄有效(方法节点)
        && 海中鱼巣::句柄有效(动态节点)
        && 海中鱼巣::句柄有效(因果引用节点)
        && 任务状态变化可读
        && 需求结算结构可读
        && 缺失当前值需求已拒绝
        && 无效目标需求已拒绝
        && 无效需求任务已拒绝
        && 缺来源动态已拒绝
        && 缺动态因果引用已拒绝;

    const auto 语素对应基础信息 = 世界.创建基础信息();
    const auto 语素入口节点 = 语素.创建语素入口(语素对应基础信息, 2);
    const auto 语素需求入口节点 = 语素.创建语素入口(需求节点, 4);
    const auto 语素错误入口 = 语素.创建语素入口(自我存在, 3);
    const auto 语素概念节点 = 世界.创建基础信息();
    const auto 语素概念入口 = 语素.创建概念入口(L"杯子", 语素对应基础信息, 语素概念节点, 5);
    const auto 语素短语入口 = 语素.创建概念入口(L"红色杯子", 语素对应基础信息, 语素概念节点, 6);
    const auto 语素绑定目标 = 语素.读取入口绑定目标(语素概念入口);
    const auto 语素概念追溯 = 语素.读取入口概念追溯(语素概念入口);
    const bool 语素第一轮通过 =
        语素.判断文本是否可作为最小词单元(L"杯子") == 海中鱼巣::语素准入状态::可作为最小词单元
        && 语素.判断文本是否可作为最小词单元(L"红色杯子") == 海中鱼巣::语素准入状态::超出第一轮长度
        && 海中鱼巣::句柄有效(语素概念入口)
        && !海中鱼巣::句柄有效(语素短语入口)
        && 语素绑定目标.has_value()
        && 语素绑定目标.value() == 语素对应基础信息
        && 语素概念追溯.has_value()
        && 语素概念追溯.value() == 语素概念节点
        && 语素.语素入口同一(语素概念入口, 语素概念入口)
        && !语素.语素入口同一(语素概念入口, 语素入口节点);
    const bool 语素入口已封口 =
        海中鱼巣::句柄有效(语素入口节点)
        && 海中鱼巣::句柄有效(语素需求入口节点)
        && !海中鱼巣::句柄有效(语素错误入口);
    const bool 领域服务可写 =
        语素入口已封口
        && 海中鱼巣::句柄有效(世界.创建基础信息())
        && 海中鱼巣::句柄有效(世界.创建存在())
        && 海中鱼巣::句柄有效(世界.创建场景())
        && 海中鱼巣::句柄有效(特征.创建特征类型())
        && 海中鱼巣::句柄有效(特征值.创建特征值())
        && 海中鱼巣::句柄有效(需求.创建需求())
        && 海中鱼巣::句柄有效(任务.创建任务())
        && 海中鱼巣::句柄有效(方法.登记方法())
        && 海中鱼巣::句柄有效(因果.记录动态())
        && 海中鱼巣::句柄有效(因果.记录因果引用())
        && 语素第一轮通过;
    const bool 入口拒绝通过 =
        无效主信息节点已拒绝
        && 未定义节点类型已拒绝
        && 主信息已删除
        && 无效主信息写值已拒绝
        && 已删除主信息写值已拒绝
        && 无效关系已拒绝
        && 已删除节点关系已拒绝
        && 未定义关系已拒绝
        && 初始父关系有效
        && 重复父关系已拒绝
        && 主键冲突已拒绝
        && 删除节点索引不命中
        && 未开始提交已拒绝
        && 失败事务已开始
        && 失败边界已记录
        && 失败边界可读
        && 失败后提交已拒绝;

    std::cout << "海中鱼巣核心内核最小自检" << '\n';
    std::cout << "事务提交: " << (已提交 ? "是" : "否") << '\n';
    std::cout << "索引绑定: " << (索引已绑定 ? "是" : "否") << '\n';
    std::cout << "节点重挂: " << (节点已重挂 ? "是" : "否") << '\n';
    std::cout << "旧句柄失效: " << (删除已执行 && 旧句柄已失效 ? "是" : "否") << '\n';
    std::cout << "子节点数: " << 子节点组.size() << '\n';
    std::cout << "结构可读: " << (结构可读 ? "是" : "否") << '\n';
    std::cout << "无效关系拒绝: " << (无效关系已拒绝 ? "是" : "否") << '\n';
    std::cout << "入口拒绝: " << (入口拒绝通过 ? "是" : "否") << '\n';
    std::cout << "语素入口封口: " << (语素入口已封口 ? "是" : "否") << '\n';
    std::cout << "语素第一轮: " << (语素第一轮通过 ? "是" : "否") << '\n';
    std::cout << "领域服务可写: " << (领域服务可写 ? "是" : "否") << '\n';
    std::cout << "最小闭环通过: " << (最小闭环通过 ? "是" : "否") << '\n';
    return 结构可读 && 已提交 && 索引已绑定 && 节点已重挂 && 删除已执行 && 旧句柄已失效
        && 子节点组.size() == 1 && 入口拒绝通过 && 领域服务可写 && 最小闭环通过 ? 0 : 1;
}
