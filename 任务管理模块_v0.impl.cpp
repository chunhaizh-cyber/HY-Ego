结构_任务管理结果 任务管理器_v0::写入反馈(
    任务节点类* 任务头结点,
    const 任务类::结构_创建结果参数& 参数,
    const std::string& 调用点)
{
    结构_任务管理结果 结果{};
    auto* 头信息 = 取头信息_(任务头结点);
    if (!头信息) {
        结果.错误摘要 = "任务头结点无效";
        return 结果;
    }

    auto* 步骤节点 = 取当前或首个步骤节点_(任务头结点);
    auto* 步骤信息 = 取步骤信息_(步骤节点);
    if (!步骤信息) {
        结果.错误摘要 = "没有可写反馈的步骤";
        return 结果;
    }

    auto 结果参数 = 参数;
    if (!结果参数.所属任务头结点) {
        结果参数.所属任务头结点 = 任务头结点;
    }
    if (结果参数.结果角色 == 枚举_任务结果角色::未定义) {
        结果参数.结果角色 = 枚举_任务结果角色::实际结果;
    }
    if (结果参数.初始状态 == 枚举_任务状态::未定义) {
        结果参数.初始状态 = 枚举_任务状态::完成;
    }
    if (!结果参数.来源方法首节点) {
        结果参数.来源方法首节点 = 步骤信息->当前选中方法首节点;
    }

    auto* 结果节点 = 任务类::创建结果节点(步骤节点, 结果参数, 调用点 + "/创建结果");
    if (!结果节点) {
        结果.错误摘要 = "创建反馈结果节点失败";
        return 结果;
    }

    结果.成功 = true;
    结果.需求 = 头信息->需求;
    结果.任务头结点 = 任务头结点;
    结果.步骤节点 = 步骤节点;
    结果.结果节点 = 结果节点;
    结果.方法首节点 = 步骤信息->当前选中方法首节点;
    return 结果;
}

结构_学习任务进化结果_v0 任务管理器_v0::推进学习任务进化(
    任务节点类* 学习任务头结点,
    const 结构_学习任务进化输入_v0& 输入,
    const std::string& 调用点)
{
    结构_学习任务进化结果_v0 结果{};
    auto* 头信息 = 取头信息_(学习任务头结点);
    if (!头信息 || !学习任务头结点->主信息) {
        结果.错误摘要 = "学习任务头结点无效";
        return 结果;
    }
    结构_任务动作事件守卫 动作守卫(
        学习任务头结点->主信息,
        动作_推进学习任务进化_(),
        头信息->场景,
        头信息->场景,
        结构体_时间戳::当前_微秒(),
        调用点 + "/动作动态");

    结果.可用方法增量 = 输入.当前可用方法数 - 输入.基线可用方法数;
    结果.稳定方法增量 = 输入.当前稳定方法数 - 输入.基线稳定方法数;
    结果.稳定度总分增量 = 输入.当前稳定度总分 - 输入.基线稳定度总分;
    结果.可用方法已增加 = 结果.可用方法增量 > 0;
    结果.方法稳定性已提升 =
        结果.稳定度总分增量 > 0
        || 结果.稳定方法增量 > 0;
    结果.应继续进化 = 输入.有待处理请求 || 输入.待进化方法数 > 0;

    const 时间戳 now = 结构体_时间戳::当前_微秒();
    auto 写I64 = [&](const char* 键, std::int64_t 值) {
        (void)任务类::写任务虚拟存在_I64特征(
            学习任务头结点->主信息,
            任务类::取任务事实特征(键),
            static_cast<I64>(值),
            now,
            调用点 + "/写任务事实/" + 键);
    };

    写I64("学习目标_基线可用方法数", 输入.基线可用方法数);
    写I64("学习目标_当前可用方法数", 输入.当前可用方法数);
    写I64("学习目标_可用方法增量", 结果.可用方法增量);
    写I64("学习目标_基线稳定方法数", 输入.基线稳定方法数);
    写I64("学习目标_当前稳定方法数", 输入.当前稳定方法数);
    写I64("学习目标_稳定方法增量", 结果.稳定方法增量);
    写I64("学习目标_基线稳定度总分", 输入.基线稳定度总分);
    写I64("学习目标_当前稳定度总分", 输入.当前稳定度总分);
    写I64("学习目标_稳定度总分增量", 结果.稳定度总分增量);
    写I64("学习目标_待进化方法数", 输入.待进化方法数);
    写I64("学习目标_可用方法已增加", 结果.可用方法已增加 ? 1 : 0);
    写I64("学习目标_方法稳定性已提升", 结果.方法稳定性已提升 ? 1 : 0);
    写I64("学习目标_本轮已达成", (结果.可用方法已增加 || 结果.方法稳定性已提升) ? 1 : 0);
    写I64("学习目标_应继续进化", 结果.应继续进化 ? 1 : 0);
    if (结果.应继续进化
        && (头信息->状态 == 枚举_任务状态::未启动
            || 头信息->状态 == 枚举_任务状态::挂起
            || 头信息->状态 == 枚举_任务状态::完成
            || 头信息->状态 == 枚举_任务状态::失败
            || 头信息->状态 == 枚举_任务状态::无法执行
            || 头信息->状态 == 枚举_任务状态::待重筹办
            || 头信息->状态 == 枚举_任务状态::未定义)) {
        任务类::设置任务状态(
            学习任务头结点,
            枚举_任务状态::就绪,
            调用点 + "/任务继续进化");
    }
    else if (!结果.应继续进化
        && (头信息->状态 == 枚举_任务状态::就绪
            || 头信息->状态 == 枚举_任务状态::排队中)) {
        任务类::设置任务状态(
            学习任务头结点,
            枚举_任务状态::完成,
            调用点 + "/任务暂时完成");
    }

    结果.成功 = true;
    return 结果;
}
