结构_任务管理结果 任务管理器_v0::分解任务(
    任务节点类* 任务头结点,
    const 任务类::结构_创建步骤参数& 参数,
    const std::string& 调用点)
{
    结构_任务管理结果 结果{};
    auto* 头信息 = 取头信息_(任务头结点);
    if (!头信息) {
        结果.错误摘要 = "任务头结点无效";
        return 结果;
    }

    auto 步骤参数 = 参数;
    if (!步骤参数.所属任务头结点) {
        步骤参数.所属任务头结点 = 任务头结点;
    }

    auto* 步骤节点 = 任务类::创建步骤节点(任务头结点, 步骤参数, 调用点);
    if (!步骤节点) {
        结果.错误摘要 = "创建步骤节点失败";
        return 结果;
    }

    if (任务头结点->主信息) {
        (void)任务类::设置任务当前步骤(任务头结点->主信息, 步骤节点, 0, 调用点 + "/设置当前步骤");
    }

    结果.成功 = true;
    结果.需求 = 头信息->需求;
    结果.任务头结点 = 任务头结点;
    结果.步骤节点 = 步骤节点;
    return 结果;
}

bool 任务管理器_v0::绑定方法(
    任务节点类* 步骤节点,
    方法节点类* 方法首节点,
    bool 设为当前方法,
    const std::string& 调用点)
{
    auto* 步骤信息 = 取步骤信息_(步骤节点);
    if (!步骤信息 || !方法首节点) {
        日志::运行_错误("[任务管理_v0] 绑定方法失败：步骤或方法为空");
        return false;
    }

    {
        using namespace 数据仓库模块;
        锁调度器守卫 锁({
            锁请求::写(任务链.链表锁, 枚举_锁域::任务链, "任务链", 调用点)
            });

        auto& 方法列表 = 步骤信息->可用方法首节点列表;
        if (std::find(方法列表.begin(), 方法列表.end(), 方法首节点) == 方法列表.end()) {
            方法列表.push_back(方法首节点);
        }
        if (设为当前方法 || !步骤信息->当前选中方法首节点) {
            步骤信息->当前选中方法首节点 = 方法首节点;
        }
    }

    日志::运行f("[任务管理_v0] 绑定方法成功: step={}, method={}", (void*)步骤节点, (void*)方法首节点);
    return true;
}

bool 任务管理器_v0::开始任务(
    任务节点类* 任务头结点,
    const std::string& 调用点)
{
    auto* 头信息 = 取头信息_(任务头结点);
    if (!头信息) {
        日志::运行_错误("[任务管理_v0] 开始任务失败：任务头结点无效");
        return false;
    }

    auto* 步骤节点 = 取当前或首个步骤节点_(任务头结点);
    if (!步骤节点) {
        日志::运行_错误("[任务管理_v0] 开始任务失败：任务下没有可执行步骤");
        return false;
    }

    if (任务头结点->主信息) {
        (void)任务类::设置任务当前步骤(任务头结点->主信息, 步骤节点, 0, 调用点 + "/设置当前步骤");
    }
    任务类::设置任务状态(任务头结点, 枚举_任务状态::运行中, 调用点 + "/任务头运行");
    任务类::设置任务状态(步骤节点, 枚举_任务状态::运行中, 调用点 + "/步骤运行");
    return true;
}

结构_任务分解调制结果_v0 任务管理器_v0::计算任务分解调制(
    const 结构_元实体& 元任务实体,
    const 任务类::结构_创建步骤参数& 基础参数,
    const std::string& 调用点) const
{
    (void)调用点;
    结构_任务分解调制结果_v0 结果{};
    if (元任务实体.是否失效 || 元任务实体.类型 != 枚举_元实体类型::任务) {
        结果.错误摘要 = "元任务实体无效";
        return 结果;
    }

    const double 当前安全值 = 取数字字段_(元任务实体.内容, "当前安全值",
        取数字字段_(元任务实体.内容, "安全值", 50.0));
    const double 当前服务值 = 取数字字段_(元任务实体.内容, "当前服务值",
        取数字字段_(元任务实体.内容, "服务值", 0.0));
    const double 安全下限 = 取数字字段_(元任务实体.内容, "安全下限", 1.0);
    const double 安全回归中心 = 取数字字段_(元任务实体.内容, "安全回归中心", 50.0);
    const double 分母 = std::max(当前安全值 - 安全下限 + 1.0, 1.0);
    const double 风险权重 = std::clamp(1.0 / 分母, 0.0, 64.0);
    const double 安全门控 = [&]() -> double {
        if (当前安全值 <= 安全下限) return 0.0;
        if (安全回归中心 <= 安全下限) return 1.0;
        if (当前安全值 >= 安全回归中心) return 1.0;
        const double x = std::clamp((当前安全值 - 安全下限) / (安全回归中心 - 安全下限), 0.0, 1.0);
        return x * x * (3.0 - 2.0 * x);
    }();
    const double 服务缺口 = std::max(0.0, 50.0 - 当前服务值);

    auto 调制后步骤参数 = 基础参数;
    const bool 低安全 = 当前安全值 <= 安全下限 + 8.0;
    const bool 中安全 = !低安全 && 当前安全值 < 40.0;
    const bool 高服务压力 = 服务缺口 >= 20.0;

    结果.当前安全值 = 当前安全值;
    结果.当前服务值 = 当前服务值;
    结果.安全下限 = 安全下限;
    结果.风险权重 = 风险权重;
    结果.分解深度上限 = 低安全 ? 1 : (高服务压力 && 安全门控 > 0.6 ? 3 : 2);
    结果.允许探索新子任务 = (!低安全 && 安全门控 > 0.7 && 当前服务值 < 40.0);
    结果.执行范围收缩级别 = 低安全 ? 2 : (中安全 ? 1 : 0);
    结果.备用方法数量 = 低安全 ? 2 : (高服务压力 ? 2 : 1);
    结果.自检强度 = 低安全 ? 2 : (中安全 ? 1 : 0);

    if (调制后步骤参数.允许重试次数 <= 0) {
        调制后步骤参数.允许重试次数 = 低安全 ? 0 : (高服务压力 ? 2 : 1);
    }
    调制后步骤参数.允许切换方法 = true;
    调制后步骤参数.局部优先级偏移 += static_cast<std::int64_t>(std::llround((服务缺口 * 10.0) - (风险权重 * 100.0)));
    if (低安全 && 调制后步骤参数.初始状态 == 枚举_任务状态::就绪) {
        调制后步骤参数.初始状态 = 枚举_任务状态::筹办中;
    }

    结果.调制后步骤参数 = 调制后步骤参数;
    结果.成功 = true;
    return 结果;
}

