struct 结构_任务动作事件守卫 {
    任务信息基类* 主信息 = nullptr;
    const 词性节点类* 动作词 = nullptr;
    场景节点类* 输入场景 = nullptr;
    场景节点类* 输出场景 = nullptr;
    时间戳 开始时间 = 0;
    std::string 调用点{};
    bool 成功 = true;
    std::int64_t 错误码 = 0;

    结构_任务动作事件守卫(
        任务信息基类* mi,
        const 词性节点类* 动作,
        场景节点类* 输入,
        场景节点类* 输出,
        时间戳 now,
        std::string cp)
        : 主信息(mi)
        , 动作词(动作)
        , 输入场景(输入)
        , 输出场景(输出)
        , 开始时间(now ? now : 结构体_时间戳::当前_微秒())
        , 调用点(std::move(cp))
    {
        if (!主信息 || !动作词) return;
        (void)任务类::记录任务动作动态(
            主信息,
            动作词,
            输入场景,
            输出场景,
            true,
            0,
            开始时间,
            开始时间,
            枚举_动作事件相位::开始运行,
            调用点 + "/开始");
    }

    void 设置结果(bool ok, std::int64_t err = 0) noexcept
    {
        成功 = ok;
        错误码 = err;
    }

    ~结构_任务动作事件守卫()
    {
        if (!主信息 || !动作词) return;
        const 时间戳 结束时间 = 结构体_时间戳::当前_微秒();
        (void)任务类::记录任务动作动态(
            主信息,
            动作词,
            输入场景,
            输出场景,
            成功,
            错误码,
            结束时间,
            结束时间,
            成功 ? 枚举_动作事件相位::完成 : 枚举_动作事件相位::失败,
            调用点 + "/结束");
    }
};

任务头结点信息* 取头信息_(任务节点类* 节点) noexcept
{
    if (!节点 || !节点->主信息) return nullptr;
    return dynamic_cast<任务头结点信息*>(节点->主信息);
}

任务步骤节点信息* 取步骤信息_(任务节点类* 节点) noexcept
{
    if (!节点 || !节点->主信息) return nullptr;
    return dynamic_cast<任务步骤节点信息*>(节点->主信息);
}

任务节点类* 取当前或首个步骤节点_(任务节点类* 任务头结点) noexcept
{
    auto* 头信息 = 取头信息_(任务头结点);
    if (!头信息) return nullptr;
    if (头信息->当前步骤节点 && 取步骤信息_(头信息->当前步骤节点)) {
        return 头信息->当前步骤节点;
    }

    auto* first = static_cast<任务节点类*>(任务头结点 ? 任务头结点->子 : nullptr);
    if (!first) return nullptr;

    auto* cur = first;
    do {
        if (取步骤信息_(cur)) return cur;
        cur = static_cast<任务节点类*>(cur->下);
    } while (cur && cur != first);
    return nullptr;
}

std::string 取字段_(const 元字段表& 字段, std::string_view 键)
{
    if (const auto it = 字段.find(std::string(键)); it != 字段.end()) {
        return it->second;
    }
    return {};
}

double 取数字字段_(const 元字段表& 字段, std::string_view 键, double 默认值 = 0.0)
{
    const auto 值 = 取字段_(字段, 键);
    if (值.empty()) return 默认值;
    try {
        return std::stod(值);
    }
    catch (...) {
        return 默认值;
    }
}
