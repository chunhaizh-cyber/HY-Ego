词节点类* 语素类::生成语素节点(结构体_分词 分词信息)
{
    return 添加词性词(分词信息.词, 分词信息.词性);
}

词节点类* 语素类::添加词(const std::string& 词字符串) {
#ifdef _DEBUG
    if (词字符串.empty())
        MessageBox(0, _T(" 语素类::添加词  输入词字符串为空"), _T("逻辑错误"), MB_OK | MB_ICONERROR);
#endif

    词节点类* 游标 = 语素链.根指针;
    词节点类* 当前节点 = nullptr;
   
    std::string 前缀;
   

    for (std::size_t i = 0; i < 词字符串.size(); ) {
        const std::size_t n = utf8_char_len((unsigned char)词字符串[i]);
        前缀.append(词字符串, i, std::min(n, 词字符串.size() - i));
        i += n;

        词主信息类 临时主信息(前缀);
        当前节点 = 查找词(前缀);

    if (!当前节点) {
        词节点类* 新词节点 = new 词节点类();
        新词节点->主信息 = new 词主信息类(前缀);  // 这里确保“拷贝拥有字符串”，别存 char* / string_view 指向临时

        当前节点 = (游标 == 语素链.根指针)
            ? 语素链.添加节点(游标, 新词节点)
            : 语素链.添加子节点(游标, 新词节点);

        if (!当前节点) {
            delete 新词节点->主信息;
            delete 新词节点;
            return nullptr;
        }
    }

    游标 = 当前节点;
}

    return 当前节点;
}



词节点类* 语素类::查找词节点(const std::string& 词字符串) {
     if (词字符串.empty()) {
         throw std::invalid_argument("查找词节点 时, 词字符串 为空!");
     }
     return 查找词(词字符串); // 复用私有版“查找词(不创建)”
 }


词节点类* 语素类::查找词性节点(const 词节点类* 词节点, 枚举_词性 词性值) {
    if (!词节点 || !词节点->子) return nullptr;

    节点类* 当前节点 = 词节点->子;
    节点类* 起点 = 当前节点;

    do {
        // 检查当前节点的主信息是否为词性主信息类
        if (auto* 词性信息 = dynamic_cast<词性主信息类*>(当前节点->主信息)) {
            if (词性信息->词性 == 词性值) {
                return static_cast<词节点类*>(当前节点);
            }
        }

        当前节点 = 当前节点->下;
        if (!当前节点 || 当前节点 == 词节点->子) break; // 防止循环或破链

    } while (当前节点 != 起点);

    return nullptr;
    
 }
词节点类* 语素类::查找词性节点(const 节点类* 词节点, std::string 词性值)
{
    枚举_词性 词性 = 枚举_词性_工厂::根据文本获取枚举值(词性值);
    return 查找词性节点(词节点, 词性);
}
词节点类* 语素类::查找词性节点(结构体_分词 分词信息)
{
    词节点类* 词节点 = 添加词(分词信息.词);
    return 查找词性节点(词节点, 分词信息.词性);
}

词节点类* 语素类::获取词性词指针(const std::string& 词字符串)
{
    return 查找词(词字符串);
}
 

 // 在词节点中添加一个词性子节点
词节点类* 语素类::添加词性词(词节点类* 词节点, 枚举_词性 词性) {
     if (!词节点) {
         throw std::invalid_argument("添加词性词(词节点, 词性词) 时, 词节点 或 词性词 为空值!");
     }
     if (词性 == 枚举_词性::未定义) {
         std::string 参数 = "添加词性词(词节点, 词性词) 时,  词性 未定义 !" +枚举_词性_工厂::根据枚举类型获取文本(词性);
         throw std::invalid_argument(参数);
     }
     词性节点类* 词性节点= 查找词性节点(词节点, 词性);
     if (词性节点 != nullptr) return 词性节点;
     词性主信息类* 词性主信息 = new 词性主信息类(词性);
     词性节点 = new 词性节点类();
     词性节点->主信息 = 词性主信息;
     return  语素链.添加子节点(词节点, 词性节点);

     
 }


词节点类* 语素类::添加词性词(std::string 词值, std::string 词性值) {
    词节点类* 词指针 = 添加词(词值);
    枚举_词性 词性 = 枚举_词性_工厂::根据文本获取枚举值(词性值);
    return 添加词性词(词指针,词性); // 直接返回现有节点
}

词节点类* 语素类::添加词性词(结构体_分词 词信息) { return 添加词性词(词信息.词, 词信息.词性); }

const 词性节点类* 语素类::绑定词性词到场景基础信息(const std::string& 词字符串, 枚举_词性 词性, 场景节点类* 场景, 基础信息节点类* 基础信息节点)
{
    if (词字符串.empty() || !基础信息节点 || !场景) {
        throw std::invalid_argument("绑定词性词到场景基础信息: 词/基础信息/场景 为空!");
    }
    if (词性 == 枚举_词性::未定义) {
        throw std::invalid_argument("绑定词性词到场景基础信息: 词性未定义!");
    }

    // 1. 先获取 / 创建词节点
    词节点类* 词节点 = 添加词(词字符串);

    // 2. 再获取 / 创建词性节点
    const 词性节点类* 词性节点 = 查找词性节点(词节点, 词性);
    if (!词性节点) {
        词性节点 = 添加词性词(词节点, 词性); // 这里只建语素结构
    }

    // 3. 绑定到词性主信息中的“场景示例列表”
    auto* 主信息 = dynamic_cast<词性主信息类*>(词性节点->主信息);
    if (!主信息) {
        throw std::invalid_argument("绑定词性词到场景基础信息: 词性节点主信息类型错误!");
    }

    

    return 词性节点;
}

词节点类* 语素类::添加二次特征词(语素节点类* 参照物, 语素节点类* 比较对象, 语素节点类* 比较类型) { return nullptr; }
 int64_t 语素类::添加对应基础信息(const 词性节点类* 词节点, 基础信息节点类* 基础信息) {
     if (!词节点) throw std::invalid_argument("词节点不能为空");
     if (!基础信息)  throw std::invalid_argument("基础信息不能为空");
     auto* 词性主信息 = dynamic_cast<词性主信息类*>(词节点->主信息);
     if (!词性主信息)  throw std::runtime_error("词节点的主信息不是词性主信息类类型");
     词性主信息->对应基础信息指针 = 基础信息;
     return 1; // 成功
 }
 std::string 语素类::获取词(const 词性节点类* 词性词指针) {
     if (!词性词指针) {
         throw std::invalid_argument("获取词：词性词指针为空！");
     }
     if (!词性词指针->父) {
         throw std::invalid_argument("获取词：词性词指针没有父节点（词信息节点）！");
     }
     if (!词性词指针->父->主信息) {
         throw std::invalid_argument("获取词：词信息节点的主信息为空！");
     }
     auto* 词主信息 = dynamic_cast<词主信息类*>(词性词指针->父->主信息);
     if (!词主信息) {
         throw std::invalid_argument("获取词：父节点主信息不是词主信息类！");
     }
     return 词主信息->词;
 }

 // 纯查找：存在则返回节点，不存在返回 nullptr（不会创建）
 词节点类* 语素类::查找词(const std::string& 词字符串) {
     if (词字符串.empty()) return nullptr;
     if (!语素链.根指针) return nullptr;
     if (语素链.根指针->下 == 语素链.根指针) return nullptr;

     词节点类* 当前父节点 = 语素链.根指针;
     std::string 前缀;
     前缀.reserve(词字符串.size());

     for (std::size_t i = 0; i < 词字符串.size(); ) {
         std::size_t n = utf8_char_len((unsigned char)词字符串[i]);
         if (i + n > 词字符串.size()) n = 1; // 防御：不完整 UTF-8
         前缀.append(词字符串, i, n);
         i += n;

         词节点类* 命中节点 = nullptr;
         if (当前父节点 == 语素链.根指针) {
             for (auto* 游标 = 当前父节点->下; 游标 && 游标 != 当前父节点; 游标 = 游标->下) {
                 if (!游标->主信息) continue;
                 if (游标->主信息->主信息类型 != 枚举_主信息类型::词) continue;
                 auto* mi = dynamic_cast<词主信息类*>(游标->主信息);
                 if (mi && mi->词 == 前缀) {
                     命中节点 = static_cast<词节点类*>(游标);
                     break;
                 }
             }
         }
         else {
             auto* 首子节点 = 当前父节点->子;
             if (!首子节点) return nullptr;
             auto* 游标 = 首子节点;
             do {
                 if (游标->主信息 && 游标->主信息->主信息类型 == 枚举_主信息类型::词) {
                     auto* mi = dynamic_cast<词主信息类*>(游标->主信息);
                     if (mi && mi->词 == 前缀) {
                         命中节点 = static_cast<词节点类*>(游标);
                         break;
                     }
                 }
                 游标 = 游标->下;
             } while (游标 && 游标 != 首子节点);
         }

         if (!命中节点) return nullptr;
         当前父节点 = 命中节点;
     }
     return 当前父节点 == 语素链.根指针 ? nullptr : 当前父节点;
 }

 枚举_主信息类型 语素类::映射_词性到主信息类型(枚举_词性 词性) {
     switch (词性) {
         // 原有映射规则
     case 枚举_词性::n:
     case 枚举_词性::名词:
     case 枚举_词性::专有名词:
     case 枚举_词性::nr:    // 人名
     case 枚举_词性::PER:   // 人名
     case 枚举_词性::ns:    // 地名
     case 枚举_词性::LOC:   // 地名
     case 枚举_词性::nt:    // 机构团体名
     case 枚举_词性::ORG:   // 机构名
     case 枚举_词性::nw:    // 作品名
     case 枚举_词性::nz:    // 其他专名
         return 枚举_主信息类型::存在信息;

     case 枚举_词性::代词:
     case 枚举_词性::r:     // 代词
         return 枚举_主信息类型::存在信息_指代;

     case 枚举_词性::动词:
     case 枚举_词性::v:     // 普通动词
     case 枚举_词性::vd:    // 动副词
     case 枚举_词性::vn:    // 名动词
         return 枚举_主信息类型::动态信息;

     case 枚举_词性::形容词:
     case 枚举_词性::a:     // 形容词
     case 枚举_词性::ad:    // 副形词
     case 枚举_词性::an:    // 名形词
         return 枚举_主信息类型::特征信息;

     case 枚举_词性::副词:
     case 枚举_词性::d:     // 副词
         return 枚举_主信息类型::二次特征_修饰;

     case 枚举_词性::数词:
     case 枚举_词性::量词:
     case 枚举_词性::m:     // 数量词
     case 枚举_词性::q:     // 量词
         return 枚举_主信息类型::特征信息_数量;

     case 枚举_词性::介词:
     case 枚举_词性::连词:
     case 枚举_词性::p:     // 介词
     case 枚举_词性::c:     // 连词
     case 枚举_词性::u:     // 助词
     case 枚举_词性::xc:    // 其他虚词
         return 枚举_主信息类型::关系信息;

         // 新增时间类映射
     case 枚举_词性::时间词:
     case 枚举_词性::t:     // 时间名词
     case 枚举_词性::TIME:  // 时间
         return 枚举_主信息类型::动态信息;  // 时间词通常与动作/状态的时间属性相关

         // 其他未明确分类的词性
     default:
         return 枚举_主信息类型::语言信息_仅记录;
     }
 }

